<h3><span>PART TWO</span> The Sea-cook</h3>
<div>

<p>
So the weeks passed on, till one fine day
there came a letter addressed to Dr. Livesey, with
the addition “To be opened, in the case of his
absence, by Tom Redruth or young Hawkins”.
I found the following important news:</p><p> </p><p>



		Dear Livesey,</p><p>
		The ship is bought and fitted, and ready for</p><p>
	sea. The name, Hispaniola. I got it through my old</p><p>
	friend, Blandly. Everyone here in Bristol helped us</p><p>
	when they heard where we wanted to go — for</p><p>
	treasure, I mean.	</p><p> </p><p>



“Redruth,” said I, “Dr. Livesey will not like
that. The squire was talking, after all.”
I read straight on:</p><p> </p><p>



		Blandly himself found the Hispaniola. But some</p><p>
	people say that the Hispaniola belonged to him,</p><p>
	and that he sold it me absurdly high.</p><p>
		I found an old sailor, he knew all the seamen</p><p>
	in Bristol, and wanted to get to sea again. His</p><p>
	name is Long John Silver, and he has lost a</p><p>
	leg. With the help of Silver I gathered a company</p><p>
	of real sea dogs.</p><p>
		So now, Livesey, there’s no time to lose. Let</p><p>
	young Hawkins go at once to see his mother and</p><p>
	then come quickly to Bristol.</p><p>
		John Trelawney.</p><p>
		PS: Blandly has found us an excellent man to</p><p>
	be captain. His name is Smollett.</p><p> </p><p>



The next day, after dinner, Redruth and I
were afoot again and on the road. I said goodbye
to Mother and the cove where I lived, and
the dear old Admiral Benbow. Next moment we 
had turned the corner, and my home was out
of sight. Soon we found ourselves in Bristol.
I was going to sea myself, to sea in a schooner,
with real seamen, to sea, and to seek for buried
treasure! While I was still in this delightful
dream, we came suddenly in front of a large inn
and met Squire Trelawney, all dressed out like
a sea-officer, in stout blue cloth, coming out of
the door with a smile on his face.</p><p>
“Here you are,” he cried, “and the doctor
came last night from London. Bravo! The ship’s
company complete!”</p><p>
“Oh, sir,” cried I, “when do we sail?”</p><p>
“Sail!” said he. “We sail tomorrow!”
The squire gave me a note addressed to
John Silver, at the sign of the Spy-glass. The
sign was newly painted; the windows had red
curtains. The customers were mostly seamen,
and they talked very loudly.</p><p>
As I was waiting, a man came out of a side
room, and I was sure he must be Long John.
His left leg was cut off close by the hip, and
under the left shoulder he carried a crutch. He
was very tall and strong, with a face as big as
a ham — plain and pale, but intelligent and
smiling.</p><p>
One look at the man before me was enough.
I saw the captain, and Black Dog, and the blind
man, Pew, and I thought I knew what a pirate
was like — a very different creature, according
to me.</p><p>
“Mr. Silver, sir?” I asked. </p><p>
“Yes, my lad,” said he, “such is my name,
to be sure. And you are our new cabin-boy;
pleased I am to see you.”</p><p>
Just then one of the customers at the far
side rose suddenly and made for the door. I
recognized him! It was the tallow-faced man who
came first to the Admiral Benbow.</p><p>
“Oh,” I cried, “stop him! It’s Black Dog!”</p><p>
“I don’t care who he is,” cried Silver. “But he
hasn’t paid his score. Harry, run and catch him.”
One of the others who was nearest the door
leaped up and started in pursuit.</p><p>
“Who did you say he was?” asked Silver.</p><p>
“Black what?”</p><p>
“Dog, sir,” said I. “Has Mr. Trelawney not
told you of the pirates? He was one of them.”</p><p>
“So?” cried Silver. “In my house! Ben, run
and help Harry. Black Dog? No, I don’t know
the name, not I. He used to come here with a
blind beggar.”</p><p>
“I knew that blind man too,” said I. “His
name was Pew.” By the time the two men came back
and said that they lost Black Dog in a crowd.</p><p>
“I hate these pirates,” said Long John. “It’s
very serious. I’ll go with you to tell Mr. Trelawney
what has happened.”</p><p>
So he did. Long John told the story from
first to last. The two gentlemen regretted that
Black Dog got away, but what to do? Long John
took his crutch and left us.</p><p>
“All the crew must be aboard by four this
afternoon,” said Mr. Trelawney.
The Hispaniola lay some way out. The captain
seemed angry with everything on board.</p><p>
“I don’t like this cruise,” said the captain.</p><p>
“I don’t like the men.”</p><p>
“Possibly, sir, you may not like your employer,
either?” said the squire.</p><p>
“Stay a bit,” said Dr. Livesey. “No use of such
questions. I require an explanation. You don’t, you
say, like this cruise, captain. Why?”</p><p>
“I find that every man knows more than I
do,” said the captain. “I learn we are going after
treasure. I don’t like treasure voyages at all, and
I don’t like them when they are secret and when
(begging your pardon, Mr. Trelawney) the secret
has been told to the parrot.”</p><p>
“Silver’s parrot?” asked the squire.</p><p>
“It’s a way of speaking,” said the captain.
“That is all clear, and true enough,” replied
Dr. Livesey. “We take the risk, but we are not
 so ignorant as you think. Next, you say you
don’t like the crew. Are they not good seamen?”</p><p>
“I don’t like them, sir,” returned Captain
Smollett.</p><p>
“Well, now, tell us what you want, captain,”
asked the doctor.</p><p>
“They are putting the powder and the arms
in the fore hold,” said the captain. “Now, you
have a good place under the cabin; why not
put them there? I’ll tell you what I’ve heard
myself,” continued Captain Smollett, “that you
have a map of an island, that there’re crosses
on the map to show where treasure is, and that
the island lies —” And then he named the latitude
and longitude exactly.</p><p>
“I never told that,” cried the squire, “to a
soul! Livesey, that must be you or Hawkins!”</p><p>
“Well, gentlemen,” continued the captain, “I
don’t know who has this map, but I think it
needs to be kept secret. I am responsible for
the ship’s safety and the life of every man. And
that’s all.”</p><p>
The Hispaniola has begun its voyage to the
Isle of Treasure. I am not going to relate that
voyage in detail. It was prosperous. The ship
proved to be a good ship, the crew were capable
seamen, and the captain understood his
business. As for Long John Silver, the men
called him Barbecue.</p><p>
“He’s no common man, Barbecue,” said the
coxswain to me. “He can speak like a book;
and brave — like a lion!”</p><p>
All the crew respected and even obeyed him.
To me he was very kind, and always glad to
see me.</p><p>
“Come away, Hawkins,” he would say.</p><p>
“Nobody is more welcome than yourself,
my son. Sit down and hear the news.</p><p>
Here’s Captain Flint — I call my parrot Captain
Flint, after the famous pirate — here’s Captain
Flint predicting success to our voyage.” 
And the parrot would say, with great rapidity,
“Pieces of eight! Pieces of eight! Pieces
of eight!” till John threw his handkerchief over
the cage.</p><p>
“Now, that bird,” he would say, “is, maybe,
two hundred years old, Hawkins — they live
forever.”</p><p>
Every man on board seemed content. Always
a barrel of apples was standing on the deck.
And you will see, it saved us.</p><p>
Just after sundown, when all my work was
over and I was on my way to my bed, I decided
to eat an apple. I ran on deck and found that
there was only one apple left. Suddenly I heard
Silver’s voice. I decided not to show myself. And
later I realized that the lives of all the honest
men aboard the ship depended upon me alone.</p><p>
“No, not I,” said Silver. “Flint was a captain;
I was a quartermaster. I’m fifty. Gentlemen of
fortune usually trust little among themselves, and
right they are. Now, here’s what I say: Captain
Smollett is a first-class seaman; he sails the ship
for us. Mr. Trelawney and the doctor have the
map. I don’t know where it is. No more do you.
Well then, I mean this squire and doctor will
find the treasure, and help us to get it aboard.</p><p>
I’ll finish with them at the island.”</p><p>
“What will you do with them?” asked a sailor.</p><p>
“Dead men don’t bite. I’m an easy man — I’m
quite the gentleman. I give my vote — death.”</p><p>
“John,” cried the coxswain, “you’re a man!”
And they went away.</p><p>
You can imagine my horror! Almost at the
same time the voice of the lookout shouted,
“Land!”</p><p>
I quickly got out of the barrel to join Hunter
and Dr. Livesey. And then I heard the voice of
Captain Smollett issuing orders.</p><p>
“And now, men,” said the captain, “has any
one of you ever seen that land ahead?”</p><p>
“I have, sir,” said Silver. “They call it Skeleton
Island. That hill to the northward they call the
Fore-mast Hill; there are three hills in a row
running southward. But the main — with the
cloud on it — they usually call the Spy-glass.
It’s there they cleaned their ships, sir.”</p><p>
“I have a chart here,” said Captain Smollett.</p><p>
“See if that’s the place.”</p><p>
Long John’s eyes burned in his head as he
took the chart, but he was disappointed. This was
not the map we found in Billy Bones’s chest,
but an accurate copy, complete in all things —
names and heights — with the single exception
of the red crosses and the written notes.</p><p> 
“Yes, sir,” said he, “this is the spot. Who
has done that, I wonder? The pirates were too
ignorant.”</p><p>
“Ah,” said Silver to me, “this here is a sweet
spot, this island — you’ll bathe, and you’ll climb
trees, and you’ll hunt goats. It’s a pleasant thing
to be young and have ten toes.”</p><p>
Captain Smollett, the squire, and Dr. Livesey
were talking together on the quarter-deck. I was
very anxious to tell them my story. Dr. Livesey
called me. I said immediately, “Doctor, I have
terrible news.”</p><p>
The three gentlemen went below. I found
them all three seated round the table, a bottle
of Spanish wine and some raisins before them.
“Now, Hawkins,” said the squire, “you have
something to say. Speak up.”</p><p>
I began. Nobody interrupted me, but they
kept their eyes upon my face.</p><p>
“First point,” began Mr. Smollett. “We must
go on, because we can’t turn back.” </p><p>
There were only seven out of the twenty-six
on whom we knew we could rely; and out of
these seven one was a boy.</p>

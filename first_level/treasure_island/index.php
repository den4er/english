<h3><span>PART THREE</span> My Shore Adventure</h3>
<div>


<p>
There was no sign of any wind. Everybody
was glad to get to land. The bottom was clean
sand. We held a council in the cabin.
“Sir,” said the captain, “We’ve only one man
to rely on.”</p><p>
“And who is that?” asked the squire.</p><p>
“Silver, sir,” returned the captain. “Let’s allow
them to go ashore. If they all go, we’ll get
the ship.”</p><p>
I believe the silly sailors thought they would
find the treasure as soon as they were landed.
Silver was the captain. Six fellows were to stay
on board, and the remaining thirteen, including 
Silver, began to embark. And at that moment it
occurred to me to go ashore with the others. It
was an idea that helped to save our lives.
When our boat reached the shore, I jumped
out and ran away, while Silver and the rest were
still a hundred yards behind.</p><p>
“Jim, Jim!”</p><p>
But I paid no attention, I ran straight till I
could run no longer.</p><p>
I now felt for the first time the joy of exploration.
Here and there were flowering plants,
unknown to me; here and there I saw snakes.
Soon I heard human voices, which grew
louder and nearer. This put me in a great fear,
and I crawled under cover of the nearest oak,
as silent as a mouse.</p><p>
Long John Silver and another of the crew
stood face to face in conversation. Silver threw
his hat beside him on the ground, and his great
face was lifted to the other man’s.</p><p>
“Tom,” he was saying, “I’m warning you that
the men will kill you if you don’t help us.”
I realized that I found an honest man in Tom!</p><p>
“John!” said the sailor, “Kill me, if you can.
I’m not afraid.”</p><p>
And with that, this brave fellow turned his
back directly on the cook. But did not go far.
With a cry John took his heavy crutch from
under his arm and threw it at Tom. It struck
poor Tom right between the shoulders, in the
middle of his back, and he fell to the ground.
 John put his hand into his pocket, brought
out a whistle, and blew upon it. I could not
tell, of course, the meaning of the signal, but it
instantly awoke my fears. More men will come.
I can be discovered. They can kill me. Instantly
I began to crawl back again, with the speed
and silence I could manage.</p><p>
It was all over, I thought. Good-bye to the
Hispaniola; good-bye to the squire, the doctor,
and the captain! There was nothing left for me
but death by starvation or death by the hands
of the pirates. I ran as I never ran before.
Suddenly I saw a figure. What was it — a
bear or a man or a monkey? Silver himself
appeared less terrible in contrast with this
creature of the woods. No doubt: it’s a cannibal!
I began to recall what I heard of cannibals.
I stood still; this man of the island advanced
towards me.</p><p>
“Who are you?” I asked.</p><p>
“Ben Gunn,” he answered, and his voice
sounded awkward. “I’m poor Ben Gunn, and I
haven’t spoken to a man for three years.”</p><p>
I could now see that he was a white man
like myself. His skin was burnt by the sun; even
his lips were black. He was clothed with tatters
of old ship’s canvas and old sea-cloth.</p><p>
“Three years!” I cried. “Were you shipwrecked?”</p><p>
“No,” said he, “marooned. Marooned three
years ago, and lived on goats since then, and
berries, and oysters. But do you have a piece of
cheese, now? No? Well, I’ve dreamed of cheese!” </p><p>
“If ever I can get aboard again,” said I, “you
will have cheese.”</p><p>
“What do you call yourself?”</p><p>
“Jim,” I told him.</p><p>
“Jim, Jim,” said he, quite pleased apparently.</p><p>
“Well, Jim, I’m rich. Rich! Rich! And I’ll make
you rich, too, Jim. Now tell me the truth: is
that the Flint’s ship?” he asked.
I began to believe that I found an ally, and
I answered him at once.</p><p>
“It’s not Flint’s ship, and Flint is dead; but
there are some of Flint’s men aboard.”</p><p>
“Not a man with one leg?”</p><p>
“Silver?” I asked.</p><p>
“Ah, Silver!” said he. “That was his name.”</p><p>
“He’s the cook, and the leader too.”</p><p>
“You’re a good lad, Jim,” he said. “Well, just
believe Ben Gunn. I was in Flint’s ship when he
buried the treasure, he and six strong seamen.
He killed them all. I was in another ship three
years ago, and we got to this island. ‘Boys,’ said
I, ‘here’s Flint’s treasure; let’s land and find it.’
Twelve days we looked for it. Finally they said,</p><p>
‘You can stay here and find Flint’s money for
yourself.’” “Well,” I said, “but how am I to get on
board?”</p><p>
“Ah,” said he, “I have a boat that I made
with my two hands. I keep it under the white
rock. But what’s that?”</p><p>
We heard shots.</p><p>
“They have begun to fight!” I cried. “Follow
me.”</p>
</div>


<h3><span>PART FOUR</span> The Stockade</h3>
<div>

<p>
The captain, the squire, and I were sitting
in the cabin. Jim Hawkins slipped into a boat
and went ashore with the pirates. It was decided
that Hunter and I would go ashore, too.
Suddenly we heard a terrible cry and my first
thought was, ‘Jim Hawkins is dead’.
The squire was sitting down, as white as
a sheet. I told my plan to the captain, and
between us we settled on the details of its accomplishment.
We put guns, food, powder, and 
my medicine chest in the boat. The loaded boat
made its first trip to the stockade. We hid it in
the island, in a log-house, behind the stockade.
The boat came back and we put the rest of the
arms, powder, pork, and bread-bags in it. The
boat made four trips.</p><p>
The fifth trip was quite different from any
of the others. In the first place, the boat was
overloaded. Five men, and three of them —
Trelawney, Redruth, and the captain — over
six feet high, that was already too much. Add
to that the powder, pork, and bread-bags. We
were afraid to breathe.</p><p>
In the second place, the current was sweeping
us out of our true course and away from our
proper landing-place.</p><p>
“I cannot keep its head for the stockade, sir,”
said I to the captain. “We’ll never get ashore,”
said I.</p><p>
“We must keep upstream,” returned the
captain.</p><p>
Suddenly the captain spoke up again, and I
thought his voice was a little changed.</p><p>
“The gun!” said he.</p><p>
We have forgotten the gun. 
Suddenly the ship’s gun fired. It didn’t hit our
boat but it turned it over. We found ourselves
in shallow water and we easily got to the beach.
There was no great harm. No lives were lost,
and we could reach ashore in safety. But we left
behind us the poor jolly-boat and a good half
of all our powder and food.</p><p>
We began to rejoice over our good success
when just at that moment a pistol cracked in
the bush, and poor Tom Redruth stumbled and
fell on the ground.</p><p>
Poor old fellow, he followed every order
silently; he was the oldest of our party, good
servant. The squire dropped down beside him
on his knees and kissed his hand, crying like
a child.</p><p>
“Tom, my man,” said I, “you’re going home.”
In the meantime the captain took out the
British flag, and with the help of Hunter he set
it up at the corner of the log-house.
Then he pulled me aside.</p><p>
“Dr. Livesey,” he said, “in how many weeks
do you and squire expect the consort?”
I told him it was a question not of weeks
but of months. “As for powder and shot, we’ll do. But the
rations are short, very short.”</p><p>
The captain sat down to write his record of
events:</p><p> </p><p>



		‘Alexander Smollett, captain; David Livesey, ship’s</p><p>
	doctor; Abraham Gray; John Trelawney, owner; John</p><p>
	Hunter, Richard Joyce, owner’s servants — came</p><p>
	ashore this day and flew the British flag on the</p><p>
	log-house on Treasure Island. Thomas Redruth, owner’s</p><p>
	servant, shot by the enemy. James Hawkins —’</p><p> </p><p>


And at the same time, I was wondering over
poor Jim Hawkins’ fate.</p><p>
“Somebody hailing us,” said Hunter, who
was on guard.</p><p>
“Doctor! Squire! Captain! Hello, Hunter, is
that you?” came the cries.</p><p>
And I ran to the door in time to see Jim
Hawkins, safe and sound.</p><p>
As soon as Ben Gunn saw the British flag,
he sat down.</p><p>
“Now,” said he, “your friends, sure enough.
Silver would raise the Jolly Roger. Your friends
are ashore in the old stockade, as was made
years and years ago by Flint.”</p><p>
“Well,” said I, “that may be so, let’s hurry
on and join my friends.”</p><p>
“Not me,” returned Ben. “When Ben Gunn
is wanted, you know where to find him, Jim.
Just where you found him today.”</p><p>
“Well,” said I, “I believe I understand. You
have something to propose, and you wish to
see the squire or the doctor. Good, and now
may I go?” “You won’t forget?” he inquired anxiously.</p><p>
“I think you can go, Jim.”</p><p>
The sun set, the sea breeze was rustling.
The Hispaniola still lay where it anchored; but
there was the Jolly Roger — the black flag of
piracy — flying from her peak.</p><p>
I thought I might return towards the stockade.
Soon I was warmly welcomed by my friends.
I told my story and began to look around.</p><p>
The log-house was made of trunks of pine —
roof, walls, and floor. There was a porch at the
door, and under this porch the little spring welled
up into an artificial basin. In one corner there
was an old rusty iron basket to contain the fire.
The cold evening breeze, of which I have
spoken, whistled through every chink of the rude
building and sprinkled the floor with a continual
rain of fine sand. There was sand in our eyes, sand
in our teeth, sand in our suppers. Our chimney
was a square hole in the roof.</p><p>
From time to time the doctor came to the
door for a little air and to rest his eyes. Before
supper was eaten we buried old Tom in the sand.
 I was dead tired; and when I got to sleep,
I slept like a log. It was a cry of surprise that
woke me the next morning.</p><p>
“Flag of truce! Silver himself!”
I jumped, and rubbing my eyes, ran to a
loophole in the wall.</p><p>
There were two men just outside the stockade,
one of them waving a white cloth, the other —
Silver himself, standing by.
It was still quite early. The sky was bright
and cloudless overhead. But where Silver stood
with his lieutenant, all was still in shadow.</p><p>
“Keep indoors, men,” said the captain.
Then he hailed the pirate.</p><p>
“Who goes? Stand, or we fire.”</p><p>
“It’s Captain Silver,” was the reply.</p><p>
“Captain Silver? I don’t know him. Who’s
he?”</p><p>
“Me, sir,” answered Long John for himself.</p><p>
“They chose me to be their captain.”</p><p>
“And what do you want with your flag of
truce?” the captain cried. “I have not the slightest
desire to talk to you. If you wish to talk to
me, you can come, that’s all.”</p><p>
“That’s enough, captain,” shouted Long John.</p><p>
“A word from you is enough.” </p><p>
Silver came to the stockade, threw over his
crutch, got a leg up, and with great skill dropped
safely to the other side.</p><p>
“Well, here it is,” said Silver. “We want that
treasure, and we’ll have it — that’s our point!
You have a chart, haven’t you?”</p><p>
“Maybe,” replied the captain.</p><p>
“Oh, well, you have, I know that,” returned
Long John. “What I mean is, we want your
chart. Now, here it is. You give us the chart.
Do it and you’ll save your lives.”</p><p>
Captain Smollett rose from his seat.</p><p>
“Is that all?” he asked. “Very good, now
you’ll hear me. If you come up one by one, unarmed,
I’ll take you home to a trial in England.</p><p>
You won’t find the treasure. You can’t sail the
ship. You can’t fight us. I stand here and tell
you so; and they’re the last good words you’ll
get from me, for I’ll put a bullet in your back
when next I meet you.”</p><p>
Silver’s face was a picture; his eyes started
in his head with wrath. He shook the fire out
of his pipe. Then he spat into the spring.
“There!” he cried. “That’s what I think of you.”
As soon as Silver disappeared, the captain
went round to check that everything was ready
for the fight.</p><p>
The captain looked on for a while in silence.
Then he spoke.</p><p>
“Doctor, you will take the door,” he said.</p><p>
“See, and don’t expose yourself; keep within,
and fire through the porch. Hunter, take the east
side, there. Joyce, you stand by the west, my
man. Mr. Trelawney, you are the best shot —
you and Gray will take this long north side; it’s
there the danger is.”</p><p>
An hour passed away. And just at that moment
came the first news of the attack.</p><p>
“Sir,” said Joyce, “if I see anyone, am I to
fire?”</p><p>
“I told you so!” cried the captain.
Several bullets struck the log-house, but not
one entered; the stockade and the woods around
it looked as quiet and empty as before.</p><p>
 We didn’t have much time left for thought.
Suddenly, with a loud shout, several pirates appeared
from the woods on the north side and
ran straight to the stockade. At the same time
fire opened from the woods.</p><p>
The pirates swarmed over the fence like monkeys.
Squire and Gray fired again and yet again.
At the same moment, another pirate grasped
Hunter’s musket, wrenched it from his hands,
and hit our friend with it.</p><p>
“At them, all hands, all hands!” roared the
captain in a voice of thunder.</p><p>
The log-house was full of smoke.</p><p>
“Out, lads, out, and fight them in the open!”
cried the captain.</p><p>
Someone was close behind, I knew not whom.
“Round the house, lads! Round the house!”
cried the captain.</p><p>
I had not time to be afraid. Right in front of
me the doctor was running after a pirate down
the hill. He caught the man and hit him across 
his face. The pirate died at once. And at that
moment the fight was over and victory was ours.
“The captain’s wounded,” said Mr. Trelawney.
“Have they run?” asked Mr. Smollett.</p><p>
“All that could,” returned the doctor, “but
there’s five of them will never run again.”
“Five!” cried the captain. “Come, that’s better.
Five against three leaves us four to nine. We
were seven to nineteen.”</p>
</div>


<h3><span>PART FIVE</span> My Sea Adventure</h3>
<div>


<p>
As for the captain, his wounds were not
dangerous. No organ was injured.
After dinner the squire and the doctor sat
by the captain’s side and talked. Afterwards, the
doctor stood up, took his pistols, put the map
in his pocket, crossed the stockade on the north
side and went quickly.</p><p>
Gray and I were sitting together at the far
end of the block house.</p><p>
“Is Dr. Livesey mad?” asked he.</p><p>
“No,” said I. “The doctor has his idea; and if
I am right, he’s going now to see Ben Gunn.”</p><p>
I was right, as appeared later. All the time I
was washing out the block house. I was a fool,
 if you like, and certainly I was going to do a
foolish act. But I was only a boy.</p><p>
I filled both my coat pockets with biscuits
and took two pistols and powder for them. The
squire and Gray were busy helping the captain,
the coast was clear. I climbed over the stockade
and ran away.</p><p>
I took my way straight for the east coast of
the island. It was already late in the afternoon,
although still warm and sunny. I have never seen
the sea quiet round Treasure Island.</p><p>
Behind me was the sea, in front the anchorage.
I found the Ben Gunn’s boat. The little boat, a
strange thing made of wood and goat-skin, was
hidden among the bushes. I lifted the small boat
onto my shoulders and carried it to the water. I
decided to come to the Hispaniola and cut the
anchor ropes.</p><p>
The coracle was a very safe boat for a
person of my height and weight. Suddenly the
wind blew and caught the Hispaniola and my
little boat with it. I glanced over my shoulder.
There, right behind me, was the glow of the
camp-fire. In a moment we were both sailing
fast into the open sea.</p><p>
I lay down flat in the bottom of the boat.
I was sure we would crash against the rocks.
Each new wave carried the threat of death. I
lay helpless for many hours. At last I became
very tired and I fell asleep. I lay and dreamed
of home and the old Admiral Benbow.</p><p>
It was broad day when I awoke and found
myself tossing at the south-west end of Treasure
Island. The sun was up but was still hid from
me behind the Spy-glass.</p><p>
I remembered what Silver had about the
current that set northward along the whole west
coast of Treasure Island.</p><p>
There was a great, smooth swell upon the
sea. I was terrified. The coracle, left to itself,
was turning from side to side.</p><p>
Well, now, thought I to myself, I must lie
where I am and not disturb the balance. So I
lay on my elbows. I began to be tortured with
thirst. The glow of the sun from above, the
sea-water that fell upon me, made my throat
burn and my brain ache.</p><p>
Right in front of me, not half a mile away,
I saw the Hispaniola. I was so distressed that I
did not know whether to be glad or sorry.
The Hispaniola was under her main-sail, and
the beautiful white canvas shone in the sun like
snow or silver. But soon I understood that nobody led it.
And if so, where were the men? Either they
were dead drunk or deserted it, I thought.
The current was bearing coracle and schooner
southward at an equal rate.</p><p>
I sailed closer. The Hispaniola turned again
and then again and again. The ship turned
to the north, to the south, to the east, to the
west. For some time the ship did the worse
thing possible for me — stood still. Then it was
coming closer and closer to my boat. I jumped
up and caught one of the ship’s ropes. Just at
that time the ship crashed the little boat. Ben
Gunn’s boat went down under the water, and
I was left on the Hispaniola.</p><p>
Suddenly the Hispaniola came right into the
wind. There were the two watchmen. The first
one was lying on his back. His chin was on his
chest, his hands were lying open before him on
the deck, his face was very white. The second
was also lying. It was Israel Hands.</p><p>
All he could do was to utter one word,
“Brandy.”</p><p>
There was no time to lose, I slipped down
the companion stairs into the cabin. All the
lockers were broken open in quest of the chart.
The floor was thick with mud. Dozens of empty
bottles clinked together in corners. One of the
doctor’s medical books lay open on the table.
I went into the cellar; all the barrels were
gone.</p><p>
I found a bottle with some brandy left, for
Hands; and for myself I took some biscuit, some
fruits, and a piece of cheese. With these I came on
 deck, had a good deep drink of water, and
then gave Hands the brandy.</p><p>
“Aye,” said he, “I wanted some of that!”
I sat down in my own corner and began to
eat.</p><p>
“Much hurt?” I asked him.</p><p>
“If that doctor was aboard,” he said, “I’d
recover very soon. Dead men don’t bite. And
where have you come from?”</p><p>
“Well,” said I, “I’ve come aboard to take
possession of this ship, Mr. Hands; and you’ll
please regard me as your captain until further
notice.”</p><p>
He looked at me sourly enough but said
nothing. Some of the colour came back into his
cheeks, though he still looked very sick.
“I don’t like this flag,” I continued. “Better
none than this.”</p><p>
I ran to the Jolly Roger, handed it down,
and chucked it overboard. </p><p>
“I think, Captain Hawkins,” said he, “you
want to get ashore now.”</p><p>
“Why, yes,” said I, “with all my heart, Mr.
Hands.” And I went back to my meal with a
good appetite.</p><p>
“Now, look here, you give me food and drink
and tie my wound up, you do, and I’ll tell you
how to navigate a vessel.”</p><p>
Well, as it seemed to me, there was some
sense in this. In three minutes the Hispaniola
was sailing easily before the wind along the
coast of Treasure Island.</p><p>
The coxswain told me how to navigate; after
many trials I succeeded.</p><p>
“Captain,” said he with the uncomfortable
smile, “here’s my old shipmate, O’Brien; heave
him overboard, will you?”</p><p>
“I’m not strong enough, and I don’t like the
job,” said I.</p><p>
“Well, will you get me a bottle of wine, Jim —
this brandy is too strong for my head.”
He wanted me to leave the deck; but with
what purpose I could not imagine. His eyes
never met mine.</p><p>
I went down with as much noise as I could,
then took off my shoes and ran quietly back
to watch him.</p><p>
Israel Hands had risen on to his hands and
knees. He crept along the deck to where some
ropes lay, took out a long knife that was hidden
under them, quickly put it in his pocket
and moved back. This was what I wanted to
know: he could move about and he was armed.
I came to him. “Well, my Captain,” he said, “there’s the
north harbour over there. It won’t be easy to
sail the ship in because it’s narrow. But if you
follow my orders we’ll manage.”</p><p>
So he issued his commands, which I obeyed.</p><p>
I was still so much interested, that I forgot the
peril that hung over my head. Perhaps I noticed
his shadow moving with the tail of my eye;
perhaps it was an instinct like a cat’s; but, sure
enough, when I looked round, there was Hands
with the dirk in his right hand.</p><p>
We cried out aloud when our eyes met. At
the same instant, he threw himself forward and
I leapt sideways. As I did so, I let go of the
tiller, and I think this saved my life, for it
struck Hands across the chest and stopped him.
Before he could recover, I was safe out of
the corner. It was wonderful how fast he could
move. Suddenly the Hispaniola struck ground for
an instant in the sand. Both of us rolled, almost
together. Israel Hands stood with his mouth open,
a perfect statue of surprise and disappointment.
He began to move. I took the pistols. </p><p>
“One more step, Mr. Hands,” said I, “and
I’ll kill you. Dead men don’t bite, you know,”
I added with a chuckle.</p><p>
He stopped instantly. I laughed aloud. Something
sang like an arrow through the air; I felt
sharp pain in my shoulder. My pistols shot, and
both escaped out of my hands. They did not
fall alone; the coxswain plunged head first into
the water.</p><p>
I began to feel sick, faint, and terrified. The
hot blood was running over my back and chest.
The dirk, where it pinned my shoulder to the
mast, seemed to burn like a hot iron.
I went below and did what I could for my
wound; it pained me, but it was neither deep
nor dangerous. I looked around me, the ship
was now my own.</p><p>
The sun was about to set.</p><p>
I was now alone upon the ship.</p><p>
I decided to quit the ship, leaving the Hispaniola
on her side. I lowered the sails, climbed
down a rope and dropped overboard. The water
wasn’t deep and I walked ashore.</p><p>
The way brought me near to where I met Ben
Gunn. I went on. At last I came to the house.
I stopped, with much wonder in my heart, and
perhaps a little terror also. I crawled, without a
sound, towards the corner of the house. I got
to the door and stood up. All was dark within,
so that I could distinguish nothing by the eye.
With my arms before me I walked steadily in. I
wanted to lie down in my own place (I thought 
with a silent chuckle) and enjoy their faces when
they found me in the morning.</p><p>
And then a shrill voice cried:</p><p>
“Pieces of eight! Pieces of eight! Pieces of
eight! Pieces of eight! Pieces of eight!” and so
forth, without pause or change.</p><p>
Silver’s green parrot, Captain Flint! It was him.
I had no time to run away.</p><p>
The sleepers awoke, and the voice of Silver cried,
“Who goes?”</p><p>
Somebody held me tight.</p><p>
“Bring a torch, Dick,” said Silver.</p>
</div>



<h3><span>PART SIX</span> Captain Silver</h3>
<div>


<p>
The pirates had the house and stores: there
was the cask of cognac, there were the pork
and bread, as before, and not a sign of any
prisoner. I could only judge that all died.
There were six of the pirates. Five of them
were on their feet. The sixth was deadly pale.
I remembered the man who was shot and ran
back among the woods in the great attack.</p><p>
“So,” said Long John, “here’s Jim Hawkins,
well, come!”</p><p>
And he sat down across the brandy cask and
began to fill a pipe.</p><p>
“And so, Jim,” said he, “here you are, and
quite a pleasant surprise for poor old John. You
left your friends, they don’t want to see you 
again. I’ve always liked you, you’ll join Captain
Silver.”</p><p>
So far so good. My friends were still alive.</p><p>
“Yesterday morning, Mr. Hawkins,” said Silver,
“came Dr. Livesey with a flag of truce. He says,
‘Captain Silver, ship’s gone.’ The old ship was
gone! ‘Well,’ says the doctor, ‘let’s bargain.’ We
bargained, him and I, and here we are: stores,
brandy, block house, the firewood. As for them,
they’ve gone away; I don’t know where they are.”</p><p>
“Well,” said I, “I am not such a fool. Let the
worst come to the worst. But there’s a thing or
two I have to tell you; and the first is this: here
you are, in a bad way — ship lost, treasure
lost, men lost; and if you want to know who
did it — it was I! I was in the apple barrel,
and I heard you, John, and you, Dick Johnson,
and Hands, who is now at the bottom of the
sea, and told everything to my friends. It was I
who cut the cable, and it was I that killed the
men you had aboard, and it was I who brought
the ship where you’ll never see it anymore. Kill
me, if you please, or spare me. But one thing 
I’ll say, and no more; if you spare me, I’ll try
to save you from the gallows.”</p><p>
“It was him,” cried the old seaman — Morgan
by name, “that knew Black Dog.”</p><p>
“Well,” added the sea-cook. “It was this same
boy that took the chart from Billy Bones.”
Morgan took his knife, “Let’s kill him!”
“Avast, there!” cried Silver. “Who are you,
Tom Morgan? Maybe you think you’re captain
here? I have never seen a better boy than that.
He’s more a man than any of you.”</p><p>
There was a long pause after this.</p><p>
“We need to discuss it,” said Morgan and
went out. One after another the rest followed
him.</p><p>
“Now, you’re here, Jim Hawkins,” said Silver
in a whisper. “They’re going to throw me off.
They’ll decide to kill you and choose a new
captain. I’ll save your life, but you will save
Long John from swinging.”</p><p>
The door opened, and the five men entered.
“Come on,” cried Silver. “I won’t eat you.”
One pirate passed something to Silver, from
hand to hand, then slipped smartly back again
to his companions.</p><p>
“The black spot! I thought so,” he noticed.</p><p>
“Where did you get the paper? A Bible! But
listen to me first. Who crossed me? And that
boy — isn’t he a hostage? Are we going to
waste a hostage? No, not us; he might be our
last chance. Kill that boy? Not me! And maybe
you don’t need a real college doctor that comes
to see you every day — you, John, or you,
George? Look there — what is it?”</p><p>
And he cast down upon the floor a paper
that I recognized — the chart on yellow paper,
with the three red crosses, that I found at
the bottom of the captain’s chest. Why did the
doctor given it to him?!</p><p>
The pirates leaped upon the chart like cats
upon a mouse. It went from hand to hand.</p><p>
“Yes,” said one, “that’s Flint’s.”</p><p>
“Silver!” cried the rest. “Barbecue forever!
Barbecue for captain!”</p><p>
We were all wakened by a clear voice.
“Here’s the doctor.”</p><p>
And the doctor it was. Although I was glad
to hear the sound, yet my gladness was not
without admixture. I felt ashamed to look him
in the face.</p><p>
“You, doctor! Early bird,” said Silver.</p><p>
“Well, well,” said Dr. Livesey, “duty first. Let
us see the patients.”</p><p>
He entered the house and, with one look at
me, began his work.</p><p>
“Well,” he said, “well, that’s done for today.
And now I wish to have a talk with that boy,
please.”</p><p>
And he nodded his head in my direction.
George Merry cried, “No!” and swore.</p><p>
Silver struck the barrel with his open hand.
“Silence!” he roared. “Doctor, I was thinking
of that. We’re all humbly grateful for your kindness.
Hawkins, will you give me your word of
honour as a young gentleman — for a young 
gentleman you are — your word of honour not
to run away?”</p><p>
I readily gave the pledge required.</p><p>
“Then, doctor,” said John Silver, “you just
step outside of that stockade, and once you’re
there I’ll bring the boy down on the inside.
Good day to you, sir. The boy will tell you how
I saved his life, and were deposed for it too.”
So saying, he stepped back a little way.</p><p>
“So, Jim,” said the doctor sadly, “here you
are. I cannot blame you.”</p><p>
“Doctor,” I said, “I have blamed myself
enough. But what I fear is torture. If they come
to torture me —”</p><p>
“Jim,” the doctor interrupted, and his voice
was quite changed, “Jim, whip over, and we’ll
run.”</p><p>
“Doctor,” said I, “I passed my word. Silver
trusted me; I passed my word, and back I go.
But, doctor, you did not let me finish. If they
come to torture me, I can tell them where the
ship is, for I got the ship. It lies in North Inlet,
on the southern beach, and just below high
water.”</p><p>
“The ship!” exclaimed the doctor. </p><p>
Rapidly I described to him my adventures.
“Every step,” he observed, “it’s you that saves
our lives. You found out the plot; you found Ben
Gunn — the best deed that ever you did. Silver!”
he cried. “Silver! I’ll give you a piece of advice,”
he continued as the cook drew near again. “Don’t
hurry after that treasure.”</p><p>
“Sir, I do my possible,” said Silver.</p><p>
“Well, Silver,” replied the doctor, “if that is
so, I’ll go one step further: look out for squalls
when you find it.”</p><p>
“Sir,” said Silver, “as between man and man,
that’s too much and too little. Why did you
leave the block house, why did you give me
that chart, I don’t know, now, do I? Tell me.”</p><p>
“No,” said the doctor. “I’ve no right to say
more; it’s not my secret, you see, Silver. I’ll
give you a bit of hope; Silver, if we both get
alive out of this, I’ll do my best to save you.”
Silver’s face was radiant. “You couldn’t say
more, I’m sure, sir, not if you was my mother,”
he cried.</p><p>
“Jim,” said Silver when we were alone, “I
save your life, you save mine; and I’ll not forget
it. I saw out of the corner of my eye that
the doctor was asking you to run and that you
refused. You and me must stick close, and we’ll
save our necks.”</p><p>
The men were in a good humour now. As
for me, I was terribly afraid.</p><p>
Next morning we left the house just after
breakfast. Everyone had guns except me. Silver
had two guns — one before and one behind —
and a knife. Captain Flint, his parrot, was sitting
on Silver’s shoulder.</p><p>
I had a line about my waist and followed obediently
after the sea-cook, who held the loose
end of the rope. I was led like a dancing bear.
The other men were carrying picks and shovels
that they brought ashore from the Hispaniola.
As we pulled over, there was some discussion
on the chart. The red cross was, of course, far
too large to be a guide. A tall tree was the principal mark. Now,
right before us the anchorage was bounded by
a plateau from two to three hundred feet high,
adjoining on the north the southern shoulder
of the Spy-glass and rising again towards the
south. The top of the plateau was dotted with
pine-trees of varying height. It was, indeed, a
most pleasant part of the island.</p><p>
We proceeded for about half a mile and were
approaching the brow of the plateau when one
pirate began to cry aloud, as if in terror.
At the foot of a big pine, a human skeleton
lay.</p><p>
“He was a seaman,” said George Merry, who
went up close and was examining the rags of
clothing.</p><p>
The body was not in a natural position. The
man lay perfectly straight — his feet pointing in
one direction, his hands, raised above his head,
pointing directly in the opposite.</p><p>
“Here’s the compass,” said Silver. The body
pointed straight in the direction of the island,
and the compass read duly E.S.E. and by E.
Before us, over the tree-tops, we beheld the
Cape of the Woods; behind, we not only looked
down upon the anchorage and Skeleton Island,
but saw a great field of open sea upon the east.</p><p>
“There are three ‘tall trees’, ” said Silver,
“about in the right line from Skeleton Island.</p><p>
‘Spy-glass shoulder,’ I think, means that. It’s
child’s play to find the stuff now. But let’s have
dinner first.”</p><p>
“As soon as I think of Flint, I lose my appetite,”
said Morgan.</p><p>
“Ah, well, my son, he’s dead,” said Silver.</p><p>
Since they found the skeleton, they spoke
lower and lower. Suddenly, out of the middle of
the trees in front of us, a thin, high, trembling
voice began to sing:</p><p>
“Fifteen men on the dead man’s chest—
Yo-ho-ho, and a bottle of rum!”</p><p>
I never have seen men more dreadfully affected
than the pirates. The colour went from
their six faces; some leaped to their feet. </p><p>
“It’s Flint!” cried Merry.</p><p>
The song stopped as suddenly as it began.</p><p>
“Come,” said Silver, “this won’t do. I can’t
name the voice, but the singer is alive and not
dead.”</p><p>
The voice cried again, “Darby McGraw, Darby
McGraw! Darby McGraw!” again and again and
again; and then rising a little higher, “Bring the
rum, Darby!”</p><p>
The pirates remained rooted to the ground.</p><p>
“These were his last words,” moaned Morgan,
“his last words before death.”</p><p>
Dick began to pray. But Silver did not surrender.
“I’m here to get the treasure,” he cried, “and
nobody will stop me! I was never feared of Flint
in his life, and I’ll face him dead. There’s seven
hundred thousand pound not a quarter of a
mile from here. It was like Flint’s voice, but it
was like somebody else’s voice as well — Ben
Gunn!” roared Silver.</p><p>
Our way lay a little downhill. The pines, great
and small, grew wide apart. The thought of the
money, as the pirates came nearer, swallowed
up their previous terrors. </p><p>
And suddenly, not ten yards further, we
stopped. A low cry arose. Before us was a
great excavation. All was clear: someone got the
treasure before us; the seven hundred thousand
pounds were gone!</p><p>
Each of these six men was struck. But Silver
found his temper very fast, and changed his
plan before the others had time to realize the
disappointment.</p><p>
“Jim,” he whispered, “take that.”</p><p>
And he passed me a double-barrelled pistol.
At the same time, he began quietly to move
northward, and in a few steps hollow was between
us two and the other five. The pirates,
with oaths and cries, began to leap, one after
another, into the pit and to dig with their fingers.
Morgan found a piece of gold. It was a
two-guinea piece, and it went from hand to hand
among them for a quarter of a minute.</p><p>
“Two guineas!” roared Merry. “That’s your
seven hundred thousand pounds, is it?”</p><p>
“Dig away, boys,” said Silver with the coolest
insolence, “you’ll find some pignuts.”</p><p>
“Pignuts!” repeated Merry, in a scream. “Do
you hear that?”</p><p>
Silver did not move; he watched them, and
looked as cool as ever I saw him. He was brave. 
At last Merry said, “Look: there are only two
of them here. One is the old cripple that brought
us all here and for nothing; the other is that
boy whom I’m going to kill. Now, mates —”
He was raising his arm and his voice, but just
then — crack! crack! crack! — three musket-shots
flashed out of the wood. Merry fell down into
the excavation; the man with the bandage fell
nearby, where he lay dead; and the other three
turned and ran away.</p><p>
At the same moment, the doctor, Gray, and
Ben Gunn joined us, with smoking muskets.
“Thank you kindly, doctor,” said Silver. “You
came in the right time. And so it’s you, Ben
Gunn!” he added. “Well, you’re a nice one.”
Ben, in his long, lonely wanderings about the
island, found the skeleton; then he found the
treasure. He took it, he carried it on his back,
in many journeys, to a cave he had on the
two-pointed hill at the north-east angle of the
island. And there it was stored in safety since
two months before the arrival of the Hispaniola.
As we passed the two-pointed hill, we could
see the black mouth of Ben Gunn’s cave and a 
figure standing by it. It was the squire, and we
waved a handkerchief and gave him three cheers.
Three miles farther, just inside the mouth
of North Inlet, we met the Hispaniola. The last
flood lifted her.</p><p>
And then we all entered the cave. It was
a large, airy place. The floor was sand. Before
a big fire lay Captain Smollett; and in a far
corner I saw great heaps of coin and bars of
gold. That was Flint’s treasure that cost already
the lives of seventeen men from the Hispaniola.</p><p>
“Come in, Jim,” said the captain. “You’re a
good boy, Jim, but I don’t think you and me
well go to sea again. Is that you, John Silver?
What brings you here, man?”</p><p>
“Come back to my duty, sir,” returned Silver.</p><p>
“Ah!” said the captain, and that was all he
said.</p><p>
What a supper I had that night, what a meal
it was, with Ben Gunn’s salted goat and some
delicacies and a bottle of old wine from the Hispaniola.
Never, I am sure, were people happier.</p><p>
The next morning we began to work, for
the transportation of this great mass of gold
near a mile by land to the beach, and then
three miles by boat to the Hispaniola, was a
considerable task for us. The three pirates did
greatly trouble us.</p><p>
It was a strange collection. English, French,
Spanish, Portuguese coins, the pictures of all
the kings of Europe for the last hundred years,
strange Oriental pieces, round pieces and square
pieces. Day after day this work went on.
We have never heard about the three pirates.
We left them powder and shot, the bulk of
the salt goat, a few medicines, and some other
necessaries, tools, clothing, a spare sail, ropes,
and tobacco.</p><p>
At last we lifted anchor and began our voyage
back. A few days later we reached one of
the many beautiful coves along South America’s
northern coast. It was just at sundown when we
cast anchor. Silver was gone. But the sea-cook 
did not gone empty-handed. He stole three or
four hundred guineas.</p><p>
Well, to make a long story short, we got
some sailors on board, made a good cruise
home, and the Hispaniola reached Bristol. Five
men only of those who sailed returned with
it.</p><p>
All of us received a good part of the treasure
and used it wisely or foolishly, according
to our natures. Captain Smollett is now retired
from the sea. Gray not only saved his money,
but also studied his profession, and he is now
part owner of a fine ship, married besides,
and the father of a family. As for Ben Gunn,
he got a thousand pounds, which he spent or
lost in three weeks, or to be more exact, in
nineteen days, for he was back begging on
the twentieth.</p><p>
Of Silver we have heard no more. That formidable
seaman with one leg has at last gone
clean out of my life. Nothing would not bring 
me back again to that accursed island; and the
worst dreams that ever I have are when I hear
the sharp voice of Captain Flint that is still
ringing in my ears: “Pieces of eight! Pieces of
eight!”</p>


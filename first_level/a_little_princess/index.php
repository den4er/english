Sara never forgot. She was in the darkness. Her
bed wasn’t soft anymore. She heard rats near her
bed. And she felt very sad and lonely in the world.
Next morning changes began. Lavinia sat in
her place in the classroom and her room was for
another pupil. Sara didn’t learn anymore, she helped
little children. The more she worked, the more Miss
Minchin wanted from her. She tried to do her work
well, but nobody became kinder to her. Lavinia was
always glad to do something bad for Sara. Miss
Minchin was very cold and angry.</p><p>
But some people tried to make Sara’s life better.
Becky came to her every morning and helped
her with her dress. Ermengarde had a kind heart
too. She went to her family for some time. When
she came back, she saw that Sara’s life was different.
She came to talk to Sara but she didn’t know
what to say. And Sara thought Miss Minchin told
her not to be friends anymore. Sara stopped talking 
with Ermengarde and turned away from her.</p><p>
Ermengarde was very unhappy and one night she
came to Sara’s room in the attic.</p><p>
“Ermengarde!” cried Sara. “You will get into
trouble.”</p><p>
“But I don’t care--I don’t care a bit. Oh, Sara,
please tell me. What is the matter? Why don’t you
like me anymore?”</p><p>
“I like you,” Sara answered. “I thought--you
see, everything is different now. I thought you--
were different”.</p><p>
Ermengarde opened her wet eyes wide.</p><p>
“Why, it was you who were different!” she
cried. “You didn’t want to talk to me. I didn’t
know what to do. It was you who were different
after I came back.”</p><p>
“I AM different,” Sara explained, “though not
in the way you think. Miss Minchin does not want
me to talk to the girls. Most of them don’t want
to talk to me. I thought--perhaps--you didn’t. So
I tried to keep out of your way.”</p><p>
“Oh, Sara,” Ermengarde cried and the girls
hugged. They decided to meet in the attic so that
Ermengarde would be able to listen to the stories.
</p>
</div>

<h3>CHAPTER 9</h3>
<div>

<p>
Soon Lottie came to see her “mother”. She
climbed the stairs to Sara’s room and was afraid
of it. Sara showed her the windows and the birds
on the roof. And Lottie thought that the room was
very good and wanted to stay there. But when Lottie
left, Sara saw how bad the room was. She became
sad again. So she sat on her bed and decided to
have some bread. Suddenly she heard some noise.
It was a big rat with clever eyes. The rat wanted
some bread crumbs. Sara gave it the crumbs. The
rat ate one and took the others somewhere.</p><p>
“I knew he wanted it for his children,” said
Sara. “I think I could make friends with him.”
One night Sara did not open the door to Ermengarde
for two or three minutes. “There!” Ermengarde
heard her say. “Take it and go home,
Melchisedec! Go home to your wife!”</p><p>
“Who ARE you talking to, Sara?” she asked.
“You must not scream, or I can’t tell you,”
Sara answered.</p><p>
“Is it something that will frighten me?” she
asked.</p><p>
“Some people are afraid of them,” said Sara.
“I was at first but I am not now.”</p><p>
“Was it a ghost?” asked Ermengarde.</p><p>
“No,” said Sara, laughing. “It was my rat.”</p><p>
“Oh! Oh!” she cried. “A rat! A rat!”</p><p>
“He is just like a person. Now watch!”</p><p>
Sara whistled and the rat came. Sara had
some crumbs in her hand. She dropped them, and
Melchisedec came quietly and ate them. He took a
large piece and took it to his home.</p><p>
“You see,” said Sara, “that is for his wife and
children. He is very nice. He only eats the little bits.
After he goes back I can always hear his family
squeaking. There are three kinds of squeaks. One
is the children’s, and one is Mrs. Melchisedec’s,
and one is Melchisedec’s own.”</p><p>
Ermengarde began to laugh.</p><p>
“Oh, Sara!” she whispered. “It is like a story!”</p><p>
“It IS a story,” said Sara. “EVERYTHING’S
a story. You are a story--I am a story. Miss
Minchin is a story.”</p>
</div>

<h3>CHAPTER 10</h3>
<div>

<p>
Friendly visits were rare, and Sara lived a strange
and lonely life. She had no one to talk to; and when
she went on errands and walked through the streets,
she felt very lonely. Sara wore her old clothes that
were small for her and she looked strange.</p><p>
There were several families in the square where
Miss Minchin’s school was. Sara liked looking at
the life of these families through the windows. She
made up stories about them. Her favourite family
was a family with eight children. Sara called them
the Large Family or the Montmorencys. They were
all rosy and friendly.</p><p>
One evening a very funny thing happened.
Several of the Montmorencys decided to go to a
children’s party. They left the house to get into
their carriage. Sara wanted to look at them for a
moment. So she stopped.</p><p>
It was Christmas time, and the Large Family
knew many stories about children who were poor.
The boy saw Sara standing near their house. He 
decided to help her. So he put his hand in his
pocket and found his sixpence and walked up to her.
“Here, poor little girl,” he said. “Here is a
sixpence. I will give it to you.”</p><p>
Sara started, and all at once understood that
she looked like poor child. Her face went red and
then it went pale, and for a second she felt as if
she could not take the dear little sixpence.
“Oh, no!” she said. “Oh, no, thank you; I
mustn’t take it, indeed!”</p><p>
Her voice was so unlike an ordinary street child’s
voice and her manner was so like the manner of a
well-bred little person that the girls of the family
leaned forward to listen.</p><p>
“Yes, you must take it, poor little girl!” the
boy said. “You can buy things to eat with it. It
is a whole sixpence!”</p><p>
There was something so kind in his face that
Sara knew she must not refuse him.</p><p>
“Thank you,” she said. “You are a kind, kind
little darling thing.”</p><p>
The children went away in the carriage and
one of the girls said “Oh, Donald, why did you
give that little girl your sixpence? I’m sure she is
not a beggar.”</p><p>
“She didn’t speak like a beggar!” cried Nora.</p><p>
“And her face didn’t really look like a beggar’s face!”
“She is a servant at the seminary,” Janet said.</p><p>
“I think she is an orphan. But she is not a beggar,
however shabby she looks.”</p><p>
And afterward she had a name with all of them,
“The-little-girl-who-is-not-a-beggar,” which was,
of course, a long name.</p><p>
Sara loved her small pupils, her friends Ermengarde,
Lottie and Becky. She became friends
with Melchisedec and he often came out with his
wife and children. She only felt that Emily was not
her real friend anymore. Sara understood she was
just a doll.</p><p>
Sara saw a beautiful house next door to the
school. She dreamt that one day someone would buy
it. And this day came. Becky said that a rich man
from India came to live in that house. Sara saw
the man some days later. He looked unhappy and
ill. His skin was yellow. His friend was the father
from the Large family. That was the beginning of
the story of the Indian gentleman.</p>
</div>

<h3>CHAPTER 11</h3>
<div>

<p>
There were fine sunsets even in the square,
sometimes. Sara went to the roof to look at the
evening sky. One day she saw another person on
her roof. It was a Lascar. He stood there with
his pet monkey and enjoyed the sunset too. He
saw Sara and smiled. The monkey jumped from his
hands and ran to Sara. She asked the Lascar in
his language: “Will he let me catch him?”</p><p>
The Lascar was happy to hear Sara’s words.
His name was Ram Dass. He was from India. He
climbed to Sara’s part of the roof. The monkey was
in the room. Ram Dass saw that the room was very
poor and shabby. But he didn’t say a word and
spoke to Sara as if she was a daughter of a rajah.
When Ram Dass left, Sarah began to think
of her life. She knew Miss Minchin wanted her to
work and be a servant.</p><p>
Then a thought came back to her which helped
her. “Whatever comes,” she said, “cannot change
one thing. If I am a princess in shabby clothes, I
can be a princess inside.” Sara said these words to
herself when she had hard days with Miss Minchin.
The morning after the talk with Ram Dass and
his monkey, Sara was in the schoolroom with her
small pupils. She finished her lesson and started
to put away the books. She had a strange face.
Miss Minchin became angry; she ran to Sara and
slapped her. Sara was surprised and she laughed.
“What are you laughing at?” Miss Minchin
cried.</p><p>
“I was thinking,” she answered with red cheeks.
“Say that you are sorry,” said Miss Minchin.</p><p>
Sara thought for a second.</p><p>
“I am sorry for laughing,” she said then; “but
I am not sorry for thinking.”</p><p>
“What were you thinking?” demanded Miss
Minchin. “How dare you think?”</p><p>
The older girls in the classroom laughed. Sara
thought again and answered and her eyes shone
like stars.</p><p>
“I was thinking,” she answered, “that you did
not know what you were doing. And I was thinking
what would happen if I were a princess and you
slapped me, what I should do to you. And I was
thinking how surprised and frightened you would be
if you suddenly found out--”</p><p>
“What?” Miss Minchin cried. “Found out
what?”</p><p>
“That I really was a princess,” said Sara, “and
could do anything-- anything I liked.”</p><p>
Every pair of eyes in the room was looking
at them.</p><p>
“Go to your room,” cried Miss Minchin, “this
second! Leave the schoolroom!”</p><p>
“Excuse me for laughing,” Sara said, and walked
out of the room.</p>
</div>

<h3>CHAPTER 12</h3>
<div>

<p>
When one lives in a row of houses, it is interesting
to think of the things which happen on 
the other side of the wall. Sara often did it. She
thought about the Indian gentleman and his life. She
liked him as he looked unhappy. The Large family
often visited the gentleman with all their children.
The Indian gentleman’s real name was Mr.</p><p>
Carrisford. One day one of the Montmorencys told
Mr. Carrisford about the little-girl-who-was-not-abeggar.
He was very much interested, and all the
more so when he heard from Ram Dass of the
adventure of the monkey on the roof. Ram Dass
made for him a very clear picture of the dirty attic.
“Carmichael,” he said to the father of the
Large Family, “I wonder how many of the attics in
this square are like that one, and how many little
servant girls sleep on such beds, while I have so
much money, most of it not mine. Do you think it
 is possible that the other child, the child I never
stop thinking of, could live like that?”</p><p>
Mr. Carmichael looked at him. He knew that
the worst thing the man could do for himself, for
his health, was to begin to think about the girl.</p><p>
“If the child at Madame Pascal’s school in
Paris was the one you are looking for,” he answered,
“she is in a good family. They have no
other children, and Madame Pascal said that they
were rich Russians.”</p><p>
“But you say ‘IF the child was the one I am
looking for’. You say ‘if.’ We are not sure. There
was a difference in the name.”</p><p>
“An English officer in India took his motherless
little girl at the school. He died suddenly after losing
his fortune.” Mr. Carmichael paused a moment.</p><p>
“Are you SURE the child was left at a school in
Paris? Are you sure it was Paris?”</p><p>
“My dear friend,” Carrisford said, “I am SURE
of nothing. I never saw the child. Ralph Crewe and
I loved each other as boys. We met many years
later in India. I knew he had a child but we never
spoke about her. We spoke about the mines. I only
knew that the child was at school somewhere. I
do not even remember, now, HOW I knew it. I
thought she could be in Paris because her mother
was French. Carmichael,” he said, “I MUST find
her. If she is alive, she is somewhere”.</p><p>
“Come, come,” said Carmichael. “We will find
her.”</p><p>
“We must. I often see Ralph Crew in my
dreams. He asks me: “Where is my little Missus?”
I must answer him.”</p><p>
On the other side of the wall Sara talked to
Melchisedec.</p><p>
“It has been hard to be a princess today,
Melchisedec,” she said. “It has been harder than
usual. It was a cold afternoon, Melchisedec. And
it’s a cold night.”</p><p>
“Oh, papa,” she whispered, “what a long time
it seems since I was your `Little Missus’!”
This was what happened that day on both sides
of the wall.</p>
</div>

<h3>CHAPTER 13</h3>
<div>

<p>
That winter was very cold. There was often a
strong wind. Sara felt bad in her small clothes, she
couldn’t get warm.</p><p>
Some very strange things happen in this world
sometimes. It certainly was a strange thing that
happened to Sara. She had to cross the street and
she saw something in the mud. It was actually a
piece of silver- a four-penny piece.</p><p>
In one second it was in her cold little redand-
blue hand.</p><p>
“Oh,” she gasped, “it is true! It is true!”
And then she looked at the shop near her. And
it was a baker’s shop. She saw the tasty buns.
It almost made Sara feel faint for a few seconds.
“But I’ll go and ask the baker woman if she
has lost anything,” she said to herself. She went to
the shop and stopped when she saw a beggar-girl.
“Are you hungry?” she asked.</p><p>
The child was very hungry.</p><p>
“If I’m a princess,” she said, “if I’m a princess,
I must share with people.”</p><p>
“Wait a minute,” she said to the beggar child.
She went into the shop.</p><p>
“If you please,” said Sara, “have you lost
fourpence--a silver fourpence?”</p><p>
The woman looked at it and then at her.</p><p>
“Bless us, no,” she answered. “Did you find it?”
“Yes,” said Sara. “In the mud.”</p><p>
“Keep it, then,” said the woman.</p><p>
“I know that,” said Sara, “but I thought I
would ask you.”</p><p>
“Not many would,” said the woman, looking
surprised and interested and good-natured all at
once.</p><p>
“Do you want to buy anything?” she added.
“Four buns, if you please,” said Sara. “A
penny each.”</p><p>
The woman went to the window and put some
in a paper bag. Sara noticed that she put in six.
“I said four, if you please,” she explained. “I
have only fourpence.”</p><p>
“I’ll throw in two,” said the woman with her
good-natured look. “I dare say you can eat them.
Aren’t you hungry?”</p><p>
“Yes,” she answered. “I am very hungry, and
I thank you. And there is a child who is hungrier
than I am.”</p><p>
Sara left the shop and came up to the beggar.
She opened the paper bag and took out one of the
hot buns.</p><p>
“See,” she said, “this is nice and hot. Eat it,
and you will not feel so hungry.”</p><p>
The child ate the bun like a wolf. Sara took
out three more buns and put them down.</p><p>
“She is hungrier than I am,” she said to herself.
But her hand trembled when she put down the
fourth bun. “I’m not very hungry,” she said--and
she put down the fifth.</p><p>
“Good-bye,” said Sara and went to her attic.
At that moment the baker-woman looked out
of her shop window.</p><p>
“Well, I never!” she exclaimed. “If that young
girl hasn’t given her buns to a beggar child.”
She went to the door and spoke to the beggar
child.</p><p>
“Who gave you those buns?” she asked her.</p><p>
The child nodded her head toward Sara.</p><p>
“How many?”</p><p>
“Five.”</p><p>
The woman thought it over.</p><p>
“Left just one for herself,” she said in a low
voice. “And she could eat the whole six--I saw it
in her eyes.”</p><p>
Then she turned to the child.</p><p>
“Are you hungry yet?” she said.</p><p>
“I’m always hungry,” was the answer.</p><p>
“Come in here,” said the woman, and she held
open the shop door. “Get yourself warm. And look
here; when you are very hungry, you can come in
here and ask for it. I’ll help you for the sake of
that girl.”</p><p>
Sara enjoyed her bun. She saw Montmorency
in the carriage. He left for Moscow to look for
some girl.</p><p>
“I wonder who the little girl is,” she thought--
“the little girl he is going to look for.”</p><p>
And she went down the steps, as the father of
the Large Family was on his way to the station to
take the train to Moscow, where he was to make
his best efforts to search for the lost little daughter
of Captain Crewe.</p>
</div>

<h3>CHAPTER 14</h3>
<div>

<p>
On this very afternoon, while Sara was out, a
strange thing happened in the attic. Only Melchisedec
saw and heard it.</p><p>
Two men were outside on the roof. One was
Ram Dass and the other was a young man who
was the Indian gentleman’s secretary; but of course
Melchisedec did not know this.</p><p>
The secretary saw Melchisedec’s tail.</p><p>
“Was that a rat?” he asked Ram Dass in a
whisper.</p><p>
“Yes; a rat, Sahib,” answered Ram Dass, also
whispering. “There are many in the walls.”</p><p>
“Ugh!” exclaimed the young man. “It is a
wonder the child is not afraid of them.”</p><p>
“The child is the little friend of all things, Sahib,”
he answered. “She is not as other children. I
see her when she does not see me. The rat comes
for crumbs. There is a little child who comes to
her in secret; there is one older who would listen
to her forever if she might.”</p><p>
“You seem to know a lot about her,” the secretary
said.</p><p>
“All her life each day I know,” answered Ram Dass.
“You are sure no one comes near this place but
herself, and that she will not return and surprise
us. She would be frightened if she found us here,
and the Sahib Carrisford’s plan would end.”</p><p>
Ram Dass stood near the door and the secretary
wrote down the details of the room.</p><p>
“It is a strange way of doing the thing,” he
said. “Who planned it?”</p><p>
“It is true that the first thought was mine, Sahib,”
Ram Dass said; “I am fond of this child; we
are both lonely. One evening she told her rat about
her dreams. I told Sahib of the girl to please him. It
seemed then but a dream, but it pleased the Sahib.
He began to think of making her dreams real.”</p><p>
“You think that we can do it while she sleeps?
“I can move as if my feet were of velvet,” Ram
Dass replied; “and children sleep soundly, even the
unhappy ones.”</p><p>
He smiled as if his heart warmed under his
white robe, and the secretary smiled back at him.
“It will be like a story,” he said.</p><p>
The young secretary seemed interested in
everything. He wrote down things about the floor,
the bed, the old table, the walls.</p><p>
“I think we can go now,” he said. “The Sahib
Carrisford has a warm heart. It is a pity that he
has not found the lost child.”</p><p>
Then the left as quietly as they came.</p>
</div>


<h3>CHAPTER 15</h3>
<div>

<p>
When Sara went into the house she met Miss
Minchin.</p><p>
“Where have you been?” she demanded. “You
have been out for hours.”</p><p>
“It was so wet and muddy,” Sara answered, “it
was hard to walk, because my shoes were so bad.”</p><p>
“Make no excuses,” said Miss Minchin, “and
tell no lies.”</p><p>
Sara went in to the cook.</p><p>
“May I have something to eat?” Sara asked.
“Tea’s over,” was the answer.</p><p>
Sara stood silent for a second.</p><p>
“I had no dinner,” she said.</p><p>
“There’s some bread,” said the cook. “That’s
all you’ll get at this time of day.”</p><p>
Sara went and found the bread. It was old and
dry. She was tired and went to her attic. Ermengarde
was there.</p><p>
“Papa has sent me some more books, Sara,”</p><p>
she said. “There they are.”</p><p>
“Look here,” Sara cried, “if you lend me these
books, I’ll read them and tell you everything that’s
in them.</p><p>
“Oh!” exclaimed Ermengarde. “Do you think
you can?”</p><p>
“I know I can,” Sara answered. “The little ones
always remember what I tell them.”</p><p>
“Sara,” said Ermengarde, with hope in her round
face, “if you do that, and make me remember, I’ll
give you anything.”</p><p>
“I don’t want you to give me anything,” said
Sara. “I want your books—I want them!” And her
eyes grew big.</p><p>
“Oh, Sara!” said Ermengarde. “This very afternoon
my nicest aunt sent me a box. It is full of
good things. I never touched it. It’s got cake in it,
and little meat pies, and jam tarts and buns, and
oranges and figs and chocolate. I’ll go back to my
room and get it this minute, and we’ll eat it now.”
Sara was happy.</p><p>
“Do you think--you COULD?” she asked.</p><p>
“I know I could,” answered Ermengarde, and
she ran to the door.</p><p>
Sara and Becky decorated the attic room. They
found some tablecloth and plates. Sara thought it
looked like a great party.</p><p>
“Doesn’t it LOOK real?” she said. “Now we
will begin the party.”</p><p>
She led the way to the table. She was in her
dream. They didn’t have time to take their pieces of
cake into their hands, not one of them had time to
do more, when they all three jumped to their feet
and turned pale faces toward the door-- listening-
-listening.</p><p>
Someone was coming up the stairs. There was
no mistake about it.</p><p>
“It’s--the missus!” whispered Becky, and
dropped her piece of cake upon the floor.
“Yes,” said Sara, her eyes growing shocked and
large in her small white face. “Miss Minchin has
found us out.”</p><p>
Miss Minchin opened the door. She was pale.
She looked from the frightened faces to the table.
“Lavinia has told the truth!” she exclaimed. She
slapped Becky and cried: “Go to your attic!”
Then it was Sara’s turn again.</p><p>
“I will talk to you tomorrow. You will have no
breakfast, dinner or supper!”</p><p>
“I have not had either dinner or supper today,
Miss Minchin,” said Sara.</p><p>
“Then all the better. You will have something
to remember. Don’t stand there.”</p><p>
“And you”--to Ermengarde--“have brought your
beautiful new books into this dirty attic. Take them
up and go back to bed. You will stay there all day
tomorrow, and I will write to your papa. What
would HE say if he knew where you are tonight?”
Something she saw in Sara’s face at this moment
made her turn on her fiercely.</p><p>
“What are you thinking of?” she demanded.
“Why do you look at me like that?”</p><p>
“I was wondering,” answered Sara.</p><p>
“What were you wondering?”</p><p>
“I was wondering,” she said in a sad and quiet
voice, “what MY papa would say if he knew where
I am tonight.”</p><p>
Miss Minchin was very angry. She flew at her
and shook her. “How dare you! How dare you!”
She picked up the books and pushed Ermengarde
to the door. The door closed and Sara was alone.
She suddenly felt so tired, perhaps because she
was hungry, that she sat down on the bed.
“Suppose there was a bright fire in the room,
with,” she whispered. “Suppose there was a comfortable
chair before it--and suppose there was a
small table near, with a little hot—hot supper on it.
And suppose this was a beautiful soft bed. Suppose--
suppose--” And her eyes closed and she
fell fast asleep.</p><p>
She did not know how long she slept. At first
she did not open her eyes. She felt too sleepy and
too warm and comfortable.</p><p>
“What a nice dream!” she murmured. “I feel
quite warm. I--don’t- -want- -to--wake--up.”
Of course it was a dream. She felt as if warm,
delightful bedclothes were upon her. She could actually
FEEL blankets. It was a sense of light, and a
sound--the sound of a crackling, roaring little fire.
“Oh, I am waking up,” she said. “I can’t help
it--I can’t.”</p><p>
Her eyes opened in spite of herself. And then
she actually smiled—for what she saw was a surprise.
“Oh, I HAVEN’T woken up,” she whispered.</p><p>
“I am dreaming yet.” She knew it MUST be a
dream. She was in the room of her dreams. And
it all happened at night.</p><p>
“It does not go away,” she gasped, “Oh, I
have never had such a dream before.”</p><p>
She touched all the things in her room. “They
are real. It’s all real!” she cried. “I am NOT--I
am NOT dreaming!”</p><p>
She ran to the books and opened the one which
was on top. There were some words:</p><p>
“To the little girl in the attic. From a friend.”
When she saw that, she cried for the first time
in many days.</p><p>
“I don’t know who it is,” she said; “but somebody
cares for me a little. I have a friend.”</p><p>
She took her candle and went out of her own
room and into Becky’s, and stood by her bed.</p><p>
“Becky, Becky!” she whispered as loudly as she
dared. “Wake up!”</p>
</div>



<h3>CHAPTER 16</h3>
<div>

<p>
The girls had a wonderful breakfast. There was
hot tea and soup with buns. Sara went down with
a smile on her face. The girls and Miss Minchin
were surprised. Sara thought about her room all day
long. She was afraid that all the things were just
her dream. But in the evening her room became
even better. There were a lot of beautiful pictures
and figures. And every day something changed
and there were new things in the room. Sara felt
stronger every day.</p><p>
In a very short time she began to look less
thin. Color came into her cheeks, and her eyes did
not seem so much too big for her face.</p><p>
Then another wonderful thing happened. A man
came to the door and left several parcels. All were
addressed in large letters, “To the Little Girl in the
right-hand attic.”</p><p>
Sara herself opened the door and took them in.
“Take the things to the young lady to whom
they belong,” Miss Minchin said.</p><p>
“They belong to me,” answered Sara, quietly.
“To you?” exclaimed Miss Minchin. “What do
you mean?”</p><p>
“I don’t know where they come from,” said
Sara, “but they are for me. I sleep in the righthand
attic. Becky has the other one.”</p><p>
Miss Minchin came to her side and looked at
the parcels.</p><p>
“What is in them?” she asked.</p><p>
“I don’t know,” replied Sara.</p><p>
“Open them,” she said.</p><p>
There were a lot of beautiful and expensive
clothes and shoes. There were even a nice hat
and an umbrella. They were all good and expensive
things. Miss Minchin thought that Sara might have
some rich relatives.</p><p>
“Well,” she said, “someone is very kind to you.
Learn your lessons in the classroom. You don’t need
to work today.”</p><p>
When Sara came into the classroom , she looked
good. The girls were surprised and Lavinia became
jealous again.</p><p>
Sara wanted to thank her secret friend and she
wanted to write him a letter. “I can write to him,”
she said, “and leave it on the table. Then perhaps
the person who takes the things away will take it,
too. I won’t ask him anything.”</p><p>
So she wrote a note. This is what she said:</p><p>********************</p><p>

“I am so grateful to you, and I am so happy--</p><p>
and so is Becky. Becky feels just as grateful as</p><p>
I do--it is all just as beautiful and wonderful</p><p>
to her as it is to me. We used to be so lonely</p><p>
and cold and hungry, and now--oh, just think</p><p>
what you have done for us! Please let me say</p><p>
just these words. It seems as if I must say them.</p><p>

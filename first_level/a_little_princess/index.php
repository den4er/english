dirty and tired. Sara asked her maid about that
girl. Her name was Becky and she did all the hard
work for school. Sara felt very sorry for her. Becky
looked very hungry.</p><p>
That day Sara came to her room after a dancing
lesson and saw Becky in her chair. Becky slept
and didn’t hear Sara. When she woke up she was
very frightened.</p><p>
“Oh, miss! Oh, miss!” she gasped. “I’m sorry,
miss!”</p><p>
Sara came quite close to her. “That’s fine. Don’t
be afraid. I won’t tell Miss Minchin.” “Have you
done your work?” she asked. “Can you stay here
a few minutes?”</p><p>
Becky lost her breath. “Here, miss? Me?”</p><p>
Sara ran to the door, opened it, and looked
out and listened. “I thought-- perhaps—you might
like a piece of cake.”</p><p>
Sara opened a cupboard, and gave Becky a
thick slice of cake.</p><p>
“Becky,” she said, “did you listen to that story?”
“Yes, miss,” said Becky. “I knew I shouldn’t do
it but it was beautiful. May I listen to it again?”
“You may,” said Sara. “I’ll tell it ALL to you.”</p><p>
When Becky went downstairs, she was not the
same Becky. She had an extra piece of cake in her
pocket, and she felt warm. But it wasn’t the room
and the cake. Something else warmed and fed her,
and the something else was Sara.</p>
</div>

<h3>CHAPTER 6</h3>
<div>

<p></p><p>
Not very long after this a very exciting thing
happened. Not only Sara, but all the school, found
it exciting. In one of his letters Captain Crewe
told a most interesting story. An old friend came
to see him in India. He was the owner of land
where there were diamonds and he was going to
build the mines. That could make Sara the richest
person in the world. Ermengarde and Lottie loved
listening about the mines. But Lavinia and Jessie
thought the mines were just a fairy tale.</p><p>
That morning Lottie cried and shouted a lot.
Lavinia was angry and wanted to slap Lottie. Sara
jumped up from her place and came up to Lavinia.</p><p>
“Well,” she said, “I would like to slap YOU-
- but I don’t want to slap you! We are both old
enough to know better.”</p><p>
Here was Lavinia’s opportunity.</p><p>
“Ah, yes, your royal highness,” she said. “We

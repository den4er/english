After this, the girls spoke of her as “Princess
Sara”.</p><p>
A few weeks before Sara’s eleventh birthday
a letter came to her from her father, it was sad.
Captain Crewe was not very well, and worried about
the diamond mines.</p><p>
“You see, little Sara,” he wrote, “your daddy
is not a businessman at all. He doesn’t understand
the documents. If my little missus were here, she
would give me some good advice”.</p><p>
One of his many jokes had been to call her
his “little missus” because she was old-fashioned.
He made wonderful preparations for her birthday.
Among other things, there was a new doll from
Paris and her dresses.</p><p>
“I am getting very old,” Sara wrote; “you see,
I will never live to have another doll. This will be
my last doll.”</p><p>
Captain Crewe laughed like a child when he
saw this.</p><p>
“Oh,” he said, “she’s better fun every year she

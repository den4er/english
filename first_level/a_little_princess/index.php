<h3>CHAPTER 4</h3>
<div>

<p>
Life in Miss Minchin’s school could be very
bad for Sara. Everyone told her only nice things.
When guests came to school, Miss Minchin asked
Sara to speak French to them or tell them stories
about India. But Sara understood that she was not
an ideal person and often discussed her life with
Ermengarde. Most girls liked Sara. But Lavinia
hated Sara. She was very jealous of her.</p><p>

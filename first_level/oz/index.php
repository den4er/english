<h3><span>CHAPTER 15</span> THE DISCOVERY OF OZ, THE TERRIBLE</h3>
<div>

<p>
The four travelers walked up to the great gate of
Emerald City and rang the bell. Soon the Guardian
opened the Gates before them.
</p><p>
”What! are you back again?" he asked, in surprise.
</p><p>
”Do you not see us?" answered the Scarecrow.
</p><p>
”But the last time I saw you, you were going to visit
the Wicked Witch of the West."
</p><p>
”We visited her," said the Scarecrow.
</p><p>
"Did she let you go away?" asked the man, in
wonder.
</p><p>
"She could not stop us, for she melted," explained
the Scarecrow.
</p><p>
"Melted! Well, that is good news, indeed," said the
man. ”Who melted her?"
</p><p>
"It was Dorothy," said the Lion gravely.
</p><p>
"Oh, dear!" exclaimed the man, and he bowed very
low before her.
</p><p>
Then he led them into his little room and gave them
green glasses from the great box. Then they passed
on through the gate into the Emerald City. The soldier
with the green beard was still on guard before the door,
but he let them in at once. The beautiful green girl met
them again and showed each of them to their old rooms
at once to have a rest until the Great Oz was ready to
see them.
</p><p>
The soldier told Oz the good news at once, but
the Great Wizard gave no answer. The travelers had
no word from him the next day, or the next, or the
next. The waiting made them tired and angry. So the
Scarecrow at last told the green girl, ”Go and tell Oz that
he must let us in to see him at once, or we’ll call the
Winged Monkeys to help us." When the Wizard heard
the Scarecrow’s words they frightened him so much
that he invited the travelers to come to the Throne
Room at four minutes after nine o’clock the next
morning. 
</p><p>
The four travelers passed a sleepless night, for each
one was thinking of the gift he was going to get from
Oz.
</p><p>
At nine o’clock the next morning the soldier with the
green beard came to them, and four minutes later they
all entered the Throne Room of the Great Oz.
</p><p>
Of course they all were greatly surprised when they
looked about and saw no one at all in the room — not
the great Head, or the lovely Lady, or the terrible Beast,
or the Ball of Fire.
</p><p>
Soon they heard a solemn Voice, that seemed to
come from somewhere near the ceiling, and it said:
</p><p>
"I am OZ, the Great and Terrible. Why do you want
to see me?"
</p><p>
They looked again in every part of the room, but there
was no one there. So Dorothy asked, "Where are you?"
</p><p>
"I am everywhere," answered the Voice, "but I am
invisible. I will now seat myself upon my throne and
you may talk to me." Indeed, the Voice seemed just then
</p><p>
to come straight from the throne itself; so they walked
towards it and stood in front of it while Dorothy said:
</p><p>
”The Wicked Witch is dead and we are here because
you must keep your promise, Oz."
</p><p>
“What promise?" asked Oz.
</p><p>
”You promised to send me back to Kansas," said the
girl.
</p><p>
"And you promised to give me brains," said the
Scarecrow.
</p><p>
"And you promised to give me a heart," said the Tin
Woodman. 
</p><p>
"And you promised to give me courage," said the
Cowardly Lion.
</p><p>
"Is the Wicked Witch really dead?" asked the Voice,
and Dorothy thought it trembled a little.
</p><p>
"Yes," she answered, "I melted her with a bucket of
water."
</p><p>
"Oh, dear," said the Voice, "how suddenly! Well, come
to me tomorrow, for I must have time to think it over."
</p><p>
”We shall not wait a day longer," said the Scarecrow.
</p><p>
"There is nothing to think about," said the Tin
Woodman.
</p><p>
"You must keep your promises to us!" exclaimed
Dorothy.
</p><p>
And the Lion gave a loud roar. It was so fierce and
terrible and frightened Toto so much that the little
dog jumped away from the Lion and tipped over the
screen that stood in a corner. As it fell with a crash they
looked that way, and the next moment all of them saw
a little old man, with a bald head and a wrinkled face,
who looked as much surprised as they were. The Tin
Woodman raised his axe, rushed towards the little man
and cried out, ”Who are you?"
</p><p>
”I am Oz, the Great and Terrible," said the little man,
in a trembling voice. "But don’t harm me — please
don’t — and I’ll do anything you want me to."
</p><p>
Our friends looked at him in surprise and dismay.
</p><p>
”I thought Oz was a great Head," said Dorothy.
</p><p>
"And I thought Oz was a lovely Lady," said the
Scarecrovm
</p><p>
”And I thought Oz was a terrible Beast," said the Tin
Woodman.
</p><p>
"And I thought Oz was a Ball of Fire," exclaimed the
Lion.
</p><p>
"No, you are all wrong," said the little man meekly.
”You saw what I wanted you to see."
</p><p>
”Are you not a Great Wizard?" cried Dorothy.
</p><p>
"Don’t speak so loud, my dear, or someone may
hear you — and this will ruin me," he said. "Everybody
thinks that I’m a Great Wizard."
</p><p>
”And aren’t you?" she asked.
”Not a bit of it, my dear; I’m just an ordinary man."
</p><p>
“You’re more than that," said the Scarecrow angrily;
"you’re a humbug."
</p><p>
”You are right!" said the little man. ”I am a humbug."
</p><p>
"But this is terrible," said the Tin Woodman. "How
shall I ever get my heart?"
</p><p>
”Or I my courage?" asked the Lion.
”Or I my brains?" exclaimed the Scarecrow.
</p><p>
"My dear friends," said Oz, "please, do not speak of
these little things. Think of me, and the terrible trouble
I’m in now."
</p><p>
”Doesn’t anyone else know you’re a humbug?" asked
Dorothy.
</p><p>
"No one knows it, only you four — and myself,"
replied Oz. ”I made a great mistake when I let you into
the Throne Room. Usually I do not see even my people,
and so they believe I am something terrible."
</p><p>
"But I don’t understand," said Dorothy. "Why did
I see you as a great Head?" 
</p><p>
"Oh, that was easy," answered Oz. "Step this way,
please, and I will tell you all about it."
</p><p>
He led the friends to a small back room, and they all
followed him. He pointed to one corner, in which the
great Head lay. It was a paper head with a painted face.
</p><p>
"I put it on the throne," said Oz. "I stood behind the
screen and pulled a string to make the eyes move and
the mouth open."
</p><p>
”But how about the voice?" she asked.
</p><p>
”Oh, I can make any sound a bird or a beast makes,"
said the little man. "And here are the other things I used
when I saw you." He showed the Scarecrow the dress
and the mask of the lovely Lady. And the Tin Woodman
saw that his terrible Beast was only a dress of skins.
And the Lion saw that his Ball of Fire was only a ball of
cotton.
</p><p>
"Shame on you!" said the Scarecrow. "You are such
a humbug!"
</p><p>
"I am — I certainly am," answered the little man
with sorrow; "but it was the only thing I could do. Sit
down, please, and I will tell you my story."
</p><p>
So they sat down and listened while he told them
his story.
</p><p>
"I was born in Omaha — "
</p><p>
"Why, that isn’t very far from Kansas!" cried Dorothy.
</p><p>
”No, but it’s farther from here," he said sadly. “When
I grew up I began to ﬂy hot-air balloons. Well, one day
I went up in a balloon, but a strong current of air caught
the balloon and carried it many, many miles away. For
a day and a night I traveled through the air, and on the
morning of the second day I woke up and saw that the
balloon was over a strange and beautiful country. 
</p><p>
"When the balloon came down, a lot of a strange
people came up to me. They saw my balloon and
thought I was a great Wizard. Of course I let them think
so, because they were afraid of me, and promised to do
anything I wished them to.
</p><p>
”So I ordered them to build this City, and my Palace;
and they did it. Then I called the city the Emerald
City because the country around it was so green and
beautiful; and to make the name fit better I ordered all
the people to wear green glasses, so that everything
they saw was green."
</p><p>
“But isn’t everything here green?" asked Dorothy.
</p><p>
"No more than in any other city," replied Oz; "but
when you wear green glasses everything you see looks
green to you. They built the Emerald City many years
ago, for I was a young man when the balloon brought
me here, and I am a very old man now. I am very good
to the people, and they like me; but I never show myself
to any of them.
</p><p>
"One of my greatest fears was the Witches. I have no
magical powers at all, but the Witches are really able to
do wonderful things. There were four of them in this
country, and they ruled the people who lived in the North
and the South and the East and the West. It was a lucky
thing that the Witches of the North and South were good,
and I knew they were not going to do me any harm; but
the Witches of the East and the West were wicked. But
they were afraid to harm me, for they thought I was
more powerful than they themselves. As it was, I lived
in fear of them for many years; so you can understand
how happy I was when I heard that the Wicked Witch of
the East was dead. When you came to me, I was ready to
promise anything for your help with the other Witch; but
now the Wicked Witch of the West is dead, too, and I am
sorry, but I cannot keep my promises.” 
</p><p>
“I think you are a very bad man,” said Dorothy.
</p><p>
“Oh, no, my dear; I’m really a very good man, but I’m
a very bad Wizard.”
</p><p>
"Can’t you give me brains?” asked the Scarecrow.
</p><p>
"You don’t need them. You are learning something
every day. A baby has brains, but it doesn’t know much.
The longer you live the more you know and the cleverer
you become.”
</p><p>
”That may all be true,” said the Scarecrow, ”but I’ll be
very unhappy without brains.”
</p><p>
Oz looked at him carefully.
</p><p>
"Well,” he said with a sigh, "I’m not a magician, as
I said; but come to me tomorrow morning, and I will
stuff your head with brains. I cannot tell you how to use
them; you must find that out for yourself.”
</p><p>
"Oh, thank you — thank you!” cried the Scarecrow.
”I’ll find a way to use them!”
</p><p>
"But how about my courage?” asked the Lion
anxiously.
</p><p>
"You have a lot of courage, I am sure," answered
Oz. "All you need is to believe in yourself. There is no
creature that is not afraid when it faces danger. The
True courage is in facing danger when you are afraid,
and you have that kind of courage." 
</p><p>
"Perhaps I have, but I’m afraid just the same," said
the Lion. “I’ll really be very unhappy without the sort of
courage that makes one forget he is afraid."
</p><p>
"Very well, I will give you that sort of courage
tomorrow," replied Oz.
</p><p>
"How about my heart?" asked the Tin Woodman.
</p><p>
"Do you really want to have a heart?" answered Oz.
"It makes most people unhappy. I think you are lucky
because you do not have a heart."
</p><p>
"That is what you think," said the Tin Woodman. ”As
for me, I’ll be unhappy without a heart."
</p><p>
"Very well," answered Oz meekly. "Come to me
tomorrow and you will have a heart."
</p><p>
”And now," said Dorothy, ”how about me? How shall
I get back to Kansas?"
</p><p>
"We’ll have to think about that," replied the little
man. ”Give me two or three days to think it over and I’ll
try to find a way to carry you over the desert. During
this time, please, be my guests, and while you live in
the Palace my people will obey all your wishes. There
is only one thing I ask in return for my help — keep my
secret and tell no one I am a humbug."
</p><p>
They promised to keep his secret, and went back to
their rooms full of hope.
</p>
</div>

<h3><span>CHAPTER 16</span> THE MAGIC POWERS OF THE GREAT HUMBUG</h3>
<div>

<p>
Next morning the Scarecrow said to his friends in
a cheerful voice:
</p><p>
"I am happy because I am going to Oz to get
my brains at last. When I return I’ll be as other men
are." Then he went to the Throne Room, and knocked
at the door.
</p><p>
"Come in," said Oz.
</p><p>
The Scarecrow went in. The little man was sitting by
the window and thinking about something.
</p><p>
”Can I have my brains?" asked the Scarecrow.
</p><p>
"Oh, yes; sit down in that chair, please," replied Oz.
"I’ll have to take your head off to put your brains in
their right place."
</p><p>
"That’s all right," said the Scarecrow. "You can take
my head off because it will be a better one when you
put it on again."
</p><p>
So the Wizard took off the Scarecrow’s head and
pulled out all the straw. Then he took a measure of
bran and mixed it with a measure of needles. Then he
shook them together carefully and filled the top of the
Scarecrow’s head with the mixture and stuffed the rest
with straw again, to hold it in place. Then he put the
Scarecrow’s head on his body again and said to him,
”Now you will be a great man, for you have a lot of new
brains." 
</p><p>
The Scarecrow was both happy and proud.
</p><p>
"Thank you," said the Scarecrow and went back to
his friends.
</p><p>
Dorothy looked at him with curiosity, for many
needles were sticking out of his head.
</p><p>
"How do you feel?" she asked.
</p><p>
”I feel wise indeed," he answered earnestly. "Soon I’ll
know everything."
</p><p>
"Why are those needles sticking out of your head?"
asked the Tin Woodman.
</p><p>
”They show that he is sharp," remarked the Lion.
</p><p>
"Well, I must go to Oz and get my heart," said the
Woodman. So he walked to the Throne Room and
knocked at the door.
</p><p>
"Come in," called Oz, and the Woodman entered and
said, ”Can I have my heart?"
</p><p>
”Very well," answered the little man. “But I’ll have to
cut a hole in your breast, so I can put your heart in the
right place. I hope it won’t hurt you."
</p><p>
"Oh, no," answered the Woodman. "I won’t feel it at
all."
</p><p>
So Oz cut a small hole in the left side of the Tin
Woodman’s breast. Then he brought a pretty heart
from the back room.
</p><p>
”Isn’t it beautiful?" he asked.
</p><p>
"Yes, it is!" replied the Woodman, who was very
happy. "But is it a kind heart?"
</p><p>
"Oh, it is very kind!" answered Oz. He put the heart
in the Woodman’s breast and then closed the hole.
</p><p>
"Now," he said, "you have a heart and you can be
proud of it."
</p><p>
“I am very grateful to you, and will never forget your
kindness," exclaimed the happy Woodman and went
back to his friends.
</p><p>
The Lion now walked to the Throne Room and
knocked at the door.
</p><p>
"Come in," said Oz.
</p><p>
"Can I have my courage?" asked the Lion when he
entered the room.
</p><p>
"Very well," answered the little man; “I will get it for you.
</p><p>
He went to a cupboard and took a green bottle from
the top shelf. He poured the contents of the bottle into
a green-gold dish and placed it before the Cowardly
Lion. Then the Wizard said:
</p><p>
“Drink it. This will give you your courage."
So the Lion drank it all.
"How do you feel now?" asked Oz.
</p><p>
"Full of courage," replied the Lion, who went happily
back to his friends.
</p><p>
When Oz was alone again, he said to himself, "It
was easy to make the Scarecrow and the Lion and the
Woodman happy, because they believed I could do
anything. But I don’t know how to carry Dorothy back
to Kansas."
</p>
</div>


<h3><span>CHAPTER 17</span> HOW THE BALLOON WAS LAUNCHED</h3>
<div>

<p>
For three days Dorothy heard nothing from Oz.
These were sad days for the little girl, although her
friends were all quite happy.
</p><p>
”There are wonderful thoughts in my head," said the
Scarecrow; "but I’m not going to tell you what they are,
for I think you won’t understand them."
</p><p>
“When I walk about my heart is beating in my chest,"
added the Tin Woodman; ”and it is kinder than my old
heart."
</p><p>
"I’m afraid of nothing in the world," remarked the
Lion; "and I’m ready to fight an army of fierce Kalidahs."
</p><p>
So, each of the little company was happy except
Dorothy, who wanted more than ever to get back to
Kansas.
</p><p>
On the fourth day, to her great joy, Oz sent for her,
and when she entered the Throne Room he said:
</p><p>
"Sit down, my dear; I think I know how to get you
out of this country."
</p><p>
”And back to Kansas?" she asked eagerly.
</p><p>
"Well, I’m not sure about Kansas," said Oz, "for I do
not know which way it lies. But the first thing to do is
to cross the desert. Then it will be easy to find your way
home."
</p><p>
"How can I cross the desert?" she asked.
</p><p>
"Well, I’ll tell you what I think," said the little man.
"You see, when I came to this country it was in a balloon.
You also came through the air — a cyclone carried you.
So I believe the best way to get across the desert will be
through the air. Now, I am not a magician and I cannot
make a cyclone; but I believe I can make a balloon."
</p><p>
”How?" asked Dorothy.
</p><p>
"We can make a balloon of silk," said Oz, "and fill it
with hot air. Then we will be able to ﬂy it."
</p><p>
”We!" exclaimed the girl. "Are you going with me?"
</p><p>
"Yes, of course," replied Oz. "1 do not want to be
a humbug any more. One day my people may discover
I am not a Wizard, and then they will be very angry
with me. So I will go back to Kansas with you."
</p><p>
”I’ll be glad to have your company," said Dorothy.
</p><p>
"Thank you," he answered. "Now, let us begin to
work on our balloon. Will you help me sew the silk
together?"
</p><p>
So Oz began to cut the silk and Dorothy sewed it
together. It took Dorothy three days to sew all the silk
together; at last they had a big bag of green silk more
than twenty feet long.
</p><p>
"But we must have a basket to ride in," said Oz. So he
sent the soldier with the green beard for a big clothes
basket and fitted it with many ropes to the bottom of
the balloon.
</p><p>
When it was all ready, Oz told his people that he was
going to make a visit to a great brother Wizard who lived
in the clouds. Everyone came to see the wonderful sight.
</p><p>
Oz ordered to place the balloon in front of the
Palace, and the people looked at it with much curiosity.
The soldiers made a fire, and Oz filled the balloon with
hot air.
</p><p>
Then Oz got into the basket and said to all the
people in a loud voice: 
</p><p>
”I am now going away to make a visit. While I am away
the Scarecrow will rule over you. I order you to obey him."
</p><p>
Now the balloon was ready for the journey back to
Kansas.
</p><p>
"Come, Dorothy!" cried the Wizard. ”Hurry up, or the
balloon will ﬂy away."
</p><p>
"I can’t find Toto anywhere," replied Dorothy, who
did not wish to leave her little dog behind. When
Dorothy at last found him, she picked him up and ran
towards the balloon.
</p><p>
Suddenly the ropes that were holding the balloon
near the ground broke and the balloon rose into the air
without her.
</p><p>
"Come back!" she screamed. ”I want to go, too!"
</p><p>
"I can’t come back, my dear," called Oz from the
basket. "Good-bye!"
</p><p>
"Good-bye!" shouted everyone. The Wizard was
rising every moment farther and farther into the sky.
</p><p>
And that was the last time they ever saw the Great
Oz, the Wonderful Wizard. But the people remembered
him, and said to one another:
</p><p>
"Oz was always our friend. When he was here he
built for us this beautiful Emerald City, and now he is
away and the Wise Scarecrow rules over us."
</p>
</div>



<h3><span>CHAPTER 18</span> AWAY TO THE SOUTH</h3>
<div>

<p>
Dorothy and her friends also felt sorry at losing Oz.
</p><p>
The Scarecrow was now the ruler of the
Emerald City, and although he was not a Wizard
the people were proud of him. ”For," they said, ”there is
not another city in all the world where a stuffed man
rules." And they were quite right.
</p><p>
On the next morning the four travelers met in the
Throne Room. The Scarecrow sat in the big throne and
the others stood before him.
</p><p>
"We are not so unlucky," said the new ruler, "for we
have this Palace and the Emerald City, and we can do
just as we please. Now I am the ruler of this beautiful
City, and I am quite happy about this."
</p><p>
"I am also happy about my new heart," said the Tin
Woodman.
</p><p>
"I am happy because I know that I am brave now,"
said the Lion.
</p><p>
"Stay with us, Dorothy," continued the Scarecrow,
”and we’ll all be happy together."
</p><p>
"But I don’t want to live here," cried Dorothy. "I
want to go to Kansas, and live with Aunt Em and Uncle
Henry."
</p><p>
“Well, then, what can we do?" asked the Woodman.
</p><p>
The Scarecrow decided to think, and he thought
hard. Finally he said:
</p><p>
"Call the Winged Monkeys, and ask them to carry
you over the desert." 
</p><p>
"I never thought of that!" said Dorothy happily. "I’ll
go at once for the Golden Cap."
</p><p>
When she brought it into the Throne Room she
spoke the magic words, and soon the Winged Monkeys
ﬂew in through the open window and stood beside her.
</p><p>
"What is your second order?" asked the Monkey
King and bowed before the little girl. "What do you
wish?"
</p><p>
”I want you to ﬂy with me to Kansas," said Dorothy.
</p><p>
But the Monkey King shook his head.
</p><p>
"We cannot do this," he said, "for we cannot leave
this country. We’ll be glad to serve you in any way in
our power, but we cannot cross the desert. Good-bye."
</p><p>
And with another bow, the Monkey King ﬂew away
through the window and the other Monkeys followed
him.
</p><p>
Dorothy was ready to cry. "This was my second
wish," she said, "and the Winged Monkeys could not
help me."
</p><p>
"It is certainly too bad!" said the Woodman.
</p><p>
The Scarecrow was thinking again.
</p><p>
"Let us call in the soldier with the green beard," he
said at last, ”and ask his advice."
</p><p>
So, when the soldier entered the Throne Room, the
Scarecrow said to him:
</p><p>
"This little girl wishes to cross the desert. How can
she do it?"
</p><p>
"I cannot tell," answered the soldier, "for nobody
ever crossed the desert in the past." 
</p><p>
"Is there no one who can help me?" asked Dorothy
earnestly.
</p><p>
“I think, Glinda can help you," he answered.
“Who is Glinda?" asked the Scarecrow.
</p><p>
”The Witch of the South. She is the most powerful of
all the Witches, and rules over the Quadlings. Besides,
her castle stands on the edge of the desert, so she may
know a way to cross it."
</p><p>
”Glinda is a Good Witch, isn’t she?" asked the child.
</p><p>
"The Quadlings think she is good," said the soldier,
”and she is kind to everyone."
</p><p>
"How can I get to her castle?" asked Dorothy.
</p><p>
"The road is straight to the South," he answered,
"but it is full of dangers to travelers. There are wild
beasts in the woods, and a race of men who do not like
strangers to cross their country. That is why none of the
Quadlings ever come to the Emerald City."
</p><p>
The soldier left them and the Scarecrow said:
</p><p>
"It seems that the best thing Dorothy can do is to
travel to the Land of the South and ask Glinda to help
her. Or Dorothy will never get back to Kansas, where
she wants to return."
</p><p>
"I’ll go with Dorothy," said the Lion, "for I am tired
of your city and I miss the woods and the country. I am
really a wild beast, you know. Besides, Dorothy will
need someone to protect her."
</p><p>

"That is true," agreed the Woodman. "I also will go
with her to the Land of the South. She may need my
help."
</p><p>
“When shall we start?" asked the Scarecrow. 
</p><p>
”Are you going?" they asked, in surprise.
"Yes, I am. I am grateful to her for my new brains. So
I’ll never leave her until she starts back to Kansas."
</p><p>
"Thank you," said Dorothy gratefully. "You are all
very kind to me. But I want to start as soon as possible."
</p><p>
"We’ll go tomorrow morning," answered the
Scarecrow. "So now let us all get ready, for it will be
a long journey."
</p>
</div>

<h3><span>CHAPTER 19</span> ATTACKED BY THE FIGHTING TREES</h3>
<div>

<p>
When the next morning Dorothy and her friends
came to the gate, they saw the Guardian. They
took off their green glasses and gave them to
the man, who put them back into the green box.
</p><p>
"You are now our ruler," he said to the Scarecrow;
”so you must come back to us as soon as possible."
</p><p>
“I certainly will," the Scarecrow replied; "but I must
help Dorothy to get home, first."
</p><p>
They all shook hands with the Guardian of the Gate
and Dorothy said:
</p><p>
"I like your lovely City, and its people who are very
kind. I cannot tell you how grateful I am." 
</p><p>
“My dear," he answered, "it is a pity that you cannot
stay with us. We all know that you wish to return to
Kansas, I hope you will find a way." He then opened
the gate, and they started on their journey towards
the Land of the South. Dorothy was full of hope again,
and the Scarecrow, and the Tin Woodman, and the Lion
were glad that they were helping her.
</p><p>
They now turned and took a last look at the Emerald
City. But they saw only the tops of towers behind the
green walls.
</p><p>
"Oz was not such a bad Wizard, after all," said the
Tin Woodman. "I have a heart and it is beating in my
breast."
</p><p>
"He knew how to give me brains, and very good
brains, too," said the Scarecrow.
</p><p>
”It’s a pity Oz didn’t drink any of the courage he gave
me," added the Lion.
</p><p>
"Oz was a bad Wizard," said Dorothy, "but he was
a good man."
</p><p>
The first day’s journey was through the green fields
that were about the Emerald City on every side. They
slept that night on the grass, and they rested very
well.
</p><p>
In the morning they traveled on until they came to
</p><p>
a thick wood. It was so big that they could not go around
it. So they looked for the place to get into the forest.
</p><p>
The Scarecrow finally discovered a passage between
the thick branches. So he walked forward to it, but just
as he came under the first branches they caught him,
raised him from the ground and threw him among his
friends.
</p><p>
This did not hurt the Scarecrow, but he looked
surprised when Dorothy picked him up. 
</p><p>
"Here is another passage between the trees," called
the Lion.
</p><p>
"Let me try it first," said the Scarecrow, "for it
doesn’t hurt me to fall." He walked up to another
tree, but its branches caught him and threw him back
again.
</p><p>
"This is strange," exclaimed Dorothy. "What shall we
do?"
</p><p>
"It seems to me that the trees want to fight us, and
stop our journey," remarked the Lion.
</p><p>
"I believe I will try it myself," said the Woodman.
He raised his axe and walked up to the trees. When
a big branch went down to catch him the Woodman
chopped it off with his axe. At once the tree began to
shake all its branches, and the Tin Woodman passed
safely under it.
</p><p>
"Come on!" he shouted to the others. "Be quick!"
They all ran forward and passed under the tree without
harm.
</p><p>
The other trees of the forest did nothing to keep
them back, so the four travelers walked through the
trees until they came to the farther edge of the wood.
Then, to their surprise, they saw before them a wild
country and another forest. The trees here were bigger
and older than in the forest they left behind.
</p>

<h3><span>CHAPTER 2</span> THE COUNCIL WITH THE MUNCHKINS</h3>
<div>


<p>
A sudden shock woke Dorothy up. Toto put his
cold little nose into her face and began to bark.
Dorothy sat up. The house was not moving and it
wasn’t dark anymore because the bright sunshine came
in through the window of the little room. She jumped
from her bed, ran to the door and opened it.
</p><p>
The little girl gave a cry of surprise and looked about
her; her eyes were growing bigger and bigger at the
wonderful sights she saw.
</p><p>
The house was in the middle of a country of great
beauty. There was lovely green grass all about, and tall
trees with rich and fine fruits. Beautiful ﬂowers were
everywhere, and colourful birds sang in the trees. A
little way off there was a small stream that ran along
between green banks. It was a very pleasant sight for
a little girl from the dry, gray prairies.
</p><p>
While she was standing in the doorway and looking
eagerly at the strange and beautiful sights, she saw
a group of strange people that was coming towards her.
They were not as big as the grown-up people; but they
were not very small either. In fact, they seemed about
as tall as Dorothy, who was a well-grown child for her
age, although they were many years older.
</p><p>
Three were men and one was a woman, and all
of them wore strange clothes. They wore round
pointed hats with little bells that made sweet sounds
as they moved. The hats of the men were blue; the
little woman’s hat was white, and she wore a long
white dress with little stars that shone in the sun like
diamonds. The men wore blue clothes and black boots
with blue tops. The men were about as old as Uncle
Henry, for two of them had beards. But the little woman
was much older. There were wrinkles all over her
face, her hair was almost white, and she walked with
difficulty.
</p><p>
When these people came up to the house, they
stopped and whispered among themselves. It seemed
to Dorothy that they were afraid to come farther. But
the little old woman walked up to Dorothy, made a low
bow and said, in a sweet voice: 
</p><p>
”You are welcome, the great Sorceress, to the land of
the Munchkins. We are so grateful to you because you
killed the Wicked Witch of the East, and set our people
free."
</p><p>
Dorothy listened to this speech with wonder. What
did the little woman mean? Why did she call her
a sorceress? Well, she didn’t kill anybody. Dorothy was
a harmless little girl, who was many miles from home.
</p><p>
But the little woman was waiting for her answer; so
Dorothy said:
</p><p>
”You are very kind, but there must be some mistake.
Why do you say that I killed somebody?"
</p><p>
"Your house did, anyway," replied the little old
woman, with a laugh, ”and that is the same thing. See!"
she pointed to the corner of the house. "There are her
two feet under the bottom of your house."
</p><p>
Dorothy looked, and cried out in fear. There just
under the corner of the house, two feet in silver shoes
with pointed toes were sticking out.
</p><p>
”Oh, dear!" cried Dorothy, in dismay. ”The house fell
on her. What shall we do?"
</p><p>
“There is nothing to do," said the little woman.
</p><p>
”But who was she?" asked Dorothy.
</p><p>
"She was the Wicked Witch of the East," answered
the little woman. "All the Munchkins were her slaves
for many years. Now they are all free, and are grateful
to you for your help."
</p><p>
”Who are the Munchkins?" asked Dorothy.
</p><p>
“They are the people who live in this land of the East
where the Wicked Witch ruled."
</p><p>
"Are you a Munchkin?" asked Dorothy.
</p><p>
"No, but I am their friend, although I live in the land
of the North. When they saw the Witch of the East was
dead the Munchkins sent for me, and I came at once.
I am the Witch of the North."
</p><p>
”Oh, dear!" cried Dorothy. ”Are you a real witch?"
</p><p>
"Yes, I am," answered the little woman. "But I am
a good witch, and the people love me. But I am not as
powerful as the Wicked Witch was who ruled here, so
I couldn’t set the people free myself."
</p><p>
”But I thought all witches were wicked," said the girl,
who was a little afraid of a real witch.
</p><p>
"Oh, no, that is a great mistake. There were only
four witches in all the Land of Oz, and two of them,
those who live in the North and the South, are good
witches. I know this is true, for I am one of them myself.
Those who lived in the East and the West were wicked
witches; but now there is only one Wicked Witch in all
the Land of Oz — the one who lives in the West."
</p><p>
”But," said Dorothy, after a moment’s thought, ”Aunt
Em says, ‘There are no witches anymore.’"
</p><p>
“Who is Aunt Em?" asked the little old woman.
</p><p>
"She is my aunt who lives in Kansas, where I came
from."
</p><p>
The Witch of the North thought for a while. Then she
looked up and said, “I do not know where Kansas is. But
tell me, is it a civilized country?"
</p><p>
”Oh, yes, it is," replied Dorothy.
</p><p>
"Then that explains everything. In the civilized
countries I believe there are no witches, wizards,
sorceresses, or magicians left. But, you see, the Land of
Oz is not civilized, because a desert cuts it off from all
the rest of the world. That’s why we still have witches
and wizards among us."
</p><p>
“Who are the wizards?" asked Dorothy.
</p><p>
"Oz himself is the Great Wizard," answered the
Witch in a whisper. "He is more powerful than all the
rest of us together. He lives in the City of Emeralds."
</p><p>
Dorothy was going to ask another question, but just
then the Munchkins, who were still standing by, gave
a loud shout and pointed to the corner of the house.
</p><p>
”What is it?" asked the little old woman, and looked,
and began to laugh. The only thing left of the dead
Witch was the silver shoes.
</p><p>
"She was so old," explained the Witch of the North,
”that she dried up quickly in the sun. That is the end of
her. But the silver shoes are yours, and you will have
to wear them." She reached down and picked up the
shoes; then she shook the dust out of them and gave
them to Dorothy.
</p><p>
"The Witch of the East was proud of those silver
shoes," said one of the Munchkins, "and there is some
charm in them; but we don’t know what it is."
</p><p>
Dorothy carried the shoes into the house and placed
them on the table. Then she came out again to the
Munchkins and said:
</p><p>
"I want to get back to my aunt and uncle, for I am
sure they will worry about me. Can you help me find
my way?"
</p><p>
The Munchkins and the Witch first looked at one
another, and then at Dorothy, and then shook their
heads.
</p><p>
"At the East, not far from here," said one, "there is
a great desert that no one can cross."
</p><p>
”It is the same at the South," said another Munchkin.
"The South is the country of the Quadlings."
</p><p>
"It is the same at the West," said the third man. “It is
the country of the Winkies and the Wicked Witch of the
West rules it. Don’t go there or she will make you her
slave."
</p><p>
"The North is my home," said the old lady, "and it is
the same there. The great desert is all around this Land
of Oz. I’m afraid, my dear, you will have to live with us."
</p><p>
Dorothy began to cry at this, for she felt lonely
among all these strange people. Her tears made
the kind Munchkins sad, and they took out their
handkerchiefs and began to cry also. As for the little old
woman, she took off her cap, put its pointed end to her
nose, and began to count ”One, two, three" in a solemn
voice. At once the cap changed to a slate with some
words on it. The little old woman took the slate from
her nose, and read the words:
</p><p>
"LET DOROTHY GO TO THE CITY OF EMERALDS"
</p><p>
Then she asked, ”Is your name Dorothy, my dear?"
</p><p>
”Yes," answered the child.
</p><p>
"Then you must go to the City of Emeralds. Perhaps
Oz will help you."
</p><p>
“Where is this city?" asked Dorothy.
</p><p>
”It is right in the center of the country, and the Great
Wizard Oz rules it."
</p><p>
”Is he a good man?" said the girl anxiously.
</p><p>
"He is a good Wizard. But I cannot tell you if he
is a man or not, because he never shows himself to
anybody."
</p><p>
"How can I get there?" asked Dorothy.
</p><p>
"You must walk. It is a long journey, through
a country that is sometimes pleasant and sometimes
dark and terrible. But I will use all the magic powers
I have to keep you from harm."
</p><p>
”Won’t you go with me?" begged the girl.
"No, I cannot do that," replied the little old woman,
"but I will give you my kiss, and no one will harm you."
</p><p>
She came close to Dorothy and kissed her on the
forehead. Where her lips touched the girl they left
a round, shining mark, as Dorothy found out soon after.
</p><p>
”The road of yellow brick will lead you to the City of
Emeralds," said the Witch, ”so you cannot miss it. When
you get to Oz do not be afraid of him, but tell your story
and ask him to help you. Good-bye, my dear."
</p><p>
The three Munchkins bowed low to her and wished
her a pleasant journey, after which they walked away
through the trees. The Witch gave Dorothy a friendly
little nod, turned around on her left heel three times,
and disappeared.
</p>

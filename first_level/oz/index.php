Then the Witch looked at the big Lion and asked,
“What will you do when Dorothy goes home?" 
</p><p>
"Over the hill of the Hammer-Head creatures," he
answered, "there is a great old forest, and all the beasts
that live there want me to become their King. I want to
get back to this forest and rule over the beasts there.
My life will be happy then."
</p><p>
"My third order to the Winged Monkeys," said
Glinda, "will be to carry you to your forest. And when
I use up the powers of the Golden Cap, I’ll give it to the
King of the Monkeys. He and his people will become
free then."
</p><p>
The Scarecrow and the Tin Woodman and the
Lion and Dorothy were very happy now, and Dorothy
exclaimed:
</p><p>
"Thank you for your kindness! You are certainly as
good as you are beautiful! But how shall I get back to
Kansas?"

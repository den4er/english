<h3><span>CHAPTER 3</span> HOW DOROTHY SAVED THE SCARECROW</h3>
<div>

<p>
When Dorothy was alone again she began to feel
hungry. So she went to the cupboard and cut
herself some bread and cheese. She gave some
to Toto. Then she started to get ready for the journey to
the City of Emeralds.
</p><p>
Dorothy had only one other dress, but that was
clean. The dress was white and blue. It was quite old,
but it was still pretty. The girl washed herself carefully,
dressed herself in the clean dress. She took a little
basket and filled it with bread from the cupboard. Then
she looked down at her feet and saw how old her shoes
were.
</p><p>

"They won’t stand a long journey, Toto," she said.
And Toto looked up into her face with his little black
eyes and barked to show he knew what she meant. 
</p><p>
At that moment Dorothy saw the silver shoes of the
Witch of the East.
</p><p>
"I hope they will fit me," she said to Toto. ”They will
be good for a long walk, for they won’t wear out."
</p><p>
She took off her old shoes and tried on the silver
ones, which fitted her.
</p><p>
Finally she picked up her basket.
</p><p>
"Come along, Toto," she said. "We will go to the
Emerald City and ask the Great Oz how to get back to
Kansas again."
</p><p>
She closed the door and put the key carefully in the
pocket of her dress. And so, with Toto behind her, she
started on her journey.
</p><p>
There were several roads near by, but it did not
take her long to find the one of yellow bricks. Soon
Dorothy and Toto were walking towards the Emerald
City. The sun shone bright and the birds sang sweetly.
As Dorothy walked along, she saw how pretty the
country was about her. There were neat fences at the
sides of the road and beyond them were fields of grain
and vegetables. It was clear that the Munchkins were
good farmers. Once in a while she passed a house, and
the people came out to look at her and bow low as she
went by; for everyone knew about her great deed and
was grateful to her. The houses of the Munchkins were
unusual-looking buildings, for each was round. All the
houses were blue, for in this country of the East blue
was the favorite color.
</p><p>
Towards evening, when Dorothy was tired with
her long walk and began to look for a place to pass
the night, she came to a house rather larger than the
rest. In front of the house many men and women were
dancing, laughing and singing, and a big table near
by was full of fine fruits and nuts, pies and cakes, and
many other good things to eat.
</p><p>
The people greeted Dorothy kindly, and invited her
to supper and to pass the night with them. This was the
home of one of the richest Munchkins in the land, and
his friends and he were celebrating because now they
were free from the Wicked Witch.
</p><p>
Dorothy ate a hearty supper and sat upon a chair
and watched the dancing people.
</p><p>
When Boq, the rich Munchkin, saw her silver shoes
he said, ”You must be a great sorceress."
</p><p>
"Why?" asked the girl.
</p><p>
"Because you wear the silver shoes of the Wicked
Witch. Besides, you have white in your dress, and only
witches and sorceresses wear white."
</p><p>
”My dress is blue and white," said Dorothy.
</p><p>
"It is kind of you to wear that," said Boq. "Blue is the
color of the Munchkins, and white is the witch color. So
we know you are a friendly witch."
</p><p>
Dorothy did not know what to say to this, for all the
people thought that she was a witch, and she knew very
well she was only an ordinary little girl in a strange
land miles and miles away from home.
</p><p>
When she got tired of watching the dancing, Boq
led her into the house, where he gave her a room
with a pretty bed in it. Dorothy slept soundly in it till
morning, with Toto beside her.
</p><p>
She ate a hearty breakfast, and watched Munchkin
children, who played with Toto and pulled his tail and
laughed. There were no dogs in the Munchkin land and
Toto was a fine curiosity to all the people.
</p><p>
“How far is it to the Emerald City?" the girl asked.
</p><p>
”I do not know," answered Boq gravely, “for we never
go there. It is better for people to keep away from Oz, if
they do not have business with him. But it is a long way
to the Emerald City, and it will take you many days. The
country here is rich and pleasant, but you must pass
through rough and dangerous places before you reach
the end of your journey."
</p><p>
This worried Dorothy a little, but she knew that only
the Great Oz could help her get to Kansas again, so she
bravely decided not to turn back.
</p><p>
"Thank you for your kindness and your help,"
she said to Boq, and again started along the road of
yellow brick. When she grew tired she stopped to rest.
Dorothy climbed to the top of the fence beside the road
and sat down. There was a great cornfield beyond the
fence, and not far away she saw a Scarecrow. It was
high on a stick to keep the birds from the cornfield.
</p><p>
Dorothy looked thoughtfully at the Scarecrow. Its
head was a small sack with straw, with bright painted
eyes, nose, and mouth to make a face. An old, pointed
blue hat was on his head, and the rest of the figure was
old clothes, also stuffed with straw. On the feet there
were some old boots with blue tops, such as every man
wore in this country. 
</p><p>
While Dorothy was looking earnestly into the
painted face of the Scarecrow, she saw that one of the
eyes slowly winked at her. She didn’t believe it at first,
for scarecrows in Kansas never wink; but soon the
figure nodded its head to her in a friendly way. Then
she climbed down from the fence and walked up to it,
while Toto ran around the stick and barked.
</p><p>
”Good day," said the Scarecrow, rather timidly.
</p><p>
”Did you speak?" asked the girl, in wonder.
</p><p>
"Certainly," answered the Scarecrow. "How do you
do?"
</p><p>
"I’m pretty well, thank you," replied Dorothy politely.
"How do you do?"
</p><p>
"I’m not feeling well," said the Scarecrow, with
a smile. ”It is so boring to stay up on this stick night and
day to scare away crows."
</p><p>
"Can’t you get down?" asked Dorothy.
”No, I cannot. Will you help me? I’ll be so grateful to you."
</p><p>
Dorothy reached up both arms and lifted the figure
off the stick, for it was quite light.
</p><p>
”Thank you very much," said the Scarecrow, when he
was on the ground. “I feel like a new man."
</p><p>
Dorothy was surprised at this, for scarecrows in
Kansas never speak or bow or walk.
</p><p>
"Who are you?" asked the Scarecrow, as they walked
along. ”And where are you going?"
</p><p>
"My name is Dorothy,” said the girl, "and I am going
</p><p>
to the Emerald City, to ask the Great Oz to send me back
to Kansas.”
</p><p>
"Where is the Emerald City?” he said. "And who is
Oz?”
</p><p>
"Don’t you know?” she asked him, in surprise. 
</p><p>
"No, I don’t. I don’t know anything. You see, I have
no brains at all; there’s only straw inside my head,” he
answered sadly.
</p><p>
"Oh,” said Dorothy, ”I’m so sorry for you.”
</p><p>
"Do you think,” he asked, "Oz can give me some
brains?”
</p><p>
“I cannot tell,” she replied, "but you may come to the
Emerald City with me, if you like.”
</p><p>
"That is good,” said the Scarecrow. "You see,” he
continued, "I don’t mind my stuffed legs and arms and
body, because nothing can hurt me. But I do not like it
when people call me a fool because my head is full of
straw instead of with brains, as yours is.”
</p><p>
”I understand how you feel,” said the little girl, who
was really sorry for him. "Come to the Emerald City
with me, and I’ll ask Oz to do all he can for you.”
</p><p>
“Thank you,” he answered gratefully.
</p><p>
They walked back to the road. Dorothy helped him
get over the fence, and they started along the path of
yellow brick for the Emerald City.
</p><p>
Toto did not like the Scarecrow at first, and he often
growled at him in an unfriendly way.
</p><p>
"Don’t mind Toto," said Dorothy to her new friend.
“H</p><p>e never bites." 

”Oh, I’m not afraid," replied the Scarecrow. ”He can’t
hurt the straw. Let me carry that basket for you. I do
not mind it, for I can’t grow tired. I’ll tell you a secret,"
he continued, as he walked along. “There is only one
thing in the world I am afraid of."
</p><p>
"What is that?" asked Dorothy; "the Munchkin
farmer who made you?"
</p><p>
"No," answered the Scarecrow; “it’s fire."
</p>

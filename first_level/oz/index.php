<h3><span>CHAPTER 1</span> THE CYCLONE</h3>
<div> 

<p>
Dorothy lived in the middle of the great Kansas
prairies, with Uncle Henry, who was a farmer, and
Aunt Em, who was the farmer’s wife. Their house
was small. There were four walls, a floor and a roof,
which made one room; and in this room there was an
old stove, a cupboard for the dishes, a table, three or
four chairs, and the beds. Uncle Henry and Aunt Em had
a big bed in one corner, and Dorothy had a little bed in
another corner. There was no cellar in the house — only
a small, dark hole in the ground, where the family could
hide from cyclones. They called it a cyclone cellar.
</p><p>
When Dorothy stood in the doorway and looked
around, she could see only the great gray prairie on
every side. There wasn’t a tree or a house around —
only a gray country that reached the horizon. The sun
was so hot that the grass was not green; it was the
same gray color that Dorothy could see everywhere.
Even the house was as gray as everything else.
</p><p>
When Aunt Em came there to live she was a young,
pretty wife. Now her eyes and lips were gray; she was
thin and never smiled. When she heard Dorothy’s
cheerful voice she looked at the little girl and thought,
"It’s strange that she can find anything to laugh at."
</p><p>
Uncle Henry never laughed. He worked hard from
morning till night and did not know what joy was. He
was gray also, from his long beard to his rough boots,
and he looked solemn, and didn’t speak very often.
</p><p>
Only Toto made Dorothy laugh, and didn’t let her
become as gray as her aunt and uncle, and the prairie
around. Toto was not gray; he was a little black dog,
with long hair, small black eyes, and a funny nose. Toto
played all day long, and Dorothy played with him, and
loved him very much.
</p><p>
But today they were not playing. Uncle Henry was
sitting in the doorway and looking anxiously at the
sky, which was even grayer than usual. Dorothy was
standing in the doorway with Toto in her arms, and
looking at the sky too. Aunt Em was washing the dishes.
</p><p>
From the far north they heard a low howl of the
wind. Then they heard a howl of the wind from the
south. Uncle Henry stood up.
</p><p>
"A cyclone is coming, Em," he called to his wife. "I’ll
go and look after the cows and horses." Then he ran
towards the sheds.
</p><p>
Aunt Em stopped washing the dishes and came to
the door. She looked at the sky and saw the coming
danger.
</p><p>
”Quick, Dorothy!" she screamed. "Run for the cellar!"
</p><p>
Toto jumped out of Dorothy’s arms and hid under
the bed, and the girl rushed to get him. Aunt Em opened
the door in the ﬂoor and climbed down the steps into
the small, dark hole. Dorothy caught Toto at last and
started to follow her aunt. When she was almost near
the cellar, she heard a great howl from the wind, and
the house shook so hard that she fell down suddenly
upon the ﬂoor.
</p><p>
Then a strange thing happened.
</p><p>
The house turned around two or three times and
rose slowly through the air. "It feels like going up in
a balloon," thought Dorothy.
</p><p>
The north and south winds met where the house
stood, and made it the center of the cyclone. The winds
raised the house up higher and higher, until it was at
the very top of the cyclone; and carried it miles and
miles away.
</p><p>
It was very dark, and the wind howled around
Dorothy. She was so afraid.
</p><p>
Toto also did not like it. He ran about the room and
barked loudly; but Dorothy sat quite still on the ﬂoor
and waited to see what was going to happen next.
</p><p>
Once Toto got too near the open cellar door, and
almost fell in; but Dorothy caught Toto by the ear, and
closed the cellar door quickly.
</p><p>
At first the little girl worried that the house was
going to break to pieces and kill her and Toto when it
fell again; but as the hours passed and nothing terrible
happened, she stopped worrying and decided to wait
and see what the future was going to bring her. At last,
she got to her bed, and lay down upon it; and Toto
followed and lay down beside her. Soon Dorothy closed
her eyes and fell fast asleep.

<h3><span>CHAPTER 4</span> THE ROAD THROUGH THE FOREST</h3>
<div>

<p>
After a few hours the road became rough, and the
A walking grew so difficult that the Scarecrow often 
stumbled over the yellow bricks, and Dorothy
had to walk around the holes in the road. The farms
were not as neat here as they were farther back. There
were fewer houses and fewer fruit trees, and the farther
they went the more lonesome the country became.
</p><p>
At noon they sat down by the roadside, near a little
stream, and Dorothy opened her basket and got out
some bread. She offered a piece to the Scarecrow, but
he said, ”I am never hungry, and it is a lucky thing I am
not, for my mouth is only painted."
</p><p>
Dorothy nodded and went on eating her bread.
</p><p>
"Tell me something about yourself and the country
you came from," said the Scarecrow after her dinner. So
she told him all about the cyclone, and Kansas, and how
gray everything was there. 
</p><p>
The Scarecrow listened carefully, and said, ”I cannot
understand why you want to leave this beautiful
country and go back to the dry, gray place you call
Kansas."
</p><p>
"That is because you have no brains," answered the
girl. "Kansas is gray, but I want to live there and not in
any other country. There is no place like home."
</p><p>
The Scarecrow sighed.
</p><p>
"Of course I cannot understand it," he said, "for
I have no brains."
</p><p>
"Will you tell me a story, while we are resting?"
asked the child.
</p><p>
The Scarecrow looked at her sadly, and answered:
</p><p>
"I really know nothing. The farmer made me only
the day before yesterday. I don’t know what happened
in the world before that time. Then the farmer carried
me under his arm to the cornfield, and set me up on
a tall stick, where you found me. He soon walked away
and left me alone.
</p><p>
"I did not like to be alone. So I tried to walk after
him but I couldn’t. So I had to stay on that stick. It was
a lonely life, for I had nothing to think of. Many crows
and other birds ﬂew into the cornfield, but as soon
as they saw me they ﬂew away again because they
thought I was a Munchkin; and this pleased me and
made me feel that I was quite an important person.
</p><p>
By and by an old crow ﬂew near me. He looked at me
carefully and then sat on my shoulder and said:
</p><p>
"That farmer can’t fool me in this clumsy way. Any
clever crow can see that you are only a stuffed man
full of straw." Then he ﬂew down and ate all the corn
he wanted. The other birds saw it and came to eat the
corn too, so in a short time there were a lot of them
about me.
</p><p>
“I felt sad at this, for it showed I was not such a good
Scarecrow after all; but the old crow comforted me. He
said: "Brains are the only important things in this world
for men, crows, and other beasts. When you get brains,
you’ll become as good as any of the farmers, and even
better than some of them.’
</p><p>
"Then the crows ﬂew away. I thought this over, and
decided to try hard to get some brains. By good luck
you came along and pulled me off the stick, and from
what you say I am sure the Great Oz will give me brains
as soon as we get to the Emerald City."
</p><p>
"I hope so," said Dorothy earnestly, "for you really
want to have them."
</p><p>
"Oh, yes; I want it so much," replied the Scarecrow.
"It is such an uncomfortable feeling to know that you
are a fool."
</p><p>
"Well," said the girl, "let us go." And she gave the
basket to the Scarecrow.
</p><p>
There were no fences at all beside the road, and
there were no fields of grain or vegetables now, and the
land was rough. Towards evening they came to a great
forest, where the trees grew so big and close together
that their branches met over the road of yellow brick.
It was quite dark under the trees, but the travelers did
not stop, and went on into the forest.
</p><p>
"This road goes in, so it must come out," said the
Scarecrow, ”and as the Emerald City is at the other end
of the road, we must follow it."
</p><p>
”Anyone knows that," said Dorothy.
</p><p>
"Certainly; that is why I know it," answered the
Scarecrovm
</p><p>
After an hour or so it became very dark, and they
were stumbling along in the darkness. Dorothy could
not see anything at all, but the Scarecrow said:
</p><p>
"I can see as well as by day."
</p><p>
So she took him by his arm and continued her way
quite well.
</p><p>
"As soon as you see any house, or any place where
we can pass the night," she said, "you must tell me; for
it is very uncomfortable to walk in the dark."
</p><p>
Soon after that the Scarecrow stopped.
</p><p>
”I see a little cottage at the right of us," he said. ”Shall
we go there?"
</p><p>
"Yes, let’s go there," answered the child. "I am very
tired."
</p><p>
So the Scarecrow led her through the trees until
they reached the cottage, and Dorothy entered and
found a bed of dry leaves in one corner. She lay down
at once, and with Toto beside her soon fell asleep. The
Scarecrow, who was never tired, stood up in another
corner and waited until morning came.
</p>
</div>

<h3><span>CHAPTER 5</span> THE RESCUE OF THE TIN WOODMAN</h3>
<div>

<p>
When Dorothy woke up the sun was shining
through the trees and Toto was running after
birds and squirrels. She sat up and looked
around her. The Scarecrow was still standing in his
corner and waiting for her. 
</p><p>
”We must go and search for water," she said to him.
”Why do you want water?" he asked.
</p><p>
"To wash my face after the dust of the road, and to
drink, so the dry bread will not stick in my throat."
</p><p>
"It must be inconvenient to be a live person," said
the Scarecrow thoughtfully, ”for you must sleep, and eat
and drink. But you have brains, and it makes up for the
trouble."
</p><p>
They left the cottage and walked through the trees
until they found a little stream of clear water, where
Dorothy drank and bathed and ate her breakfast. She
saw there was not much bread in the basket, and the
girl was thankful the Scarecrow did not have to eat
anything, for there was hardly enough for herself and
Toto for the day.
</p><p>
When she finished her meal, and was ready to go
back to the road of yellow brick, she heard a deep groan
nearby.
</p><p>
"What was that?" she asked timidly.
</p><p>
"I don’t know," replied the Scarecrow; "but we can
go and see." 
</p><p>
Just then they heard another groan, and this time
the sound came from behind them. They turned and
walked through the forest a few steps, when Dorothy
saw something that was shining in the sunlight that
came through the trees. She ran to the place and then
stopped, with a little cry of surprise.
</p><p>
There was a man made entirely of tin. He was
standing beside a half-chopped tree. In his raised hands
there was an axe. The man stood motionless, and it
seemed to Dorothy that he could not move at all.
</p><p>
Dorothy looked at him in surprise, and so did the
Scarecrow, while Toto barked loudly at the tin man.
</p><p>
”Did you groan?" asked Dorothy.
</p><p>
"Yes," answered the tin man, "I did. I can’t move.
It happened to me a year ago. Can you help me?"
</p><p>
The sad voice in which the man spoke moved the
girl and she asked softly:
</p><p>
"What can I do for you?"
</p><p>
"Get an oil-can and oil my joints," he answered.
"They rusted so badly that I cannot move them at all.
As soon as you oil them well, I’ll be all right again. You
will find an oil-can on a shelf in my cottage."
</p><p>
Dorothy at once ran back to the cottage and found
the oil-can, and then she returned and asked anxiously,
“Where are your joints?"
</p><p>
“Oil my neck, first," replied the Tin Woodman. So she
oiled it. When the man could turn his tin head he said:
</p><p>
"Now oil the joints in my arms." And Dorothy oiled
them. When the tin man’s joints were quite free from
rust, he gave a sigh of relief, lowered his axe and put it
near the tree.
</p><p>
"This is a great comfort," he said. "I rusted a year
ago, and I’m glad to be able to put it down at last. Can
you oil the joints of my legs now? Then I’ll be all right
once more."
</p><p>
So they oiled his legs until he could move them
freely. The tin man was a very polite creature, and very
grateful, so he said:
</p><p>
"Thank you for your help. But what are you doing
here?"
</p><p>
"We are on our way to the Emerald City to see the
Great Oz," Dorothy answered, ”and we stopped at your
cottage to pass the night."
</p><p>
”Why do you wish to see Oz?" he asked.
</p><p>
"I want him to send me back to Kansas, and the
Scarecrow wants him to put some brains into his head,"
she replied.
</p><p>
The Tin Woodman thought it over for a moment.
Then he said:
</p><p>
”Do you think Oz can give me a heart?"
“I think he can," Dorothy answered.
</p><p>
"Can I go to the Emerald City with you? I hope Oz
will help me."
</p><p>
"Come along," said the Scarecrow.
</p><p>
”I’ll be glad to have your company," added Dorothy.
</p><p>
So the Tin Woodman picked up his axe and they all
passed through the forest until they came to the road
of yellow brick. 
</p><p>
"Can you put the oil-can in your basket?" asked the
Tin Woodman. "I always rust when it rains. So, you will
need the oil-can to oil me."
</p><p>
Dorothy and the Scarecrow were very lucky to have
the Tin Woodman in their company, for soon they came
to a place where the trees and branches grew so thick
over the road that the travelers could not pass. But the
Tin Woodman took his axe and chopped so well that
soon he cleared a passage for the travelers.
</p><p>
Dorothy was thinking so carefully as they walked
along that she did not notice when the Scarecrow
stumbled into a hole and rolled over to the side of the
road. So he had to ask Dorothy for help.
</p><p>
"Why didn’t you walk around the hole?" asked the
Tin Woodman.
</p><p>
“I don’t know enough," replied the Scarecrow. “There
is only straw in my head, you know, and that is why
I am going to Oz to ask him for some brains."
</p><p>
"Oh, I see," said the Tin Woodman. "But, after all,
brains are not the best things in the world."
</p><p>
”Do you have any?" asked the Scarecrow.
</p><p>
"No, my head is quite empty," answered the
Woodman. "But once I had brains, and a heart also, so
I know what it is like. Now I want to have a heart."
</p><p>
”And why is that?" asked the Scarecrow.
</p><p>
"I will tell you my story, and then you will know."
</p><p>
So, while they were walking through the forest, the
Tin Woodman told them his story: 
</p><p>
"I was the son of a woodman who chopped down
trees in the forest and sold the wood for a living. When
I grew up, I became a woodman too, and after my
father’s death I took care of my old mother as long as
she lived. Then I made up my mind to marry because
I didn’t want to live alone.
</p><p>
"There was one of the Munchkin girls who was very
beautiful. I loved her with all my heart. ‘I will marry you,’
she promised, ‘but first you have to earn enough money
to build a better house for me.’ So I began to work harder
than ever. But the girl lived with an old woman who did
not want her to marry anyone. She was very lazy and
wanted the girl to remain with her and do the cooking
and the housework. So the old woman went to the
Wicked Witch of the East, and promised her two sheep
and a cow for her help. The Wicked Witch charmed my
axe, and when I was chopping down a tree one day, the
axe slipped all at once and cut off my left leg.
</p><p>
"This was a great misfortune, for I knew a man with
one leg could not be a good woodman. So I went to
a tinsmith. ‘Will you make a new leg out of tin for me?’
I asked him. So he made a tin leg for me, and it worked
very well. But this made the Wicked Witch of the East
angry. When I began to chop again, my axe slipped
and cut off my right leg. Again I went to the tinsmith,
and again he made me a leg out of tin. After this the
charmed axe cut off my arms, one after the other; but
the tinsmith made me tin ones. Then one day the axe
slipped and cut off my head, and at first I thought that
was the end of me. But the tinsmith saw it and made
me a new head out of tin. 
</p><p>
"I thought that my bad luck was over, and I worked
harder than ever; but the Wicked Witch was a very
cruel enemy. She thought of a new way to kill my love
for the beautiful Munchkin girl, and made my axe slip
again and cut my body into two halves. Once more the
tinsmith came to my help and made me a body of tin.
Then he joined my tin arms and legs and head to it, and
I could move around well again. But now I had no heart,
so I lost all my love for the Munchkin girl and didn’t
marry her. I think she is still living with the old woman,
and I hope she is still waiting for me to come after her.
</p><p>
”My body shone so brightly in the sun that I felt very
proud ofit and no axe could cut me. There was only one
danger — that my joints could rust; but I kept an oil-
can in my cottage and oiled myself every time I needed
it. But one day I forgot to do this. The rain caught me
and my joints rusted. So I had to stand in the woods
until you came to help me. It was a terrible time. But my
misfortune gave me time to think. Now I know that the
loss of my heart is my greatest loss. While I was in love
I was the happiest man in the world; but no one can love
without a heart, and so I want to ask Oz to give me one.
Then I will go back to the Munchkin girl and marry her."
</p><p>
The Tin Woodman’s story made Dorothy and the
Scarecrow sad, but now they knew why he wanted to
get a new heart so much.
</p><p>
”All the same," said the Scarecrow, ”I’ll ask for brains
instead of a heart; for a fool does not know what to do
with a heart even if he has one."
</p><p>
"I’ll take the heart," replied the Tin Woodman; "for
brains do not make one happy, and happiness is the
best thing in the world."
</p><p>
Dorothy did not say anything, for she could not
decide which of her two friends was right.
</p><p>
"As soon as I get back to Kansas and Aunt Em, I’ll
forget all about the Scarecrow, the Woodman, and their
problems," she thought.
</p><p>
What worried her most was that there was hardly
enough bread for herself and Toto for another meal.
Although the Woodman and the Scarecrow never ate
anything, she was a girl, and Toto was a dog, and they
could not live without food.
</p>
</div>


<h3><span>CHAPTER 6</span> THE COWARDLY LION</h3>
<div>


<p>
Dorothy and her friends were still walking through 
the thick woods, and along the road of yellow 
bricks. But now there were many dry branches
and dead leaves from the trees on the road, and the
walking was quite hard.
</p><p>
There were few birds in this part of the forest, for
birds love the open country where there is much
sunshine. But now and then they could hear a growl
from some wild animal deep in the forest. These sounds
made the little girl’s heart beat fast, for she did not
know what made them; but Toto knew, and he walked
close to Dorothy’s side, and did not even bark back.
</p><p>
“How long will it take us,” the child asked of the Tin
Woodman, ”to leave the forest?”
</p><p>
"I cannot tell,” answered the Woodman, "for I don’t
know the way to the Emerald City. But my father went
there once, when I was a boy. ‘It is a long journey through
a dangerous country,’ he said, ‘although nearer to the
city where Oz lives the country is beautiful.’ But I am not
afraid because I have my oil-can, and nothing can hurt the
Scarecrow. You have upon your forehead the mark of the
Good Witch’s kiss, and that will protect you from harm.”
</p><p>
”But Toto!” said the girl anxiously. “What will protect
him?”
</p><p>
"We must protect him ourselves when he is in
danger,” replied the Tin Woodman.
</p><p>
Just as he spoke a terrible roar came from the
forest, and the next moment a great Lion jumped into
the road. With his big paw he struck the Scarecrow
and sent him over to the edge of the road, and then
he struck the Tin Woodman with his sharp claws.
But, to the Lion’s surprise, he did not hurt the tin
man, although the beast knocked him down, and the
Woodman fell down and lay still.
</p><p>
When little Toto saw the Lion, he barked loudly
and ran towards the enemy. But when the great beast
opened his mouth to bite the dog, Dorothy rushed
forward and struck the Lion upon his nose as hard as
she could, while she cried out:
</p><p>
”Don’t bite Toto! It’s a shame for a big beast like you
to bite a poor little dog!”
</p><p>
"I didn’t bite him," said the Lion, as he rubbed his
nose with his paw.
</p><p>
”No, but you tried to," she replied. ”You are just a big
coward."
</p><p>
"I know it," said the Lion, in shame. "But how can
I help it?"
</p><p>
"I don’t know, I’m sure. How could you strike
a stuffed man, like the poor Scarecrow!"
</p><p>
"A stuffed man?" asked the Lion, in surprise, while
Dorothy picked up the Scarecrow and set him upon his
feet again.
</p><p>
"Of course he is a stuffed man," replied Dorothy, who
was still angry.
</p><p>
"That’s why he went over so easily," remarked the
Lion. “What about the other one?"
</p><p>
”He’s made of tin," said Dorothy. And she helped the
Woodman up again.
</p><p>
”That’s why I almost broke my claws," said the Lion.
”And what is that little animal?"
</p><p>
”He is my dog, Toto," answered Dorothy.
”A tin dog or a straw dog?" asked the Lion.
”He’s a — a — a meat dog," said the girl.
</p><p>
"Oh! He’s a curious animal. Now I can see that he is
very small. Only a coward like me can think of biting
such a little creature," continued the Lion sadly.
</p><p>
"What makes you a coward?" asked Dorothy. She
was looking at the great beast in wonder, for he was as
big as a small horse.
</p><p>
"I don’t know,” replied the Lion. "I think I was born
that way. All the other animals in the forest think that
I am brave, for the Lion is the King of Beasts. When
I roar very loudly every creature gets out of my way
in fear. When I meet a man I am very afraid; but I just
roar at him, and he always runs away as fast as he can
go. Just as soon as they hear my roar they all try to
get away from me, and of course I let them go. But as
soon as I meet an elephant, or a tiger, or a bear, I will
certainly run myself — I’m such a coward.”
</p><p>
"But that isn’t right. The King of Beasts can’t be
a coward,” said the Scarecrow. 
</p><p>
"I know it,” replied the Lion sadly. "It is my great
sorrow, and makes my life very unhappy. When there is
danger, my heart begins to beat fast.”
</p><p>
"Maybe you have heart disease,” said the Tin Woodman.
</p><p>
”Maybe,” said the Lion.
</p><p>
"You must be glad then, for it means you have
a heart.” continued the Tin Woodman. “I have no heart;
so I cannot have heart disease.”
</p><p>
"Maybe,” said the Lion thoughtfully, "a creature
without a heart cannot be a coward.”
</p><p>
”Do you have brains?” asked the Scarecrow.
"I think so,” replied the Lion.
</p><p>
"I am going to the Great Oz to ask him to give me
some,” remarked the Scarecrow, ”for there is only straw
in my head.”
</p><p>
"And I am going to ask him to give me a heart,” said
the Woodman.
</p><p>
“And I am going to ask him to send Toto and me back
to Kansas," added Dorothy.
</p><p>
"Do you think Oz can give me courage?" asked the
Cowardly Lion.
</p><p>
"Just as easily as he can give me brains," said the
Scarecrovm
</p><p>
”Or give me a heart," said the Tin Woodman.
</p><p>
”Or send me back to Kansas," said Dorothy.
</p><p>
”Then I would like to go with you," said the Lion, ”for
my life is so bad without a bit of courage."
</p><p>
"You will be very welcome," answered Dorothy, "for
you will help to keep away the other wild beasts. It
seems to me they are more cowardly than you are if
they allow you to scare them so easily."
</p><p>
"They really are," said the Lion, "but that doesn’t
make me any braver, and as long as I stay a coward I’ll
be unhappy."
</p><p>
So once more the little company started on the
journey. The Lion walked at Dorothy’s side and Toto did
not like the Lion at first. But after a time Toto and the
Cowardly Lion became good friends.
</p><p>
During the rest of that day they had no other
adventures. Once the Tin Woodman stepped upon
a beetle and killed the poor little thing. This made
the Tin Woodman very unhappy and he cried a little.
The tears ran slowly down his face and his jaw
rusted. When Dorothy asked him a question the Tin
Woodman could not open his mouth. This frightened
him very much but the Scarecrow took the oil-can from
Dorothy’s basket and oiled the Woodman’s jaws, so that
after a few moments he could talk again. 
</p><p>
“This will serve me a lesson," said he, ”to look where
I step. I don’t want to cry when I kill another beetle.
And I don’t want my jaws to rust again."
</p><p>
So he walked very carefully, with his eyes on the
road, and when he saw a beetle he stepped over it not
to harm it. The Tin Woodman knew very well he had no
heart, and so he did his best never to be cruel or unkind
to anything.
</p>
</div>




<h3><span>CHAPTER 7</span> THE JOURNEY TO THE GREAT OZ</h3>
<div>

<p>
They had to camp out that night under a large
tree in the forest, for there were no houses near.

The tree made a good, thick roof to protect them
from the dew, and the Tin Woodman chopped a lot of
branches with his axe and Dorothy built a nice fire that
warmed her and made her feel less lonely. She and Toto
ate the last of their bread, and now they had no food left
for breakfast.
</p><p>
“I can go into the forest and kill a deer for you," said
the Lion. ”You can cook it by the fire, and then you will
have a very good breakfast."
</p><p>
"Don’t! Please don’t," begged the Tin Woodman. ”I’ll
certainly cry for the poor deer, and then my jaws will
rust again."
</p><p>
So the Lion went away into the forest and found his
own supper, and no one ever knew what it was, for he
didn’t tell about it. And the Scarecrow found a tree full
of nuts and filled Dorothy’s basket with them.
</p><p>
"Now you will not be hungry for a long time," said
the Scarecrow.
</p><p>
"It is very kind and thoughtful of you. Thank you,"
said the little girl, and laughed at the clumsy way in
which the poor creature picked up the nuts. His stuffed
hands were so clumsy and the nuts were so small that
he dropped almost as many as he put in the basket.
But the Scarecrow did not mind how long it took him
to fill the basket, for he could stay away from the fire
this way. He only came near to cover Dorothy with dry
leaves when she lay down to sleep. These kept her very
warm, and she slept soundly until morning.
</p><p>
When it was morning, the girl bathed her face in
a little stream, and soon after they all started towards
the Emerald City.
</p><p>
This was a very eventful day for the travelers. They
saw before them a great ditch that crossed the road as
far as they could see on either side. It was a very wide
ditch, and when they came up to the edge and looked
into it they could see it was also very deep, and there
were many big rocks at the bottom. It was so deep that
they could not climb down, and for a moment it seemed
that their journey must end.
</p><p>
"What shall we do?" asked Dorothy sadly.
</p><p>
”I do not know," said the Tin Woodman, and the Lion
shook his big head and looked thoughtful.
</p><p>
But the Scarecrow said, "We cannot ﬂy, that is
certain. And we cannot climb down into this great ditch
or jump over it. So, we must stop where we are.”
</p><p>
"I think I can jump over it,” said the Cowardly Lion
thoughtfully.
</p><p>
“Then we are all right,” answered the Scarecrow, ”for
you can carry us all over on your back, one at a time.”
</p><p>
"Well, I’ll try it,” said the Lion. ”Who will go first?”
</p><p>
"I will,” said the Scarecrow. "The ditch is very wide
and you may fall down. The fall won’t hurt me at all,
but I don’t want Dorothy to die, or the Tin Woodman to
crash on the rocks below.”
</p><p>
"I am very afraid of falling, myself,” said the
Cowardly Lion, ”but I think we must try it. So get on my
back and we will make a try.”
</p><p>
The Scarecrow sat upon the Lion’s back, and the big
beast walked to the edge of the ditch and stopped.
</p><p>
"Why don’t you run and jump?” asked the
Scarecrovm.
</p><p>
"Because that isn’t the way we Lions do these
things,” he replied. Then he made a great jump and
landed safely on the other side. They were all very
happy to see how easily he did it, and as soon as the
Scarecrow got down from his back the Lion jumped
across the ditch again.
</p><p>
Dorothy was the next one; so she took Toto in her
arms, climbed on the Lion’s back, and held tightly to
his neck with one hand. The next moment the Lion
jumped; and then, before she had time to think about
it, she was safe on the other side. The Lion went back
a third time and got the Tin Woodman, and then they
all sat down for a few moments to give the beast some
rest. 
</p><p>
The forest was very thick on this side of the ditch,
and it looked dark. Soon they got up and started along
the road of yellow brick again. They wanted to come
to the end of the woods and reach the bright sunshine
again. They soon heard strange noises deep in the
forest, and the Lion whispered to them:
</p><p>
”The Kalidahs live in this part of the country."
"What are the Kalidahs?" asked the girl.
</p><p>
”They are terrible beasts with bodies like bears and
heads like tigers," replied the Lion. "Their claws are so
long and sharp that they can kill me as easily as I can
kill Toto. I’m very afraid of the Kalidahs."
</p><p>
"Of course you are," replied Dorothy. "It’s not
surprising. They are terrible beasts."
</p><p>
The Lion wanted to reply when suddenly they came
to another ditch across the road. But this one was so
wide and deep that the Lion knew at once he could not
jump across it.
</p><p>
So they sat down to think what to do, and after
a while the Scarecrow said:
</p><p>
"There is a great tree close to the ditch. The Tin
Woodman must chop it down. It will fall to the other
side of the ditch and we’ll be able to walk across it
easily."
</p><p>
“That is a good idea," said the Lion. “Are you sure you
have straw in your head?"
</p><p>
The Woodman started to work at once. His axe was
so sharp that he chopped the tree down very quickly.
The big tree fell with a crash across the ditch.
</p><p>
When they were ready to start to cross this bridge,
they heard a loud growl. They turned round and saw
two great beasts with bodies like bears and heads like
tigers. The monsters were running towards them.
</p><p>
"They are the Kalidahs!" said the Cowardly Lion, and
began to tremble with fear.
</p><p>
”Quick!" cried the Scarecrow. ”Let us cross over."
</p><p>
So Dorothy went first with Toto in her arms, the Tin
Woodman followed, and the Scarecrow came next. The
Lion, although he was certainly afraid, turned to face
the Kalidahs, and then he gave a roar. It was so loud
and terrible that Dorothy screamed and the Scarecrow
fell over backward; even the fierce beasts stopped and
looked at him in surprise.
</p><p>
But then the Kalidahs saw that they were bigger
than the Lion, and remembered that there were two of
them and only one of him. They again rushed forward,
and the Lion crossed over the tree and turned to see
what the monsters were going to do next. The fierce
beasts also began to cross the tree. And the Lion said
to Dorothy:
</p><p>
"That’s the end. They will surely kill us. But stand
close behind me, and I will fight them as long as I live."
</p><p>
"Wait a minute!" called the Scarecrow. "The
Woodman must chop away the end of the tree that lies
on our side of the ditch."
</p><p>
The Tin Woodman began to use his axe at once,
and very soon the tree with the two Kalidahs fell with
a crash into the ditch.
</p><p>
"Well," said the Cowardly Lion with a sigh of relief,
”I see we are going to live a little while longer, and I am
glad of it. Those creatures frightened me so badly that
my heart is beating yet."
</p><p>
"Oh," said the Tin Woodman sadly, "it’s a pity that
I do not have a heart that can beat." 
</p><p>
This adventure made the travelers want to get out of
the forest more than ever, and they walked so fast that
Dorothy became tired, and had to ride on the Lion’s
back. To their great joy the trees became thinner the
farther they went, and in the afternoon they suddenly
came upon a wide river. On the other side of the water
they could see the road of yellow brick. It ran through
a beautiful country. They could see green fields, bright
ﬂowers and trees full of fine fruits. They were very
happy to see this country before them.
</p><p>
“How shall we cross the river?" asked Dorothy.
</p><p>
"That is easy enough," replied the Scarecrow. "The
Tin Woodman will build us a raft, and we’ll get to the
other side."
</p><p>
So the Woodman took his axe and began to chop
down small trees to make a raft, and while he was busy
at this the Scarecrow found on the riverbank a tree full
of fine fruit. Dorothy was very hungry, and she made
a hearty meal of the nice fruit.
</p><p>
But when night came the Tin Woodman was still
making a raft. So they found a nice place under the
trees where they slept well until the morning; and
Dorothy dreamed of the Emerald City, and of the good
Wizard Oz, and of her home in Kansas.
</p>
</div> 

 
<h3><span>CHAPTER 8</span> THE DEADLY POPPY FIELD</h3>
<div>


<p>
Our little company of travelers woke up the next
morning full of hope, and Dorothy had a hearty
breakfast of the fruit from the trees beside the river.
Behind them there was the dark forest full of dangers;
but before them there was a lovely, sunny country and
the road of yellow brick that led to the Emerald City.
</p><p>
When the Woodman finished the raft, they were
ready to start. Dorothy sat down in the middle of the
raft and held Toto in her arms. The Lion sat down next
to her. The Scarecrow and the Tin Woodman had long
sticks in their hands to push the raft through the water.
</p><p>
They got along quite well at first, but when they
reached the middle of the river the current caught the
raft and carried it farther and farther away from the
road of yellow brick. And the water became so deep
that the long sticks could not touch the bottom.
</p><p>
"This is bad," said the Tin Woodman. "We must get
to the land or the river will carry us into the country of
the Wicked Witch of the West, and she will charm us
and make us her slaves."
</p><p>
”And then I’ll get no brains," said the Scarecrow.
”And I’ll get no courage," said the Cowardly Lion.
”And I’ll get no heart," said the Tin Woodman.
”And I’ll never get back to Kansas," said Dorothy.
</p><p>
"We must certainly get to the Emerald City," the
Scarecrow continued, and he pushed so hard on his
long stick that it stuck in the mud at the bottom of the
river. Then, before he could pull it out again — or let
go — the raft moved on, and the stick with the poor
Scarecrow stayed behind in the middle of the river.
</p><p>
“Good-bye!" he called after them, and they were very
sorry to leave him.
</p><p>
Of course this was a bad thing for the Scarecrow.
</p><p>
"The things are now worse than when I first met
Dorothy," he thought. "Then, I was stuck on a stick in
a cornfield, where I could scare the crows. But surely
there is no use for a Scarecrow in the middle of a river.
I am afraid I’ll never have any brains, after all!"
</p><p>
The raft moved on down the river, and the poor
Scarecrow was soon far behind. Then the Lion said:
</p><p>
"We must do something to save us. I think I can
swim to the bank and pull the raft after me, and you
will hold on to my tail."
</p><p>
So he jumped into the water, and the Tin Woodman
held on to his tail. Then the Lion began to swim
towards the bank. It was hard work, although he was
so big; but by and by the Lion drew them out of the
current, and then Dorothy took the Tin Woodman’s
long stick and helped push the raft to the bank.
</p><p>
They were all tired when they reached the bank at
last and stepped on the pretty green grass. And they
also knew that now they were quite far from the road
of yellow brick that led to the Emerald City.
</p><p>
"What shall we do now?" asked the Tin Woodman,
as the Lion lay down on the grass to let the sun dry him. 
</p><p>
"We must get back to the road, somehow," said
Dorothy.
</p><p>
"The best plan is to walk along the riverbank until
we get to the road again," remarked the Lion.
</p><p>
So, after a short rest, Dorothy picked up her basket
and they started along the bank back to the road of
yellow brick. It was a lovely country, with a lot of
ﬂowers and fruit trees and sunshine. But they were
not very happy, because they felt so sorry for the poor
Scarecrovm.
</p><p>
They walked along as fast as they could, and after
a time the Tin Woodman cried out: ”Look!"
</p><p>
Then they all looked at the water and saw the
Scarecrow upon his stick in the middle of the river. He
looked very lonely and sad.
</p><p>
"What can we do to save him?" asked Dorothy.
</p><p>
The Lion and the Woodman both shook their heads,
for they did not know. So they sat down upon the bank
and looked sadly at the Scarecrow until a Stork ﬂew by.
She saw the travelers and stopped to rest at the water’s
edge.
</p><p>
"Who are you and where are you going?" asked the
Stork.
</p><p>
“I am Dorothy," answered the girl, "and these are my
friends, the Tin Woodman and the Cowardly Lion; and
we are going to the Emerald City."
</p><p>
“This isn’t the road," said the Stork and looked at the
strange company.
</p><p>
"I know it," answered Dorothy, "but the Scarecrow
is missing, and we are wondering how we’ll get him
again."
</p><p>
“Where is he?" asked the Stork.
</p><p>
”Over there in the river," answered the little girl.
</p><p>
"It is a pity he is so big and heavy. I cannot get him
for you," remarked the Stork.
</p><p>
"He isn’t heavy," said Dorothy eagerly, "for he is full
of straw. Can you try and bring him back to us?"
</p><p>
"Well, I’ll try," said the Stork. "I hope he won’t be
too heavy to carry, or I’ll have to drop him in the river
again."
</p><p>
So the big bird ﬂew into the air and over the water
till she came to where the Scarecrow was upon his
stick. Then the Stork with her great claws picked up the
Scarecrow by the arm and carried him up into the air
and back to the bank, where Dorothy and the Lion and
the Tin Woodman and Toto were sitting.
</p><p>
When the Scarecrow found himself among his
friends again, he was very happy; and as they walked
along he kept singing at every step.
</p><p>
”I was afraid I was going to stay in the river forever,"
he said, "but the kind Stork saved me. When I get my
brains I’ll find the Stork again and do some good thing
for her in return."
</p><p>
"That’s all right," said the Stork, who was ﬂying
along beside them. "I always like to help anyone in
trouble. But I must go now, for my babies are waiting
for me. I hope you will find the Emerald City and that
Oz will help you."
</p><p>
"Thank you," replied Dorothy, and then the kind
Stork ﬂew away and was soon out of sight. 
</p><p>
As they walked along, they listened to the singing
birds and looked at the lovely ﬂowers. There were big
yellow and white and blue and purple ﬂowers, and
scarlet poppies in the field.
</p><p>
"Aren’t they beautiful?" the girl asked, as she
breathed in the lovely scent of the bright ﬂowers.
</p><p>
"I think so," answered the Scarecrow. "When I have
brains, I may like them better."
</p><p>
"When I have a heart, I may like them, too," added
the Tin Woodman.
</p><p>
"I like ﬂowers very much," said the Lion.
</p><p>
They now met more and more of the big scarlet
poppies, and fewer and fewer of the other ﬂowers; and
soon they were in the middle of a great field of poppies.
Everybody knows that when there are many of these
ﬂowers together their scent is so powerful that anyone
who breathes it falls asleep, and someone must carry
the sleeper away from the scent of the ﬂowers, or he
will sleep on and on forever. But Dorothy did not know
this; so soon she felt she must sit down to rest and to
sleep.
</p><p>
But the Tin Woodman did not let her do this.
</p><p>
"We must hurry and get back to the road of yellow
brick before dark," he said; and the Scarecrow agreed
with him. But Dorothy could not walk any longer. Her
eyes closed and she forgot where she was and fell
among the poppies, fast asleep.
</p><p>
"What shall we do?" asked the Tin Woodman.
</p><p>
”We cannot leave her here, for she will die," said the
Lion. ”The smell of the ﬂowers is killing us all. I myself
can hardly keep my eyes open, and the dog is sleeping
already."
</p><p>
It was true; Toto was sleeping beside the little girl.
But the Scarecrow and the Tin Woodman did not feel
the scent of the ﬂowers.
</p><p>
"Run fast," said the Scarecrow to the Lion, "and get
out of this deadly poppy field as soon as you can. We
will bring the little girl with us. But do not fall asleep
for you are too big to carry."
</p><p>
So the Lion ran forward as fast as he could go. In
a moment he was out of sight.
</p><p>
"Let us make a chair with our hands and carry her,"
said the Scarecrow. So they made a chair with their
hands and carried the sleeping girl and Toto through
the ﬂowers.
</p><p>
They walked on and on, and at last came upon their
friend the Lion. He was sleeping among the poppies
close to the end of the poppy field.
</p><p>
”We can do nothing for him," said the Tin Woodman,
sadly; "for he is too heavy to carry. We must leave him
here to sleep on forever."
</p><p>
"I’m sorry," said the Scarecrow. ”The Lion was a very
good friend. But let us go on."
</p><p>
They carried the sleeping girl to a pretty place
beside the river, far from the poppy field; and here they
laid her on the soft grass and waited for the fresh wind
to wake her up.
</p>
</div>


<h3><span>CHAPTER 9</span> THE QUEEN OF THE FIELD IVIICE</h3>
<div>

<p>
We cannot be far from the road of yellow
brick, now," remarked the Scarecrow, as he
stood beside the girl. 
</p><p>
The Tin Woodman wanted to reply something when
he heard a low growl. He turned his head and saw
a strange beast that was coming over the grass towards
them. It was a great yellow Wildcat. As it came nearer
the Tin Woodman saw that it was running after a little
gray field mouse, and although he had no heart he
knew it was wrong for the Wildcat to try to kill such
a little, harmless creature.
</p><p>
So the Woodman raised his axe, and cut the beast’s
head off from its body.
</p><p>
The field mouse saw it and stopped. Then she came
slowly up to the Woodman and said, in a little voice:
</p><p>
”Oh, thank you so much for saving my life."
</p><p>
"Please, don’t speak of it," replied the Woodman.
"I have no heart, you know, so I do my best to help all
those who may need a friend, even if it is only a mouse."
</p><p>
"Only a mouse!" cried the little animal. "I am
a Queen — the Queen of all the Field Mice!"
</p><p>
"Oh," said the Woodman and made a bow.
</p><p>
At that moment several mice ran up to them,
and when they saw their Queen they all bowed and
exclaimed:
</p><p>
”Oh, Queen! How did you escape the great Wildcat?"
</p><p>
"This strange tin man," she answered, "killed the
Wildcat and saved my life. So from now on you must all
serve and obey him."
</p><p>
“We will!" cried all the mice. Then one of the biggest
mice asked:
</p><p>
”Is there anything we can do for you?"
"Nothing," answered the Woodman; but the
Scarecrow said, quickly, "Oh, yes; you can save our
friend, the Cowardly Lion, who is sleeping in the poppy
field."
</p><p>
"A Lion!" cried the little Queen. "Won’t he eat us all
up?"
</p><p>
"Oh, no," replied the Scarecrow; "this Lion is
a coward."
</p><p>
“Really?" asked the Mouse.
</p><p>
"He says so himself," answered the Scarecrow, "and
he will never hurt anyone who is our friend."
</p><p>
"Very well," said the Queen, "we believe you. But
what shall we do?"
</p><p>
“Are there many mice which obey you?"
”Oh, yes; there are thousands," she replied.
</p><p>
"Then send for them all, and tell each one to bring
a long piece of string."
</p><p>
The Queen turned to the mice and told them to go
at once and get all her people. As soon as they heard
her orders they ran away in every direction as fast as
possible.
</p><p>
"Now," said the Scarecrow to the Tin Woodman, ”you
</p><p>
must go to those trees by the riverbank and make a cart
that will carry the Lion." 
</p><p>
So the Woodman went at once to the trees and
began to work; and soon the cart was ready.
</p><p>
The mice came from all directions, and there were
thousands of them; and each one brought a piece of
string in his mouth. About this time that Dorothy woke
from her long sleep and opened her eyes. Of course, she
was greatly surprised that she was lying upon the grass,
with thousands of mice around her. But the Scarecrow
told her about everything and introduced her to the
Queen of all the Field Mice.
</p><p>
Then the Scarecrow and the Woodman tied one end
of a string around the neck of each mouse and the other
end to the cart. Of course the cart was much bigger
than any of the mice, but all together the mice pulled
it quite easily. They took the cart and the Scarecrow
and the Tin Woodman to the place where the Lion was
sleeping.
</p><p>
After some hard work, for the Lion was heavy, they
got him up on the cart and started back. Soon they
rolled the Lion out of the poppies to the green fields,
where he could breathe the sweet, fresh air again.
</p><p>
Dorothy came to meet them and said to the little mice:
"Thank you for saving my friend from death."
</p><p>
Then the Woodman and the Scarecrow set the
mice free and they ran away through the grass to their
homes. The Queen of the Mice said:
</p><p>
"When you need us again, come out into the field
and call, and we’ll hear you and come to help you.
Good-bye!"
</p><p>
"Good-bye!" they all answered and the Queen ran
away.
</p><p>
After this they sat down beside the Lion who was
still sleeping.
</p>
</div>


<h3><span>CHAPTER 10</span> THE GUARDIAN OF THE GATE</h3>
<div>

<p>
When the Cowardly Lion woke up, he was very
glad that he was still alive.
"I ran as fast as I could," he said, "but the
</p><p>
ﬂowers were too strong for me. How did you get me
out?"
</p><p>
Then they told him of the field mice, and the
Cowardly Lion said:
</p><p>
“How strange it all is! But, friends, what shall we do
now?"
</p><p>
"We must find the road of yellow brick again," said
Dorothy, ”and then we can go to the Emerald City."
</p><p>
So they all started on their journey again. Soon they
reached the road of yellow brick and turned again
towards the Emerald City where the Great Oz lived.
</p><p>
The road was good now, and the country about was
beautiful. There were fences and houses beside the
road; but they were green. The people were all dressed
in clothes of a lovely emerald-green color and wore
pointed hats like those of the Munchkins. Sometimes
people came to the doors and looked at them; but no
one came near them or spoke to them because of the
great Lion, of which they were very much afraid.
</p><p>
"I think it is the Land of Oz," said Dorothy, "and we
are surely getting near the Emerald City." 
</p><p>
”Yes," answered the Scarecrow. “Everything is green
here, while in the country of the Munchkins blue
was the favorite color. But the people do not seem as
friendly as the Munchkins, and I’m afraid we won’t find
a place to pass the night."
</p><p>
"Toto and I are very hungry," said the girl. "Let us
stop at the next house and talk to the people."
</p><p>
So, when they came to a big farmhouse, Dorothy
walked bravely up to the door and knocked.
</p><p>
A woman opened it a little, and said, "What do you
want, child, and why is that great Lion with you?"
</p><p>
"Can we pass the night with you?" asked Dorothy.
"The Lion is my friend and he will not hurt you."
</p><p>
“He isn’t wild, is he?" asked the woman and opened
the door a little wider.
</p><p>
“No, he isn’t," said the girl, "and he is a great coward,
too. He will be more afraid of you than you are of him."
</p><p>
The woman thought it over and said: ”Well, you may
come in, and I will give you some supper and a place to
sleep."
</p><p>
So they all entered the house. There was a man and
two children in the house. They didn’t expect to see
such a strange company, and while the woman was
laying the table the man asked:
</p><p>
"Where are you all going?"
</p><p>
”To the Emerald City," said Dorothy, ”to see the Great
Oz."
</p><p>
"Oh!" exclaimed the man. “Are you sure that Oz will
see you?"
”Why not?" she replied.
</p><p>
"Well, people say that he never shows himself to
anybody. I visited the Emerald City many times in the
past, and it is a beautiful and wonderful place; but
I never saw the Great Oz."
</p><p>
”Does he never go out?" asked the Scarecrow.
</p><p>
"Never. He sits day after day in the great Throne
Room of his Palace, and even those who serve him do
not see him face to face."
</p><p>
"What is he like?" asked the girl.
</p><p>
”That is hard to tell," said the man thoughtfully. ”You
see, Oz is a Great Wizard, and he can change his form.
So some people say he looks like a bird; and some say
he looks like an elephant; and some say he looks like
a cat. But who the real Oz is, when he is in his own
form, no one can tell."
</p><p>
"That is very strange," said Dorothy, "but we must
try, in some way, to see him."
</p><p>
”Why do you wish to see the terrible Oz?" asked the
man.
</p><p>
“I want to ask him to give me some brains," said the
Scarecrow eagerly.
</p><p>
"Oh, that will be easy for Oz," said the man. "He has
more brains than he needs."
</p><p>
"And I want to ask him to give me a heart," said the
Tin Woodman. 
</p><p>
"That will be easy, too," continued the man, "for Oz
has a lot of hearts, of all sizes and shapes."
</p><p>
”And I want to ask him to give me courage," said the
Cowardly Lion.
</p><p>
"Oz keeps a great bottle of courage in his Throne
Room," said the man. "He will be glad to give you some."
</p><p>
"And I want to ask him to send me back to Kansas,"
said Dorothy.
</p><p>
“Where is Kansas?" asked the man, in surprise.
</p><p>
"I don’t know," replied Dorothy sadly, "but it is my
home."
</p><p>
"Well, OZ can do anything; so I think he will find
Kansas for you. But first you must see him, and that will
be a hard task; for the Great Wizard does not like to
see anyone. But what do you want?" he asked Toto. But
Toto didn’t say anything, for he could not speak.
</p><p>
”Supper is ready," said the woman, so they gathered
around the table and Dorothy had a hearty meal. The
Lion and Toto ate some of the porridge, but did not like
it. The Scarecrow and the Tin Woodman ate nothing at
all.
</p><p>
The woman now gave Dorothy a bed to sleep in,
and Toto lay down beside her. The Lion lay down near
the door and guarded it. The Scarecrow and the Tin
Woodman stood up in a corner all night, for they could
not sleep.
</p><p>
Early next morning they started on their way, and
soon saw a beautiful green glow in the sky just before
them.
</p><p>
"That is the Emerald City, I’m sure," said Dorothy.
</p><p>
As they walked on, the green glow became brighter
and brighter. They were glad that their journey was
going to end soon. In the afternoon they came to the
great wall around the City. It was high and thick and of
a bright green color.
</p><p>
In front of them, and at the end of the road of yellow
brick, there was a big gate. Emeralds covered it all over.
</p><p>
There was a bell beside the gate, and Dorothy
rang it. Then the big gate opened, and they all passed
through. Now they were in a big room. Emeralds
covered its walls, too.
</p><p>
There was a little man in the room. He was about
the same size as the Munchkins. His clothes were green,
and even his skin was slightly green in colour. At his
side there was a large green box.
</p><p>
When he saw Dorothy and her friends the man
asked, "What do you wish in the Emerald City?"
</p><p>
”We are here to see the Great Oz," said Dorothy.
</p><p>
The answer surprised the man so much that he sat
down to think it over.
</p><p>
”The last time when anyone asked me to see Oz was
many years ago," he said. "He is powerful and terrible,
and he doesn’t like it when people ask him to do stupid
things. He gets angry and destroys such people."
</p><p>
"But it is very important," replied the Scarecrow.
”And we know that Oz is a good Wizard."
</p><p>
"Yes, he is," said the green man, "and he rules the
Emerald City wisely and well. But he is terrible to those
who are not honest, and he never shows himself to
anyone. I am the Guardian of the Gates, and as you want
to see the Great Oz I must take you to his Palace. But
first you must put on the glasses."
</p><p>
“Why?" asked Dorothy.
"Because the brightness and glory of the Emerald
</p><p>
City will blind you without glasses. Even those who live
in the City must wear glasses night and day." 
</p><p>
He opened the big box, and Dorothy saw that it was
full of glasses of every size and shape. The green man
gave the travelers a pair of green glasses each. The
travelers put on the green glasses; then the Guardian of
the Gates put on his own glasses.
</p><p>
"I’m ready to show you the palace," he said.
</p><p>
Then he took a big golden key and opened another
gate, and they all passed through. The travelers were in
the Emerald City at last.
</p>
</div>


<h3><span>CHAPTER 11</span> THE WONDERFUL CITY OF OZ</h3>
<div>

<p>
Dorothy and her friends were at first surprised 
by the wonderful City. Everything was green — 
the houses, streets, and window panes — and
emeralds were everywhere. Even the sun and the sky
above the City looked green, for the travelers were
wearing green glasses. There were many people — men,
women, and children, and they all wore green clothes,
and their skin was slightly green in colour. There
were many shops in the street, and Dorothy saw that
everything in them was green — green sweets, green
shoes, green hats, and green clothes of all sorts. There
were no horses or animals of any kind; the men carried
things around in little green carts, which they pushed
before them. Everyone seemed happy and rich.
</p><p>
The Guardian of the Gates led them through the
streets until they came to a big building, right in the
middle of the City, which was the Palace of Oz, the
Great Wizard. There was a soldier before the door. His
uniform was green, and his long beard was green, too.
</p><p>
”Here are strangers," said the Guardian of the Gates
to him, ”and they want to see the Great Oz."
</p><p>
"Step inside," answered the soldier, "and I will tell
him about you."
</p><p>
So they passed through the Palace Gates and came
into a big room with a green carpet and lovely green
furniture. There were a lot of emeralds on the walls and
the furniture.
</p><p>
"Please sit down and I will go to the door of the
Throne Room and tell Oz you are here."
</p><p>
They had to wait a long time before the soldier
returned. When, at last, he came back, Dorothy asked:
</p><p>
”Is Oz going to see us?"
</p><p>
”Oh, yes," replied the soldier, "he is going to see you;
but each one of you must enter the Throne Room alone,
and he is going to see only one of you each day. So, you
must remain in the Palace for several days. You will
</p><p>
have the rooms where you may rest in comfort after
your journey." 
</p><p>
"Thank you," replied the girl; "that is very kind of
Oz."
</p><p>
The soldier now blew a green whistle, and at once
a young girl in a pretty green silk dress entered the
room. She had lovely green hair and green eyes, and she
bowed low before Dorothy as she said, "Follow me and
I will show you your room."
</p><p>
”Good-bye," said Dorothy to all her friends. Then she
took Toto in her arms and followed the green girl to her
room.
</p><p>
It was the sweetest little room in the world, with
a soft comfortable bed and a small fountain in the
middle of the room. Beautiful green ﬂowers stood in
the windows, and there was a shelf with little green
books. In a wardrobe there were many green silk
dresses, and all of them fitted Dorothy well.
</p><p>
”Make yourself at home," said the green girl. “Oz will
send for you tomorrow morning."
</p><p>
She left Dorothy alone and went back to the others,
and took them to their rooms.
</p><p>
The next morning, after breakfast, the green girl
came to take Dorothy to the Throne Room. Dorothy
put on a lovely green dress, took Toto in her arms and
followed the green girl to the Throne Room of the Great
Oz.
</p><p>
First they came to a great hall in which there were
many ladies and gentlemen in rich clothes. These
people always came to wait outside the Throne Room
every morning, although Oz never showed himself to
them. When Dorothy entered they looked at her with
curiosity, and one of them whispered:
</p><p>
"Are you really going to look at the face of Oz the
Terrible?"
</p><p>
"Of course," answered the girl, ”for I hope he will see me".
</p><p>
"Oh, he will see you," said the soldier before the
door, "although he does not like it when people ask to
see him. ‘Send them back,’ he said at first, for he was
angry. Then he asked, ‘What does she look like?’ And
when he heard about your silver shoes he was very
much interested. At last I told him about the mark upon
your forehead, and he said, ‘I’ll see her tomorrow.”
</p><p>
Just then a bell rang, and the green girl said to
Dorothy, ”You must go into the Throne Room now."
</p><p>
She opened a little door and Dorothy walked bravely
through. The Throne Room was a wonderful place. It
was a big, round room. There were large emeralds on
its walls and ceiling and ﬂoor.
</p><p>
In the middle of the room there was a big throne.
There were bright emeralds on it, too. In the center of
the throne there was a very big Head without a body or
any arms or legs. There was no hair upon this head, but
it had eyes and a nose and mouth.
</p><p>
As Dorothy looked at the Head in wonder and fear,
its eyes opened slowly and looked at her. Then the
mouth moved, and the Head said:
</p><p>
"I am OZ, the Great and Terrible. Who are you, and
why do you want to see me?"
</p><p>
The girl took courage and answered: 
</p><p>
“I am Dorothy, the Small and Weak, and I need your
help."
</p><p>
The eyes looked at her thoughtfully for a full minute.
Then the Head said:
</p><p>
"Where did you get the silver shoes?"
</p><p>
“I got them from the Wicked Witch of the East, when
my house fell on her and killed her," she replied.
</p><p>
"Where did you get the mark upon your forehead?"
continued the voice.
</p><p>
"That is where the Good Witch of the North kissed
me when she sent me to you," said the girl.
</p><p>
Again the eyes looked at her, and they saw she was
telling the truth. Then Oz asked, ”What do you wish me
to do?"
</p><p>
"Send me back to Kansas, where my Aunt Em and
Uncle Henry are," she answered earnestly. "I don’t
like your country, although it is so beautiful. And I am
sure my Aunt Em and Uncle Henry are very unhappy
because I am away so long."
</p><p>
The eyes closed and opened three times, and then
they turned up to the ceiling and down to the ﬂoor. And
at last they looked at Dorothy again.
</p><p>
“Why do you think I’ll do this for you?" asked Oz.
</p><p>
"Because you are strong and I am weak; because you
are a Great Wizard and I am only a little girl."
</p><p>
“But you killed the Wicked Witch of the East, so you
are strong," said Oz.
</p><p>
"That just happened," replied Dorothy simply; “I did
not want it."
</p><p>
"Well," said the Head, "1 will give you my answer.
I will send you back to Kansas, but you must do
something for me in return. In this country everyone
must pay for everything he gets. Help me and I will help you.
</p><p>
"What must I do?" asked the girl.
“Kill the Wicked Witch of the West," answered Oz.
”But I cannot!" exclaimed Dorothy, in surprise.
</p><p>
"You killed the Witch of the East and you wear the
silver shoes, which have a powerful charm. There is
only one Wicked Witch in all this land now, and when
you can tell me she is dead I will send you back to
Kansas — but not before."
</p><p>
The little girl began to cry.
</p><p>
"How can I kill the Wicked Witch, if you, who are
Great and Terrible, cannot kill her yourself ?"
</p><p>
"I do not know," said the Head; "but that is my
answer, and until the Wicked Witch dies you will not
see your uncle and aunt again. Remember that the
Witch is wicked — very wicked — and you must kill
her. Now go, and do not ask to see me again until the
Wicked Witch is dead."
</p><p>
Sadly Dorothy left the Throne Room and went
back where the Lion and the Scarecrow and the Tin
Woodman were waiting for her. "There is no hope for
me," she said sadly, ”for Oz will not send me home until
the Wicked Witch of the West is dead; and I don’t want
to kill anyone."
</p><p>
Her friends were sorry, but could do nothing to help
her; so Dorothy went to her room and lay down on the
bed and cried until she fell asleep. 
</p><p>
The next morning the soldier with the green beard
came to the Scarecrow and said:
</p><p>
“Come with me, for Oz wants to see you."
</p><p>
So the Scarecrow followed him and entered the
great Throne Room, where he saw a very lovely Lady
who was sitting in the emerald throne. She wore
a green dress and an emerald crown, and had beautiful
wings.
</p><p>
The Scarecrow bowed before this beautiful creature,
and she looked upon him sweetly, and said:
</p><p>
"I am Oz, the Great and Terrible. Who are you, and
why do you want to see me?"
</p><p>
The Scarecrow was very surprised to see a lovely
Lady instead of the great Head; but he answered her
bravely.
</p><p>
“I am only a Scarecrow. So I have no brains. Can you
put some brains in my head instead of straw? I want to
be like any other man in this country."
</p><p>
"Why do you think I’ll do this for you?" asked the
Lady.
</p><p>
“Because you are wise and powerful, and no one else
can help me," answered the Scarecrow.
</p><p>
"I will give you brains, but you must do something
for me in return," said Oz; "You must kill for me the
Wicked Witch of the West, then I will give you brains,
and you will be the wisest man in all the Land of Oz."
</p><p>
”I thought you asked Dorothy to kill the Witch," said
the Scarecrow, in surprise.
</p><p>
"Yes, I did. I don’t care who kills her. But until she is
dead I will not do anything for you. Now go, and do not
ask to see me again until the Wicked Witch is dead."
</p><p>
The Scarecrow went sadly back to his friends and
told them everything; and it was such a surprise for
Dorothy to find that the Great Wizard was not a Head,
but a lovely Lady.
</p><p>
On the next morning the soldier with the green
beard came to the Tin Woodman and said:
</p><p>
”Oz wants to see you. Follow me."
</p><p>
So the Tin Woodman followed him and came to the
great Throne Room. But when the Woodman entered it
he did not see the Head or the Lady; he saw a terrible
Beast. It was as big as an elephant, and the green
throne seemed too small for it. The Beast had a head
like that of a rhinoceros, only there were five eyes in
its face. He had five long arms and five long legs. Thick
hair covered every part of its body. It was lucky that the
Woodman was tin, so he was not at all afraid.
</p><p>
"I am Oz, the Great and Terrible," roared the Beast.
”Who are you, and why do you want to see me?"
</p><p>
"I am a Tin Woodman. I have no heart, and cannot
love. Can you give me a heart? I want to be just like
other men."
</p><p>
"Why do you think I’ll do this for you?" asked the
Beast.
</p><p>
"Because I ask it, and you alone can do this,"
answered the Woodman.
</p><p>
Oz gave a low growl at this, but said: "You must do
something for me to get a heart."
</p><p>
"What must I do?" asked the Woodman.
</p><p>
“Help Dorothy to kill the Wicked Witch of the West,"
replied the Beast. "When the Witch is dead, come to
me, and I will then give you the biggest and kindest and
most loving heart in all the Land of Oz." 
</p><p>
So the Tin Woodman had to return sadly to his
friends and tell them of the terrible Beast. Then the
Lion said:
</p><p>
"I hope he will be a Beast when I go to see him. I’ll
roar at him as loudly as I can and frighten him so much
that he will give me courage."
</p><p>
The next morning the soldier with the green beard
led the Lion to the great Throne Room and the Lion at
once passed through the door. He looked around and
saw, to his surprise, that before the throne there was
a Ball of Fire. He tried to go nearer, but the Ball of Fire
was so hot that he ran back to the door at once.
</p><p>
Then a low, quiet voice came from the Ball of Fire,
and these were the words it spoke:
</p><p>
"I am Oz, the Great and Terrible. Who are you, and
why do you want to see me?"
</p><p>
And the Lion answered, "I am a Cowardly Lion,
afraid of everything. I am here to beg you to give me
courage. I want to become a real King of Beasts."
</p><p>
“Why do you think I’ll do this for you?" asked Oz.
</p><p>
"Because of all Wizards you are the greatest,"
answered the Lion.
</p><p>
The Ball of Fire was silent for a time, and then
the voice said, "Kill the Wicked Witch, and I will give
you courage. But as long as the Witch lives, you must
remain a coward."
</p><p>
The Lion was very angry, but did not say anything.
Then the Ball of Fire became so hot that the Lion
rushed from the room. He was glad to find his friends
who were waiting for him, and told them of the terrible
Ball of Fire.
</p><p>
“What shall we do now?" asked Dorothy sadly.
</p><p>
“There is only one thing we can do," replied the Lion,
"and that is to go to the land of the Winkies, find the
Wicked Witch, and destroy her. And then I’ll have my
courage."
</p><p>
”And I’ll have my brains," added the Scarecrow.
”And I’ll have a heart," added the Tin Woodman.
</p><p>
“And I’ll see Aunt Em and Uncle Henry," said Dorothy.
Then she added, "I think we must try it; but I am sure
I do not want to kill anybody."
</p><p>
“I will go with you; but I’m a coward and I cannot kill
the Witch," said the Lion.
</p><p>
"I will go too," said the Scarecrow; "but I am a fool
and won’t help you much."
</p><p>
"I do not have a heart, but I cannot harm anyone,
even a Witch," remarked the Tin Woodman; "but I’ll go
with you."
</p><p>
So they decided to start on their journey the next
morning, and the Woodman sharpened his axe and
oiled all his joints well. The Scarecrow stuffed himself
with fresh straw and Dorothy put new paint on his eyes
to make him see better. The green girl, who was very
kind to them, filled Dorothy’s basket with food.
</p><p>
Dorothy went to bed quite early and slept soundly
until morning.
</p>
</div> 

 
<h3><span>CHAPTER 12</span> THE SEARCH FOR THE WICKED WITCH</h3>
<div>

<p>
The soldier with the green beard led them through 
the streets of the Emerald City until they reached
the room where the Guardian of the Gates lived.
The Guardian took their green glasses and put them
back in his great box; and then he politely opened the
gate for our friends.
</p><p>
“Which road leads to the Wicked Witch of the West?"
asked Dorothy.
</p><p>
"There is no road," answered the Guardian of the
Gates. "No one ever wishes to go that way."
</p><p>
"How, then, are we going to find her?" asked the girl.
</p><p>
"That will be easy," replied the man, "for when she
knows you are in the country of the Winkies she will
find you, and make you all her slaves."
</p><p>
"She will not," said the Scarecrow, ”for we are going
to destroy her."
</p><p>
”Oh, that is different," said the Guardian of the Gates.
“But take care; for she is wicked and fierce, and may not
allow you to destroy her. Keep to the West, where the
sun sets, and you will find her."
</p><p>
“Thank you," said the travelers, and turned towards
the West. Dorothy still wore the pretty silk dress, but
now, to her surprise, she found it was no longer green,
but white.
</p><p>
Soon they left the Emerald City far behind. As they
walked the ground became rougher and hillier, and
there were no trees, or farms, or houses in this country
of the West.
</p><p>
They walked all the afternoon, and before night
Dorothy and Toto and the Lion were so tired, that they
lay down upon the grass and fell asleep; the Woodman
and the Scarecrow guarded them all night.
</p><p>
The Wicked Witch of the West had only one eye,
but it was as powerful as a telescope, and could see
everywhere. So, as she sat in the door of her castle, she
just looked around and saw Dorothy and her friends.
They were still far from her castle, but the Wicked
Witch was angry to find them in her country; so she
blew her silver whistle. 
</p><p>
At once a lot of great wolves gathered around her.
They had long legs and fierce eyes and sharp teeth.
</p><p>
”Go to those people," said the Witch, “and kill them."
</p><p>
"Are you not going to make them your slaves?" asked
the leader of the wolves.
</p><p>
"No," she answered, "one is of tin, and one is of
straw; one is a girl and another is a Lion. They are not
fit to work, so you may kill them."
</p><p>
"Very well," said the wolf, and he ran away quickly,
and the other wolves followed him.
</p><p>
It was lucky the Scarecrow and the Woodman did
not sleep and heard the wolves. 
</p><p>
”This is my fight," said the Woodman, ”so get behind
me and I will meet them as they come."
</p><p>
He took his sharp axe and as the leader of the
wolves jumped at the Tin Woodman he chopped the
wolf’s head from its body. There were forty wolves, and
the Tin Woodman killed them all.
</p><p>
Then he put down his axe and sat beside the
Scarecrow, who said, ”It was a good fight, friend."
</p><p>
They waited until Dorothy woke up the next
morning. It frightened the little girl to see the dead
wolves, but the Tin Woodman told her all.
</p><p>
"Thank you for saving us," she said, and sat down
to breakfast, after which they started again on their
journey.
</p><p>
This same morning the Wicked Witch came to the
door of her castle and looked out with her one eye
that could see far off. She saw that all her wolves were
dead, and the strangers were still traveling through her
country. This made her angrier than before, and she
blew her silver whistle twice.
</p><p>
At once a lot wild crows gathered around her. And
the Wicked Witch said to the King Crow, "Fly at once to
the strangers and kill them."
</p><p>
The wild crows ﬂew towards Dorothy and her
friends. When the little girl saw them she was afraid.
</p><p>
But the Scarecrow said, "This is my fight, so lie down
beside me and I will meet them as they come."
</p><p>
So they all lay upon the ground except the
Scarecrow; and he stood up and raised his arms.
And when the crows saw him they were afraid, as
scarecrows always frighten these birds, so they did not
come any nearer. But the King Crow said: 
</p><p>
"It is only a stuffed man. Do not be afraid of him."
</p><p>
The King Crow ﬂew at the Scarecrow, who caught it
by the head and killed it. There were forty crows, and
the Scarecrow killed them all.
</p><p>
"You can get up," he said to his friends, and again
they went on their journey.
</p><p>
When the Wicked Witch looked out again and saw
that all her crows were dead, she became very angry,
and blew three times her silver whistle.
</p><p>
At once a lot of black bees gathered around her.
</p><p>
"Go to the strangers and sting them to death!"
ordered the Witch, and the bees turned and ﬂew
towards Dorothy and her friends. But the travelers saw
them, and the Scarecrow said to the Woodman:
</p><p>
”Take out my straw and put it over the little girl and
the dog and the Lion, and the bees cannot sting them."
So the Woodman did this, and as Dorothy lay close
beside the Lion and held Toto in her arms, the straw
covered them all.
</p><p>
The bees came and found only the Woodman to
sting, so they ﬂew at him and broke off all their stings
against the tin, but did not hurt the Woodman at all.
And as bees cannot live with broken stings, they all
died.
</p><p>
Then Dorothy and the Lion got up, and the girl
helped the Tin Woodman put the straw back into the
Scarecrow again, until he was as good as ever. So they
started on their journey once more. 
</p><p>
The Wicked Witch was very angry when she saw
that her black bees were dead. So she called a dozen of
her slaves, who were the Winkies, and said:
</p><p>
”Go to the strangers and destroy them!"
</p><p>
The Winkies were not very brave, but they had to
obey the Wicked Witch. So they walked away until they
came near to Dorothy. Then the Lion gave a great roar
and jumped at them, and frightened them terribly, and
the poor Winkies ran back as fast as they could.
</p><p>
When they returned to the castle the Wicked Witch
beat them and sent them back to their work. Then she
sat down to think what she was going to do next. She
was a powerful and wicked Witch, and she soon made
up her mind what to do.
</p><p>
There was, in her cupboard, a Golden Cap with
beautiful diamonds and rubies. This Golden Cap had
a charm. Its owner could call three times upon the
Winged Monkeys and they had to obey any order from
the owner. But no person could call upon these strange
creatures more than three times. ”I already called upon
them two times in the past," thought the Wicked Witch;
”once they helped me make the Winkies my slaves. The
second time they helped me fight against the Great Oz
himself, and make him leave the land of the West. I can
use this Golden Cap only one more time. But now that
my fierce wolves and my wild crows and my black bees
are dead, and my slaves are afraid of the Cowardly Lion,
this is the only way to destroy Dorothy and her friends."
</p><p>
So the Wicked Witch took the Golden Cap from her
cupboard and placed it upon her head. Then she stood
upon her left foot and said slowly: 
</p><p>
”Ep-pe, pep-pe, kak-ke!"
Next she stood upon her right foot and said:
"Hil-lo, hol-lo, hel-lo!"
</p><p>
After this she stood upon both feet and cried in
a loud voice:
</p><p>
"Ziz-zy, zuz-zy, zik!"
</p><p>
Now the charm began to work. The sky became
dark, and she heard the sound of many wings. And
when the sun came out of the dark sky she saw a crowd
of monkeys, each with a pair of great and powerful
wings on his shoulders.
</p><p>
One, much bigger than the others, was their leader.
He ﬂew close to the Witch and said, "This is the third
and last time. What is your order?"
</p><p>
”Go to the strangers who are in my land and destroy
them all except the Lion," said the Wicked Witch. “Bring
that beast to me, for I want to make him my slave."
</p><p>
"We’ll obey your order," said the leader. Then the
Winged Monkeys ﬂew away to the place where Dorothy
and her friends were walking.
</p><p>
Some of the Monkeys caught the Tin Woodman and
carried him through the air to a country full of sharp
rocks. Here they dropped the poor Woodman, who fell
on the rocks, where he lay and could not move or groan.
</p><p>
Other Monkeys caught the Scarecrow, and with their
long fingers pulled all of the straw out of his clothes
and head. Then they threw his hat and boots and
clothes into the top branches of a tall tree.
</p><p>
The rest of the Monkeys caught the Lion and tied
him up with a strong rope. Then they lifted him up
and ﬂew away with him to the Witch’s castle, where
they placed him in a small yard with a high iron fence
around it, so that he could not escape.
</p><p>
But they did not harm Dorothy at all. She stood, with
Toto in her arms, and watched her friends sadly. "I’m
going to be the next one," she thought. But when the
leader of the Winged Monkeys ﬂew up to her, he saw
the mark of the Good Witch’s kiss upon her forehead
and stopped. Then he said to the other Monkeys: 
</p><p>
"Do not harm this little girl, for the Power of Good
protects her, and that is greater than the Power of Evil.
All we can do is to carry her to the castle of the Wicked
Witch and leave her there."
</p><p>
So, carefully, they lifted Dorothy in their arms and
carried her through the air until they came to the
castle, where they set her down before the Witch. Then
the leader said to the Witch:
</p><p>
"The Tin Woodman and the Scarecrow are dead;
the Cowardly Lion is on a chain in your yard. The little
girl and her dog are in your castle. Your power over the
Winged Monkeys is now over, and you will never see us
again."
</p><p>
Then all the Winged Monkeys ﬂew into the air and
were soon out of sight.
</p><p>
It surprised and worried the Wicked Witch to see
when she saw the mark on Dorothy’s forehead, for she
knew well that she could not hurt the girl in any way.
She looked down at Dorothy’s feet, and saw the Silver
Shoes. She began to tremble with fear, for she knew
that they had a powerful charm. At first the Witch
wanted to run away from Dorothy; but then she looked
into the child’s eyes and understood that the little girl
did not know of the wonderful power the Silver Shoes
gave her. So the Wicked Witch laughed to herself, and
thought, “I can still make her my slave, for she does not
know how to use her power." Then she said to Dorothy:
</p><p>
"Come with me; and do everything I tell you. You
must obey me or I will kill you, as I killed the Tin
Woodman and the Scarecrow."
</p><p>
Dorothy followed her through many of the beautiful
rooms in her castle until they came to the kitchen,
where the Witch made her clean the room.
</p><p>
Dorothy began to work meekly, for she was glad the
Wicked Witch was not going to kill her.
</p><p>
Then the Witch went to the yard to look at the
Cowardly Lion. But as she opened the gate the Lion
gave a loud roar and jumped at her so fiercely that the
Witch was afraid, and ran out and shut the gate again.
</p><p>
"You must obey me," said the Witch to the Lion, "or
I will not give you any food. And when you get hungry
you will do as I wish."
</p><p>
So after that she did not give the Lion any food; but
every day she came to the gate at noon and asked, ”Are
you ready to obey me?"
</p><p>
And the Lion always answered, "No. Don’t come in
this yard, or I will bite you."
</p><p>
In fact the Lion did not have to obey the Witch
because every night, while the woman was asleep,
Dorothy brought him some food. Then they talked of
their troubles and tried to plan some way to escape.
But they could find no way to get out of the castle, for
the yellow Winkies, who were the slaves of the Wicked
Witch, guarded it.
</p><p>
The girl had to work hard during the day. But the
Witch could not harm Dorothy, because of the mark
upon her forehead. The child did not know this, and
was full of fear for herself and Toto. Dorothy’s life
became very sad and she often cried.
</p><p>
Soon the Wicked Witch decided to get the Silver
Shoes which the girl always wore. "My bees and my
crows and my wolves are dead, and I cannot use the
power of the Golden Cap any more," she thought. "So
I must get the Silver Shoes; they will give me more
power than the silver whistle or the Golden Cap ever
could." She wanted to steal the Silver Shoes, but the
child never took them off except at night and when
she took her bath. The Witch was too much afraid of
darkness to go in Dorothy’s room at night to take the
shoes, and her fear of water was greater than her fear
of darkness, so she never came near when Dorothy was
taking her bath.
</p><p>
But finally the Witch decided how to get what she
wanted. She placed a piece of iron in the middle of the
kitchen ﬂoor, and then used her magic powers to make
the iron invisible. So, when Dorothy walked across the
ﬂoor, she stumbled over it, and fell. She was not much
hurt, but in her fall one of the Silver Shoes came off. The
Witch picked it up and put it on her own foot. 
</p><p>
The wicked woman was quite happy because one of
the shoes gave her half of their charm.
</p><p>
The little girl became angry, and said to the Witch,
”Give me back my shoe!"
</p><p>
“I will not," replied the Witch, ”for it is now my shoe,
and not yours."
</p><p>
"You are a wicked creature!" cried Dorothy. "You
have no right to take my shoe from me."
</p><p>
"I’ll keep it, just the same," said the Witch, and
laughed at her. ”And one day I’ll get the other one from
you, too."
</p><p>
This made Dorothy so angry that she picked up the
bucket of water that stood near and threw it over the
Witch. The Witch became wet from head to foot.
</p><p>
The wicked woman gave a loud cry of fear, and then,
as Dorothy looked at her in wonder, the Witch began to
melt.
</p><p>
”See what is happening!" she screamed. "In a minute
I’ll melt away."
</p><p>
“I’m very sorry," said Dorothy, who was really afraid
because the Witch was really melting away like sugar
before her very eyes.
</p><p>
”Don’t you know that water may kill me?" asked the
Witch.
</p><p>
”Of course not," answered Dorothy.
</p><p>
"Well, in a few minutes I’ll melt away, and you will
have the castle to yourself. Look out — here I go!"
</p><p>
With these words the Witch fell down and melted
away. Dorothy picked up her silver shoe, cleaned and
dried it with a cloth, and put it on her foot again. Then
she ran out to the yard to tell the news to the Lion.
</p>
</div> 

 
<h3><span>CHAPTER 13</span> THE RESCUE</h3>
<div>

<p>
The Cowardly Lion was happy to hear that the
Wicked Witch was dead, and Dorothy at once set
him free. They went together to the castle, where
Dorothy called all the Winkies together and told them,
”You are no longer slaves."
</p><p>
The yellow Winkies were very happy to be free at
last and made this day a great holiday.
</p><p>
"It is a pity that our friends, the Scarecrow and the
Tin Woodman, are not with us," said the Lion.
</p><p>
"Do you think we can rescue them?" asked the girl
anxiously.
</p><p>
”We can try," answered the Lion.
</p><p>
So they called the yellow Winkies and asked them:
”Can you help us rescue our friends?"
</p><p>
”We will do our best for you," answered the Winkies.
</p><p>
So Dorothy chose a number of the Winkies and they
all started away. They traveled that day and part of the
next until they came to the rocky country where the Tin
Woodman lay. His axe was near him, rusty and broken.
</p><p>
The Winkies lifted him carefully in their arms, and
carried him back to the Yellow Castle again. When they
reached the castle Dorothy said to the Winkies:
</p><p>
”Are any of your people tinsmiths?"
</p><p>
"Oh, yes. Some of us are very good tinsmiths," they
told her.
</p><p>
"Then bring them to me," she said. And when the
tinsmiths came, she asked, ”Can you mend him?"
</p><p>
The tinsmiths looked the Woodman over carefully
and then answered:
</p><p>
“Yes, we can mend him. He will be as good as ever."
</p><p>
So they began to work in one of the big yellow
rooms of the castle and worked for three days and
four nights until the Woodman’s arms and legs and
body and head and joints worked as well as ever. The
tinsmiths mended the Woodman’s axe too. At last he
walked into Dorothy’s room and said:
</p><p>
"Thank you for rescuing me."
</p><p>
They were so happy that they even cried a little.
Then Dorothy told the Woodman her story.
</p><p>
"It’s a pity we do not have the Scarecrow with us,"
said the Tin Woodman.
</p><p>
”We must try to find him," said the girl.
</p><p>
So she called the Winkies to help her, and they
walked all that day and part of the next until they came
to the tall tree in the branches of which there were the
Scarecrow’s hat and boots and clothes.
</p><p>
It was a very tall tree, and no one could climb it; but
the Woodman said at once, ”I’ll chop it down, and then
we can get the Scarecrow’s clothes." 
</p><p>
The Tin Woodman began to chop, and in a short
time the tree fell over with a crash, and the Scarecrow’s
clothes fell out of the branches and rolled off on the
ground.
</p><p>
Dorothy picked them up and the Winkies carried
them back to the castle, where they stuffed them with
nice and clean straw. Soon the Scarecrow was as good
as ever.
</p><p>
"Thank you for saving me," he said to his friends.
”We are together again, and that is good."
</p><p>
Dorothy and her friends spent a few happy days at
the Yellow Castle, where they found everything they
needed to make them comfortable.
</p><p>
But one day the girl thought of Aunt Em, and said,
"We must go back to Oz. He must keep his promise now.
”Yes," said the Woodman, "at last I’ll get my heart."
"And I’ll get my brains," added the Scarecrow happily.
”And I’ll get my courage," said the Lion thoughtfully.
</p><p>
"And I’ll get back to Kansas," cried Dorothy. "Oh, let
us start for the Emerald City tomorrow!"
</p><p>
So, the next day they called the Winkies together
and said to them, "Good-bye, friends. We are going
back to the Emerald City." The Winkies were sorry and
did not want to let them go. They even begged the Tin
Woodman, ”Please, stay and rule over us and the Yellow
</p><p>
Land of the West”, for they liked him so much. But when
they understood that the travelers were going to leave
them, they gave them beautiful presents.
</p><p>
Dorothy went to the Witch’s cupboard to fill her
basket with food for the journey, and there she saw the
Golden Cap. She tried it on her own head and found
that it fitted her well. She did not know anything about
the charm of the Golden Cap, but she saw that it was
pretty, so she made up her mind to wear it.
</p><p>
Then they all started for the Emerald City.
</p>
</div> 

 
<h3><span>CHAPTER 14</span> THE WINGED MONKEYS</h3>
<div>

<p>
As you remember there was no road between
the castle of the Wicked Witch and the Emerald
City. The travelers knew, of course, they must go
straight east, towards the rising sun; and they started
offin the right way. But at noon, when the sun was over
their heads, they did not know which was east and
which was west, and soon they lost their way in the
great fields.
</p><p>
"We must not stop,” said Dorothy, "then, I am sure,
we’ll sometime come to some place.”
</p><p>
But day by day passed away, and they still saw
nothing before them but the scarlet fields. One day the
Scarecrow said:
</p><p>
"We don’t know the way. We won’t be able to reach
the Emerald City in time, and I’ll never get my brains.”
</p><p>
“And I’ll never get my heart,” said the Tin Woodman.
</p><p>
"And I do not have enough courage to keep looking
for the Emerald City," said the Cowardly Lion. 
</p><p>
Then Dorothy lost heart. She sat down on the grass
and looked at her friends, and they sat down and
looked at her.
</p><p>
"Let’s call the field mice," she said. "I hope they will
tell us the way to the Emerald City."
</p><p>
"Of course, they will," cried the Scarecrow. "Why
didn’t we think of that before?"
</p><p>
Dorothy blew the little whistle that was the Queen
of the Mice’s present. In a few minutes there were
thousands of small gray mice around them. The Queen
herself was among them. She asked the girl:
</p><p>
"What can I do for my friends?"
</p><p>
"Can you tell us where the Emerald City is?" said
Dorothy.
</p><p>
"Certainly," answered the Queen; "but it is a great
way of Then she saw Dorothy’s Golden Cap, and said,
"Why don’t you use the charm of the Cap, and call the
Winged Monkeys to you? They will carry you to the City
of Oz in less than an hour."
</p><p>
"I didn’t know there was a charm," answered
Dorothy, in surprise. "What is it?"
</p><p>
"You can read it inside the Golden Cap," replied the
Queen of the Mice. ”But then we must run away, for we
do not like the Winged Monkeys."
</p><p>
”Won’t they hurt me?" asked the girl anxiously.
</p><p>
“Oh, no. They must obey the owner of the Cap. Good-
bye!" And soon she was out of sight, and all the mice
followed her.
</p><p>
Dorothy looked inside the Golden Cap, read the
directions carefully and put the Cap upon her head.
</p><p>
"Ep-pe, pep-pe, kak-ke!" she said, as she was
standing on her left foot.
</p><p>
"What did you say?" asked the Scarecrow, who did
not know what she was doing.
</p><p>
"Hil-lo, hol-lo, hel-lo!" Dorothy went on, as she was
standing this time on her right foot.
</p><p>
"Hello!" replied the Tin Woodman.
</p><p>
"Ziz-zy, zuz—zy, zik!" said Dorothy, who was now
standing on both feet. A few moments later the friends
heard the sound of many wings and a crowd of Winged
Monkeys ﬂew up to them.
</p><p>
The King bowed low before Dorothy, and asked,
"What is your order?"
</p><p>
"We wish to go to the Emerald City," said the child,
”but we don’t know how to get there."
</p><p>
”We will carry you," replied the King, and two of the
Monkeys caught Dorothy in their arms and ﬂew away
with her. Others took the Scarecrow and the Woodman
and the Lion, and one little Monkey took Toto and ﬂew
after them.
</p><p>
The Scarecrow and the Tin Woodman didn’t like it
at first; but soon they saw that the Monkeys were not
going to harm them and relaxed.
</p><p>
Dorothy was riding easily between two of the
biggest Monkeys, one of them the King himself. They
held her in their hands and were careful not to hurt the
girl.
</p><p>
"Why do you have to obey the charm of the Golden
Cap?" she asked.
</p><p>
"That is a long story," answered the King, with
a laugh; "but as we have a long journey before us, I will
tell you about it."
</p><p>
”I’ll be glad to hear it," she replied.
</p><p>
"Once," began the leader, "long ago, long before Oz,
we were free and lived happily in the great forest. We
ﬂew from tree to tree, ate nuts and fruit, and did just
as we pleased and enjoyed every minute of the day.
Nobody ruled us.
</p><p>
"There lived here then, away at the North,
a beautiful princess, who was also a powerful sorceress.
She used all her magic powers to help the people, and
she never hurt anyone who was good. Her name was
Gayelette, and she lived in a handsome ruby palace.
Everyone loved her, but her greatest sorrow was that
she could find no one to love in return, for all the men
were much too stupid and ugly for such a beautiful
and wise princess. At last, she found a boy who was
handsome and wise. Gayelette made up her mind to
make him her husband when he grew to be a man, so
she took him to her ruby palace and used all her magic
powers to make him as strong and good and handsome
as any woman could wish. When he grew to be a man,
Quelala — that was his name — was the best and wisest
and the most handsome man in all the land. Gayelette
was making everything ready for the wedding.
</p><p>
"My grandfather was at that time the King of
the Winged Monkeys which lived in the forest near
Gayelette’s palace, and he loved a joke better than
a good dinner. One day, just before the wedding, my
grandfather was ﬂying out with his friends when he
saw Quelala. The man was walking beside the river. He
was dressed in rich silk clothes, and my grandfather
decided to play a joke on him. He and his friends ﬂew
down and caught Quelala, carried him in their arms
until they were over the middle of the river, and then
dropped him into the water.
</p><p>
Swim out, my fine friend,’ cried my grandfather,
‘and perhaps the water will not ruin your clothes.’
Quelala only laughed, when he came to the top of the
water, and swam to the bank of the river. But when
Gayelette found him on the bank she saw that his rich
silk clothes were all ruined by the river.
</p><p>
"The princess was angry, and she knew, of course,
who did it. She gathered all the Winged Monkeys before
her, and she ordered her men, ‘Tie their wings and drop
them into the river.’ But my grandfather begged her,
‘Please, do not do this. The Monkeys will drown in the
river.’ So Gayelette finally took pity on them, but said,
‘This Golden Cap is my wedding present to Quelala.
From now on the Winged Monkeys will have to obey
three orders of the owner of the Golden Cap.’ Of course
my grandfather and all the other Monkeys at once
agreed, and that is how it happens that we are three
times the slaves of each owner of the Golden Cap."
</p><p>
”And what became of them?" asked Dorothy.
"Quelala was the first owner of the Golden Cap,"
replied the Monkey, ”so he was the first to order us. As
</p><p>
the princess did not like us, he called us all to him in the
forest and ordered us always to keep where she could
never see us again. We were glad to do this, for we were
all afraid of her.
</p><p>
"This was his only wish. But then the Wicked Witch
of the West became the owner of the Golden Cap. First,
she ordered us to make the Winkies her slaves. Then
she ordered us to make Oz leave the Land of the West.
Now the Golden Cap is yours, and you have the right to
order us three times."
</p><p>
As the Monkey King finished his story Dorothy
looked down and saw the green walls of the Emerald
City before them. She was glad the journey was over.
The strange creatures set the travelers down carefully
before the gate of the City, the King bowed low to
Dorothy, and then ﬂew away, and all the Winged
Monkeys followed him.
</p><p>
"That was a good ride," said the little girl.
</p><p>
"Yes, and a quick way out of our troubles," replied
the Lion. “How lucky it was that you brought away that
wonderful Cap!"
</p>

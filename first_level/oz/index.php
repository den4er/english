<h3><span>CHAPTER 23</span> HOME AGAIN</h3>
<div>

<p>
Aunt Em came out of the house and looked around.
Then she saw Dorothy who was running towards
her.
</p><p>
"My child!" she cried and put her arms around
Dorothy’s neck. "Where did you come from?"

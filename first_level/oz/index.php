<h3><span>CHAPTER 20</span> THE LION BECOMES THE KING OF BEASTS</h3>
<div>

<p>
"This forest is wonderful," said the Lion and
looked around him with joy. "It is the most
beautiful place in the world."
</p><p>
"It is too dark," said the Scarecrow.
</p><p>
"Not at all," answered the Lion. "I would like to live
here all my life. See how soft the dry leaves are under
your feet and how tall these old trees are. It is a good
home for any beast."
</p><p>
"Perhaps there are wild beasts in the forest now,"
said Dorothy.
</p><p>
“I think there are," replied the Lion, "but I do not see
any of them about."
</p><p>
They walked through the forest until it became
too dark to go any farther. Dorothy and Toto and the
Lion lay down to sleep, while the Woodman and the
Scarecrow guarded them as usual.
</p><p>
When morning came, they started again. Suddenly
they heard the growl of many wild animals, but none
of them was afraid, and they went on until they came
to an opening in the wood, in which there were
hundreds of different beasts. There were tigers and
elephants and bears and wolves and foxes and many
others, and for a moment Dorothy was afraid. But the
Lion said:
</p><p>
"They are having a meeting and I think they are in
great trouble."
</p><p>
As he spoke several of the beasts saw him, and at
once all the animals became quiet. The biggest of the
tigers came up to the Lion, bowed before him, and said:
</p><p>
"Welcome, King of Beasts! You are here to fight our
enemy and bring peace to all the animals of the forest
once more."
</p><p>
"What is your trouble?" asked the Lion quietly.
</p><p>
"We are afraid of a fierce monster," answered the
tiger. "It is like a great spider, with a body as big as
an elephant and legs as long as the tallest tree in our
forest. It catches animals and eats them. Not one of
us is safe while this fierce creature is alive. We were
having a meeting to decide what to do when you
came."
</p><p>
The Lion thought for a moment.
“Are there any other lions in this forest?" he asked.
”No; there were some, but they are all dead now."
</p><p>
"I’ll kill your enemy," said the Lion, "but you will
have to bow down to me and obey me as King of the
Forest."
</p><p>
"We’ll do that," replied the tiger; and all the other
beasts roared: ”We will!"
</p><p>
“Where is this great monster now?" asked the Lion.
</p><p>
"Over there, among the trees," said the tiger and
pointed with his paw.
</p><p>
"Take good care of my friends," said the Lion, "and
I’ll go at once to fight the monster."
</p><p>
The great spider was sleeping when the Lion found
him. The Lion knew that it was easier to fight a sleeping
enemy, so he jumped and landed right on the monster’s
back. Then he knocked the spider’s head off from its
body with his heavy paw. Then the Lion went back to
the opening where the beasts of the forest were waiting
for him and said proudly:
</p><p>
”Your enemy is dead."
</p><p>
Then the beasts bowed down to the Lion as their
King, and he promised to come back and rule over
them as soon as Dorothy was safely on her way to
Kansas.

"Your Silver Shoes will carry you over the desert,"
replied Glinda. "It’s a pity that you didn’t know their
power on the very first day you came to this country."
</p><p>
"No, it isn’t, because she helped me get my
wonderful brains!" cried the Scarecrow.
</p><p>
"And she helped me get my loving heart," said the
Tin Woodman.
</p><p>
"And she helped me get my courage," exclaimed the
Lion. 
</p><p>
"This is all true," said Dorothy, "and I am glad I was
able to help my good friends. But now each of them is
happy, and I want to go back to Kansas."
</p><p>
"The Silver Shoes," said the Good Witch, "have
wonderful powers. And one of the most curious things
about them is that they can carry you to any place in
the world in three steps, and you’ll make each step in
a wink. All you have to do is to knock the heels together
three times and order the shoes to carry you to any
place you wish to go."

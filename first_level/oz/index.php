came up to the gates, one of them said to Dorothy: 
</p><p>
“Why are you here, in the South Country?"
</p><p>
"To see the Good Witch who rules here," answered
the girl. ”Will you take us to her?"
</p><p>
"I must ask Glinda if she can see you. But tell me
your names first." They told their names, and the girl
soldier went into the Castle. After a few moments she
came back and said:
</p><p>
”Come in. Glinda is going to see you now."
</p>

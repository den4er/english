<h3><span>CHAPTER 21</span> THE COUNTRY OF THE QUADLINGS</h3>
<div>

<p>
The four travelers passed safely through the rest
of the forest, and when they left it they saw a high
hill before them. Great pieces of rock covered it
from top to bottom.
</p><p>
“That will be a hard climb," said the Scarecrow, "but
we must get over the hill all the same."
</p><p>
So he began to climb the hill and the others followed
him. But when they reached the first rock they heard
a rough voice: "Keep back!"
</p><p>
”Who are you?" asked the Scarecrow.
</p><p>
Then a head showed itself over the rock and the
same voice said, "It is our hill, and we don’t allow
anyone to cross it." 
</p><p>
"But we must cross it," said the Scarecrow. "We’re
going to the country of the Quadlings."
</p><p>
"But you will not!" replied the voice, and a strange
man stepped from behind the rock. He was quite short
and quite fat and had a big head on a thick wrinkled
neck; but he had no arms at all. When the Scarecrow saw
this he thought, "This strange creature will not stop us."
So he said, ”I’m sorry; you may like it or not, but we must
pass over your hill," and he walked bravely forward.
</p><p>
But at this moment the strange man’s neck became
very long and his head struck the Scarecrow and
knocked him down the hill. Then his head went back to
the body, and the man laughed loudly and said, ”It isn’t
as easy as you think!"
</p><p>
Then hundreds of the strange Hammer-Head
creatures came from behind the other rocks.
</p><p>
Then the Lion became quite angry. He gave a loud
roar and ran up the hill.
</p><p>
Again the strange man’s neck became very long and
his head knocked the Lion down the hill.
</p><p>
Dorothy ran down and helped the Scarecrow to his
feet, and the Lion came up to her and said, ”We cannot
fight these Hammer-Head people."
</p><p>
"What can we do, then?" she asked.
</p><p>
"Let us call the Winged Monkeys," said the Tin
Woodman. ”You can order them one more time."
</p><p>
“Very well," she answered. Then Dorothy put on
the Golden Cap, said the magic words and in a few
moments a crowd of the Winged Monkeys stood before
her. 
</p><p>
“What is your order?" asked the King of the Monkeys
and bowed low to her.
</p><p>
"Carry us over the hill to the country of the
Quadlings," answered the girl.
</p><p>
“We’ll do this," said the King, and at once the Winged
Monkeys caught the four travelers and Toto up in their
arms and carried them safely over the hill and set them
down in the beautiful country of the Quadlings.
</p><p>
”This is the last time you can call us," said the leader
to Dorothy; ”so good-bye and good luck to you."
</p><p>
"Good-bye, and thank you very much," replied the
girl; and the Monkeys rose into the air and ﬂew away.
</p><p>
The country of the Quadlings seemed rich and
happy. There were green fields, and good roads, and
pretty streams with strong bridges across them. The
fences and houses and bridges were all bright red. The
Quadlings themselves were short and fat and looked
kind and friendly. They all wore red clothes.
</p><p>
The four travelers saw a farmhouse, and walked up
to it and knocked at the door. The farmer’s wife opened
the door, and when Dorothy asked for something to eat
the woman gave them all a good dinner.
</p><p>
"How far is it to the Castle of Glinda?" asked the

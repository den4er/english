</p><p>
She put her arms around the Lion’s neck and
kissed him. Then she kissed the Tin Woodman and the
Scarecrovm.
</p><p>
Glinda the Good stepped down from her ruby throne
and kissed the little girl.
</p><p>
”Thank you for your kindness to me and my friends,"
said Dorothy.
</p><p>
Dorothy now took Toto up in her arms, knocked the
heels of her shoes together three times, and said:
</p><p>
”Take me home to Aunt Em!"
</p><p>
She made only three steps, and then she stopped so
suddenly that she rolled over upon the grass several
times before she knew where she was. Then she sat up
and looked about her.
</p><p>
“I’m home!" she cried.
</p><p>
She was sitting on the Kansas prairie, and just
before her there was a new farmhouse. Dorothy stood
up and saw that Silver Shoes were gone.
</p>

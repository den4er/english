Be-e-e-etter! Be-e-ehh!’.</p><p>
Alice looked at the Queen, then
she rubbed her eyes, and looked again.
 She couldn’t understand what
had happened. Was she in a shop?
And was that really... was it really a
sheep that was sitting here? She was
in a little dark shop, and there was an
old Sheep, who was sitting in an armchair
and knitting.</p><p>
		‘What do you want to buy?’ the
Sheep asked.</p><p>
‘I don’t know yet,’ Alice said,

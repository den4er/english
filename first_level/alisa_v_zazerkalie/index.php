butter, bread-and-butter’.</p><p>
Alice saw that the Queen was very
untidy.</p><p>
‘What has happened to you hair?’
asked Alice.</p><p>
‘The brush has got entangled in
it!’ the Queen said. ‘And I lost my
comb yesterday.’</p><p>
Alice did her best to get the hair

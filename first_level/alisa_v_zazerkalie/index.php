Alice asked at last. ’I’m not a
bird!’</p><p>
‘You are,’ said the Sheep: ‘you’re
a little goose.’</p><p>
It offended Alice a little, so they
didn’t talk to each other for a minute
or two. The boat went gently on.</p><p>
‘Oh! There are some water-lilies!’
Alice cried. ‘They are so beautiful!</p><p>
		Please, may we wait and pick some?’

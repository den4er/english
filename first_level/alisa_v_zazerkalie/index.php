The King felt very uncomfortable,
because he sat down between
the two great creatures; but there
was no other place for him.
Alice sat down on the bank of a
little brook, with the great dish on
her knees. She tried to cut the cake.
‘It’s very strange!’ she said, ‘I’ve cut 
several slices already, but they always
join on again!’</p><p>
		‘You don’t know how to cut
Looking-glass cakes,’ the Unicorn
said. ‘Hand it round first, and cut it
afterwards.’</p><p>
It was nonsense, but Alice got up
and carried the dish round, and the
cake divided itself into three pieces
as she did so. ‘Now cut it,’ said the
Lion, when she returned to her place
with the empty dish.</p><p>
‘I say, it isn’t fair!’ cried the
Unicorn. ‘The Monster has given the
Lion twice as much as me!’</p><p>
‘She’s given none for herself,
anyhow,’ said the Lion. ‘Do you like
plum-cake, Monster?’</p><p>
But before Alice could answer
him, the drums began. She couldn’t
understand where the noise came
from. She got up and jumped over
the little brook.</p><p>
She looked back and saw, that the
Lion and the Unicorn rose to their feet
with angry looks that they were interrupted
in their feast. Alice put her
hands over her ears, trying not to hear
the terrible sound of drums.</p><p>
‘If that doesn’t “drum them out
of town,”’ she thought to herself,
‘nothing ever will!’</p>
</div>

		<h3><span>Chapter 8.</span> ‘It’s my own invention!’</h3>
<div>

<p>		After a while the noise disappeared,
and Alice looked around.</p><p>
There was nobody, and her first
thought was that she had a dream
about the Lion and the Unicorn. But
then she saw a dish near her feet.</p><p>
‘So I wasn’t dreaming, after all,’ she
said to herself, ‘or... we’re all part
of the same dream. Only I hope it’s
my dream, and not the Red King’s!
I don’t like to be in another person’s
dream.’</p><p>
At this moment her thoughts
were interrupted by a loud shouting
of ‘Ahoy! Ahoy! Check!’ and a
Knight dressed in red, came galloping
to her. When he reached her, the
horse stopped suddenly: ‘You’re my
prisoner!’ the Knight cried.
But Alice was more frightened
for him than for herself at the moment,
because he fell down from the
horse. When he was again in the saddle,
he began once more ‘You’re my...</p><p>
’ but here another voice cried ‘Ahoy!
Ahoy! Check!’ and Alice looked round
in some surprise.</p><p>
This time it was a White Knight.
He came up to Alice and fell off his
horse just as the Red Knight had
done. Then he got on again, and the
two Knights sat and looked at each
other for some time without speaking. 
Alice looked from one to the other
in some confusion.</p><p>
		‘She’s my prisoner, you know!’
the Red Knight said at last.</p><p>
‘Yes, but then I came and saved
her!’ the White Knight said.</p><p>
‘Well, we must fight for her,
then,’ said the Red Knight and put
his helmet on.</p><p>
‘You will follow the Rules of
Battle, of course?’ the White
Knight said, putting on his helmet
too.</p><p>
‘I always do,’ said the Red Knight,
and they began fighting with such
fury that Alice hid behind a tree.</p><p>
‘I wonder, what the Rules of
Battle are,’ she said to herself, ‘if
one Knight hits the other, he knocks
him off his horse, and if he misses,
he falls off himself... What a noise
they make when they fall! And how
quiet the horses are! They let them
get on and off them as if they were
tables!’</p><p>
Another Rule of Battle was that
they always fell on their heads. The
battle ended when the both fell off
their horses. When they got up,
they shook hands, and then the Red
Knight galloped off.</p><p>
‘It was a victory, wasn’t it?’ said
the White Knight.</p><p>
‘I don’t know,’ Alice said doubtfully.</p><p>
‘I don’t want to be anybody’s
prisoner. I want to be a Queen.’</p><p>
		‘You will be a Queen, when you’ve
crossed the next brook,’ said the
White Knight. ‘I’ll help you to get
to the end of the wood... and then I
must go back. That’s the end of my
move.’</p><p>
‘Thank you very much!’ said
Alice. ‘May I help you with your helmet?’
It was not very easy, but finally
she took it off.</p><p>
‘Now I can breathe more easily,’
said the Knight. He put back his hair
with both hands, and turned his gentle
face and large kind eyes to Alice.
She thought she had never seen
such a strange-looking soldier in all
her life. He was dressed in armour,
which fit him very badly. And he had
a strange little letter-box across his
shoulder, which was upside-down.
Alice looked at it with great curiosity.
‘I see you like my little box,’ the
Knight said in a friendly tone. ‘It’s
my own invention... I keep clothes
and sandwiches here. You see I carry
it upside-down, so that the rain can’t
get in.’</p><p>
‘But the things can fall out,’
Alice said. ‘Do you know that the
lid’s open?’</p><p>
‘I didn’t know that,’ the Knight
said. ‘Then all the things have fallen
out! And the box is useless.’ He
was going to throw it into the bushes,
when a sudden thought came to him, and he hung it carefully on a
tree.</p><p>
		‘Can you guess why I did that?’ he
said to Alice.</p><p>
Alice shook her head.</p><p>
‘I hope some bees can make a nest
in it... then I will get the honey.’</p><p>
‘But you’ve got already a beehive...
or something like it... on your
saddle,’ said Alice.</p><p>
‘Yes, it’s a very good beehive,’
the Knight said, ‘one of the best! But
there are no bees. And the other thing
is a mouse-trap. I think the mice keep
the bees out... or the bees keep the
mice out, I don’t know.’</p><p>
‘I don’t think there are any mice
on the horse’s back,’ said Alice.</p><p>
‘Perhaps,’ said the Knight: ‘but
if they come...You see,’ he went on
after a pause, ‘it’s as well to be provided
for everything. That’s why
the horse has all those bracelets
round its feet.’</p><p>
‘But what are they for?’ Alice asked
in a tone of great curiosity. ‘Against
the bites of sharks,’ the Knight said.</p><p>
‘It’s an invention of my own! What’s
this dish for?’</p><p>
‘For plum-cake,’ said Alice.</p><p>
‘We’d better take it with us, the
Knight said. ‘It’ll be usefull if we
find any plum-cake. Help me to put it
into this bag.’</p><p>
It took a very long time to do it.
Alice held the bag very carefully, but 
the Knight was very clumsy: he fell
in the bag himself instead several
times.</p><p>
		‘It’s very difficult, you see,’ he
said, as they put the dish in it at last;
‘There are so many candlesticks in
the bag.’ And he hung it to the saddle,
which was already full of bunches
of carrots and many other things.</p><p>
Alice and the Knight walked
some time in silence. They stopped
very often, and Alice helped the poor
Knight, who was not a good rider.
Every time the horse stopped, he fell
off in front; and every time it went
on again, he fell off behind.</p><p>
‘I’m afraid you haven’t had much
practice in riding,’ she said, when
she was helping him.</p><p>
The Knight looked very much
surprised and a little offended.</p><p>
‘What makes you say that?’ he
asked, as he got back into the saddle.</p><p>
‘Because people don’t fall off
so often, when they’ve had enough
practice.’</p><p>
‘I’ve had a lot of practice,’ the
Knight said: ‘a lot of practice! The
great art of riding,’ the Knight said
in a loud voice, waving his arms as
he spoke, ‘is to keep...’ Here the sentence
ended suddenly, because the
Knight fell on the top of his head
exactly in the path where Alice was walking.
 She was frightened this
time, and asked, ‘I hope no bones are
broken?’</p><p>
		‘None,’ the Knight said. ‘The
great art of riding, as I was saying,
is... to keep your balance! Like
this... ’</p><p>
He wanted to show Alice what he
meant, and this time he fell on his
back, right under the horse’s feet.
‘A lot of practice!’ he went on repeating
all the time when Alice was
getting him on his feet again. ‘A lot
of practice!’</p><p>
‘It’s just funny!’ cried Alice, she
lost all her patience. ‘You should
ride a wooden horse on wheels!’
‘Fine! I’ll get one,’ the Knight
said thoughtfully to himself. ‘One or
two... several.’</p><p>
There was a short silence after
this, and then the Knight went on
again. ‘I’m great at inventing things.
I think you noticed that when you
picked me up last time, I was thoughtful.
Well, just then I was inventing
a new way, how to get over a gate...
Would you like to hear it?’</p><p>
‘Yes, please,’ Alice said politely.</p><p>
‘I’ll tell you!’ said the Knight.</p><p>
‘You see, I said to myself, “The only
difficulty is with the feet: the head
is high already.” Now, first I put my
head on the top of the gate... then I
stand on my head... then the feet are
high, you see... then I’m over!’</p><p>
		‘Yes, I think you’d be over,’ Alice
said thoughtfully: ‘but don’t you
think it would be very difficult?’</p><p>
‘I haven’t tried it yet,’ the
Knight said: ‘so I can’t tell now...
but I’m afraid it would be a little
difficult.’</p><p>
He looked so worried, that Alice
decided to change the subject.
‘What a curious helmet you’ve
got!’ she said cheerfully. ‘Is that
your invention too?’</p><p>
The Knight looked proudly at his
helmet, which hung on the saddle.
‘Yes,’ he said, ‘but I’ve invented a
better one than that... like a sugar
loaf. When I wore it, if I fell off the
horse, it always touched the ground
directly. So I had a very little way to
fall, you see... But there was the danger...
I could fall into it. And it happened
to me once... and the worst
of it was, that before I could get out,
the other White Knight came and
put it on. He thought it was his own
helmet.’</p><p>
Alice tried not to laugh. ‘I’m
afraid you had hurt him,’ she said,
‘because you were on the top of his
head.’</p><p>
‘I had to kick him, of course,’ the
Knight said, very seriously. ‘And
then he took the helmet off again...
but it took hours and hours to get me out.
 I was as fast as... as lightning,
you know.’</p><p>
		He raised his hands in some excitement
as he said this, rolled out of
the saddle, and fell into a hole.
Alice ran up to him, she was
afraid that he was really hurt this
time. At first she could see only his
feet, but then she heard that he kept
on talking in his usual tone. ‘It was
careless of him to put another man’s
helmet on... with the man in it, too.’
‘How can you keep on talking?’
Alice asked, as she helped him to
stand.</p><p>
The Knight looked surprised. ‘My
mind goes on working all the same.
In fact, when I am downwards, I keep
inventing new things.’</p><p>
‘Now the cleverest thing of the
sort that I ever did,’ he went on
after a pause, ‘was inventing a new
pudding.’</p><p>
‘What was it made of?’ Alice
asked.</p><p>
‘Blotting paper,’ the Knight answered.</p><p>
‘It wasn’t very nice, I’m afraid...’
‘Not very nice alone,’ he interrupted:</p><p>
‘but if it would be mixed
with other things... And here I must
leave you.’</p><p>
		They had just come to the end of
the wood.</p><p>
Alice was very much puzzled: she
was thinking of the pudding.</p><p>
‘You are sad,’ the Knight said:</p><p>
‘let me sing you a song!’</p><p>
‘Is it very long?’ Alice asked, she
had heard a lot of poetry that day.</p><p>
‘It’s long,’ said the Knight, ‘but
very, very beautiful. The name of the
song is called “Haddocks Eyes.”’</p><p>
‘Oh, that’s the name of the song,
is it?’ Alice said.</p><p>
‘No, you don’t understand,’ the
Knight said, ‘That’s what the name
is called. The name really is “The
Aged Aged Man.”’</p><p>
‘Then I have to say “That’s what
the song is called”?’ Alice corrected
herself.</p><p>
‘No, that’s quite another thing!
The song is called “Ways and Means”,
but that’s only what it’s called!’</p><p>
‘Well, what is the song, then?’
said Alice, who was completely puzzled.
‘The song really is “A-sitting On
A Gate”, and the tune is my own invention,’
the Knight said.</p><p>
He stopped his horse and with a
weak smile he began singing.</p><p>
Alice saw a lot of strange
things in her journey Through The
Looking-Glass, but this was that
she always remembered very clearly.
After many years she could retell 
it, as if it had been only yesterday...
The blue eyes and kind smile
of the Knight... the setting sun that
was shining through his hair... the
horse was quietly moving about...
and the black shadows of the forest
behind... And she listened in a half
dream to the music of the song.</p><p>
		When the Knight sang the last
words of the song, he said. ‘You have
to go only a few yards,’ he said,
‘down the hill and over that little
brook, and then you’ll be a Queen...
But you’ll stay and watch me first,
won’t you?’ he added. ‘It won’t be
long. You’ll wait and wave your
handkerchief when I get to that turn
in the road, won’t you? I think it’ll
encourage me...’</p><p>
‘Of course I’ll wait,’ said Alice:
‘and thank you very much that
you’ve come with me... and thank
you for the song... I liked it very
much!’</p><p>
So they shook hands, and then
the Knight rode slowly away into the
forest. ‘It won’t take long, I think,’
Alice said to herself, she stood and
watched him. ‘There he goes!
Right on his head as usual! However,
he gets on again very easily...’ The
Knight fell off again and again. After
the fourth or fifth fall he reached the
turn. Alice waved her handkerchief
 to him. ‘I hope he’ll be alright,’ she
said and she ran down the hill.</p><p>
‘And now it’s time for the last
brook, and I’ll be a Queen! How great
it sounds!’ She was at the edge of the
brook. ‘The Eighth Square at last!’
she cried when she jumped it over.</p><p>
‘Oh, how glad I am to get here! And
what is on my head?’ she asked. Alice
put her hands up to something very
heavy round her head.</p><p>
‘But how can it be there?’ she
said to herself, and took it off. It was
a golden crown.</p>
</div>

		<h3><span>Chapter 9.</span> Queen Alice</h3>
<div>

<p>		‘Well, this is great!’ said Alice.
‘I never thought I would be a Queen
so soon... and I’ll tell you, your
majesty,’ she said to herself, ‘you
shouldn’t lie on the grass like that!’
So she got up and walked about.
She was afraid that the crown could
fall off.</p><p>
‘But if I am really a Queen...’ she
began.</p><p>
Everything happened so strangely
that she wasn’t even surprised
when she saw the Red Queen and
the White Queen, who were sitting
close to her, one on each side. She
wanted to ask them how they came
there, but then she thought it would
not be polite. However, she thought 
she should ask if the game was over.
‘Please, would you tell me... ’ she began,
looking at the Red Queen.</p><p>
‘Speak when somebody speaks to
you!’ The Queen interrupted her.
‘But if everybody obeyed that
rule,’ said Alice, ‘and if you only
spoke when somebody spoke to you,
and the other person always waited
that you would begin, nobody would
say anything!’</p><p>
‘Don’t you understand, child...
’ cried the Red Queen, but then she
stopped, thought a bit and suddenly
changed the subject of the conversation.
‘What right have you to call
yourself a Queen? You can’t be a
Queen, till you’ve passed the examination.
And the sooner we begin it,
the better. Always speak the truth...
think before you speak... and write it
down!’</p><p>
There was then an uncomfortable
silence for a minute or two. Finally
the Red Queen said to the White
Queen, ‘I invite you to Alice’s dinner-
party this afternoon.’</p><p>
The White Queen smiled and said
‘And I invite you.’</p><p>
‘I didn’t know I have a party at
all,’ said Alice; ‘but if it’s true, I
think I should invite the guests.’</p><p>
‘Now you can do it,’ the Red
Queen said: ‘but I think you haven’t
had many lessons in manners.’</p><p>
		‘Lessons don’t teach you manners,’
said Alice. ‘Lessons teach you
to do sums and so on.’</p><p>
‘And do you know how to do
Addition?’ the White Queen asked.</p><p>
‘What’s one and one and one and
one and one and one and one and one
and one and one?’</p><p>
‘I don’t know,’ said Alice. ‘I lost
count.’</p><p>
‘She can’t do Addition,’ the Red
Queen interrupted. ‘Can you do
Subtraction? Takeninefromeight.’</p><p>
‘I can’t,’ Alice said: ‘but... ’</p><p>
‘She can’t do Subtraction,’ said
the White Queen.</p><p>
‘Can you do Division? Divide a
loaf by a knife... what’s the answer?’</p><p>
‘I think... ’ Alice began, but the
Red Queen already answered. ‘Breadand-
butter, of course. Try another
Subtraction sum. Take a bone from a
dog: what remains?’</p><p>
Alice thought a bit. ‘The bone
wouldn’t remain, of course, if I
took it... and the dog wouldn’t remain;
it would run to bite me... and I
wouldn’t remain!’</p><p>
‘Then you think nothing would
remain?’ said the Red Queen.</p><p>
		‘I think that’s the answer.’</p><p>
‘Wrong, as usual,’ said the Red
Queen: ‘the dog’s temper would remain.’</p><p>
‘But I don’t understand...’</p><p>
‘Look here!’ the Red Queen cried.</p><p>
‘The dog would lose its temper,
wouldn’t it?’</p><p>
‘Perhaps it would,’ Alice said.</p><p>
‘Then if the dog went away, its
temper would remain!’ the Queen
cried.</p><p>
‘And if it went away too?’ asked
Alice. But she thought to herself
all the time, ‘What nonsense we are
talking!’</p><p>
‘She can’t do sums at all!’ the
Queens said together.</p><p>
‘Can you do sums?’ Alice said and
turned suddenly to the White Queen.
The Queen shut her eyes. ‘I can do
Addition if you give me time...’</p><p>
‘Of course, you know your ABC?’</p><p>
said the Red Queen.</p><p>
‘I do,’ said Alice.</p><p>
‘So do I,’ the White Queen whispered:
‘we’ll learn it together, dear.</p><p>
And I’ll tell you a secret... I can read
words of one letter! Isn’t that great?
Don’t worry, you’ll learn it some
day!’</p><p>
Here the Red Queen began again.
‘Can you answer useful questions?’
she said. ‘How is bread made?’</p><p>
‘I know that!’ Alice cried. ‘You
take some flour...’</p><p>
		‘Where can you take the flower?’</p><p>
the White Queen asked. ‘In a garden,
or in the hedges?’</p><p>
‘You don’t understand,’ Alice explained:
‘it’s ground... ’</p><p>
‘How many acres of ground?’ said
the White Queen.</p><p>
‘Fan her head!’ the Red Queen
interrupted. ‘She’ll be hot!’ So they
fanned her with leaves, till she had
to ask them to stop.</p><p>
‘She’s all right again,’ said
the Red Queen. ‘Do you know
Languages? What’s the French for
fiddle-de-dee?’</p><p>
‘Fiddle-de-dee’s not English,’
Alice said.</p><p>
‘I didn’t say it,’ said the Red
Queen.</p><p>
‘If you’ll tell me what language
“fiddle-de-dee” is, I’ll tell you the
French for it!’ Alice said.</p><p>
‘Don’t let us quarrel,’ the White
Queen said. ‘What is the cause of
lightning?’</p><p>
‘The cause of lightning...’ Alice
said, ‘is the thunder... no, no!’ she
corrected herself. ‘The other way.’</p><p>
‘It’s too late to correct it,’ said
the Red Queen.</p><p>
‘Oh, we had such a thunderstom
last Tuesday,’ the White Queen said,
she was very nervous, ‘I mean last
Tuesdays.’</p><p>
Alice was puzzled.</p><p>
		‘In our country,’ she said, ‘we
have only one day at a time.’</p><p>
The Red Queen said, ‘Now here,
we have days and nights two or three
at a time, and sometimes in the winter
we have five nights together...
for warmth, you know.’</p><p>
‘It’s like a riddle with no answer!’
Alice thought.</p><p>
Here the White Queen began
again. ‘It was such a thunderstorm,
you can’t imagine! And part of the
roof came off, and thunder got in...
and it went rolling round the room...
and knocking over the tables and
things... I was so frightened, I
couldn’t remember my own name!’
‘Your Majesty must excuse her,’
the Red Queen said to Alice, she took
one of the White Queen’s hand in
her own and gently stroked it: ‘she
is good, but she always says foolish
things.’</p><p>
The White Queen looked at Alice
in a shy way. Alice felt she should say
something kind, but couldn’t think
of anything at the moment.</p><p>
‘It’s amazing how good she is! Pat
her on the head, and see how pleased
she’ll be!’ the Red Queen went on.
Suddenly the White Queen laid
her head on Alice’s shoulder. ‘I am
so sleepy,’ she said.</p><p>
‘She’s tired, poor thing!’ said
the Red Queen. ‘Give her your nightcap
and sing her a lullaby.’ ‘I haven’t got a nightcap with
me,’ said Alice: ‘and I don’t know
any lullabies.”</p><p>
‘I must do it myself, then,’ said
the Red Queen, and she began:</p><p> 

		
		‘Hush-a-by lady, in Alice’s lap!</p><p>
		Till the feast’s ready, we ve time for a nap.’</p><p>
		When the feast’s over, we’ll go to the ball—</p><p>
		Red Queen, and White Queen, and Alice, and all!</p><p> </p><p>
		

		‘Now you know the words,’ she
added and put her head down on
Alice’s other shoulder, ‘just sing it.</p><p>
I’m sleepy, too.’ In another moment
both Queens were asleep, and snoring
loud.</p><p>
‘What should I do? said Alice,
‘I don’t think it happened before,
that any one had to take care of
two Queens asleep at once! No... it
couldn’t, you know, because there
was never more than one Queen at a
time. Wake up, you heavy things!’
she went on, but there was no answer
but a gentle snoring. </p><p>
Suddenly the two Queens disappeared,
and Alice saw that she was
standing before the door over which
were the words “QUEEN ALICE” in
large letters, and on each side there
was a bell-handle; one was marked
‘Visitors’ Bell,’ and the other
‘Servants’ Bell.’</p><p>
‘Which bell should I ring?’ she
said. ‘I’m not a visitor, and I’m not a
servant. There should be one, marked
“Queen”.’</p><p>
Just then the door opened, and
a creature with a long beak put its
head out for a moment and said ‘No
entry till the next week!’ and shut
the door.</p><p>
Alice knocked and rang for a long
time, but at last, a very old Frog, who
was sitting under a tree, got up and
came slowly to her. He was dressed in
bright yellow, and had large boots.
‘What is it?’ the Frog whispered.
Alice turned round. ‘Where’s the
servant who answers the door?’ she
began angrily.</p><p>
‘Which door?’ said the Frog.
‘This door, of course!’ Alice said.
The Frog looked at the door with
his large dull eyes for a minute: then
he went nearer, then he looked at
Alice.</p><p>
‘To answer the door?’ he said.</p><p>
‘Who asks it?’ ‘I don’t know what you mean,’
she said.</p><p>
‘I talk English, don’t I?’ the Frog
went on. ‘Or are you deaf? What did
it ask you?’</p><p>
‘Nothing!’ Alice said. ‘I’ve
knocked at it!’</p><p>
‘You shouldn’t do that...’ the
Frog said. Then he went up and
kicked the door with one of his
great feet. ‘Let it alone,’ he said,
‘and it’ll let you alone.’
At this moment the door opened,
and Alice heard a song:</p><p> </p><p>


			
		‘To the Looking-Glass world it was Alice that said,</p><p>
		“I’ve a sceptre in hand, I’ve a crown on my head;</p><p>
		Let the Looking-Glass creatures, whatever they be,</p><p>
		Come and dine with the Red Queen, the White Queen, and me.”’</p><p>
		‘Then fill up the glasses as quick as you can,</p><p>
		And sprinkle the table with buttons and bran.’</p><p>
		Put cats in the coffee, and mice in the tea—</p><p>
		And welcome Queen Alice with thirty-times-three!’	</p><p> </p><p>



		Alice thought to herself ‘Thirty
times three makes ninety. Can anybody
 count here? I think I’ll go in.’
There was silence when she appeared.
Alice walked into a large hall
with a table and noticed that there
were about fifty guests. Some were
animals, some birds, and there were
even a few flowers among them. ‘I’m
glad they’ve come without invitation,’
she thought: ‘I didn’t know
who to invite!’</p><p>
There were three chairs at the
head of the table. The Red and White
Queens had already taken two of
them, but the middle chair was
empty. Alice sat down in silence.
At last the Red Queen began.</p><p>
‘You’ve missed the soup and fish,’ she
said. ‘Bring the meat!’ And the waiters
brought a leg of mutton. Alice
looked very nervous, because she had
never carved mutton leg before.</p><p>
‘You look shy; let me introduce
you to that leg of mutton,’ said
the Red Queen. ‘Alice — Mutton;
Mutton — Alice.’ The leg of mutton
got up in the dish and bowed to Alice.
And she bowed too, she didn’t know
whether she should be frightened or
amused.</p><p>
‘May I give you a slice?’ Alice
said and took the knife and fork. She
looked from one Queen to the other.</p><p>
‘Certainly not,’ the Red Queen:</p><p>

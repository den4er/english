<h3><span>CHAPTER VII</span> The Piper at the Gates of Dawn</h3>
<div>



<p>
It was past ten o’clock at night, midsummer night. Mole lay
on the bank, still resting from the stress of the hot day,
I and waited for his friend. He left the Water Rat with Otter;
and came back to find the house dark and empty. It was still
too hot to think of staying indoors, so he lay on some cool 
leaves, and thought over the past day and its doings, and
how very good they all were.
</p><p>
The Rat’s light footfall approached over the grass. ‘O, the
wonderful coolness!’ he said, and sat down.
</p><p>
‘You stayed to supper, of course?’ said the Mole.
</p><p>
‘Yes,’ said the Rat. ‘You know how kind they always are.
And they made things as happy for me as ever they could,
right up to the moment I left. But I felt bad all the time, as it
was clear to me they were very unhappy, though they tried
to hide it. Mole, I’m afraid they’re in trouble. Little Portly
is missing again; and you know how much his father cares
about him, though he never says much about it.’
</p><p>
‘What, that child?’ said the Mole lightly. ‘He’s always
walking off and getting lost, and coming back again. But
no harm ever happens to him. Everybody knows him and
likes him, and some animal or other will come across him
and bring him back again all right. Why, we found him ourselves, miles from home, and quite happy!’
</p><p>
‘Yes; but this time it’s more serious,’ said the Rat. ‘He’s missing
for some days now, and the Otters hunted everywhere without finding him. And they asked every animal for miles around,
and no one knows anything about him. Otter’s nervous. You
know, young Portly can’t swim very well yet. There’s a lot of water coming down still, and the place was always interesting for
the child. And then there are — well, things — YOU know. Ot-
ter’s not the fellow to be nervous about his sons. And now he IS
nervous. He is going to spend the night watching by the old ford.’
</p><p>
‘I know it very well,’ said the Mole. ‘But why did Otter
choose to watch there?’
</p><p>
‘Well, it was there he gave Portly his first swimming-lesson,’ answered the Rat. ‘And it was there he taught him fishing.
 The child loves the place, and Otter thinks he might
come there. So Otter goes there every night and watches.’
</p><p>
They didn’t speak for some time, both thinking of the
same thing — the lonely, sad animal, sitting by the ford,
watching and waiting the long night through.
</p><p>
‘Well, well,’ said the Rat, ‘I think we must go to bed.’ But
he didn’t move.
</p><p>
‘Rat,’ said the Mole, ‘I simply can’t go and sleep, and DO
nothing. We’ll get the boat out, and row up stream. The moon
will be up in an hour or so, and then we will look as well as
we can — anyhow, it will be better than going to bed and
doing NOTHING.’
</p><p>
‘Iust what I was thinking myself,’ said the Rat. ‘It’s not
the sort of night for bed anyhow.’
</p><p>
They got the boat out and set off. The night was full of small
noises, song and chatter, telling of the busy little animals who
were up and about. They heard the water’s own noises, too.
</p><p>
The friends landed in this silent, silver kingdom, and explored the hedges, the trees, the tunnels, the ditches and dry
water-ways.
</p><p>
Then the horizon became clearer, field and tree came
more into sight, and somehow with a different look. Suddenly they heard sweet music and followed it. Mole rowed,
and soon they came to a point where the river divided.
</p><p>
On either side of them they saw the rich meadow-grass.
In the middle of the river, behind the willows, lay a small island. Slowly, the two animals stopped their boat at the ﬂow-
ery side of the island. In silence they landed, and pushed
through the ﬂowers and grass.
</p><p>
‘This is the place of my song-dream, the place the music
played to me,’ said the Rat, as if in a dream.
</p><p>
Then suddenly the Mole felt something great fall upon
him, that changed his muscles to water. It was no panic terror — indeed he felt wonderfully at peace and happy — but
without seeing, he knew it could only mean that some great
Presence was very, very near. With difficulty he turned to
look for his friend and saw him at his side shocked.
</p><p>
He raised his head; and then, in that clearness of the
early morning he looked in the very eyes ofthe Friend and
Helper; he saw the big nose between the kindly eyes that
were looking down on them, while the bearded mouth
broke into a half-smile at the corners; saw the muscles on
the arm that lay across the broad chest, the long hand still
holding the pan-pipes; saw, last of all, between his very
feet, sleeping in peace, the little, round, childish form of
the baby otter. All this he saw, for one moment.
</p><p>
‘Rat!’ he said, shaking. ‘Are you afraid?’
</p><p>
‘Afraid?’ answered the Rat, his eyes shining with love.
‘Afraid! Of HIM? O, never, never! And yet — and yet — O,
Mole, I am afraid!’
</p><p>
Then the two animals bowed their heads.
</p><p>
The sun’s broad golden disc showed itself over the horizon facing them. When they could see once more, the Vision
disappeared, and the air was full of the carol of birds.
</p><p>
As they looked, a little wind, dancing up from the water,
shook the roses and blew lightly in their faces; and with
its soft touch came forgetfulness. Mole rubbed his eyes and
looked at Rat. ‘I beg your pardon; what did you say, Rat?’
he asked.
</p><p>
‘I think I was only saying,’ the Rat said slowly, ‘that this
was the right sort of place to find Portly. And look! There he
is!’ And with a cry ofjoy he ran towards the sleeping Portly.
</p><p>
But Mole stood still a moment, deep in thought. He
seemed to be trying to remember a beautiful dream, but
he could remember nothing but a sense of the beauty of it.
So Mole shook his head sadly and followed the Rat.
</p><p>
Portly woke up, and laughed with pleasure at the sight
of his father’s friends. But in a moment his face grew sad,
and he fell to hunting round in a circle. As a child that falls
happily asleep in its nurse’s arms, and wakes to find itself
alone and in a strange place, and runs from room to room,
so Portly ran about the island, till at last the black moment
came for giving it up, and sitting down and crying.
</p><p>
The Mole ran quickly to comfort the little animal. Portly
was soon quiet, thinking of a trip on the river in Mr. Rat’s
real boat; and the two animals led him to the water’s side,
placed him securely between them in the bottom of the
boat, and rowed off. The sun was fully up by now, birds
sang, and ﬂowers smiled.
</p><p>
As they drew near the ford, the Mole took the boat into
the bank, and they lifted Portly out and set him on his legs,
gave a friendly pat on the back, and rowed out into mid-
stream. They watched the little animal till they saw his walk
break into a run. Looking up the river, they could see Otter
stand up, and could hear his joyous cry. So they let the full
stream bear them down again.
</p><p>
‘I feel strangely tired, Rat,’ said the Mole. ‘I feel as ifI was
in something very exciting and even terrible; but nothing
happened}
</p><p>
‘Or something very surprising and beautiful,’ said the
Rat, closing his eyes. ‘I feel just as you do, Mole. It’s lucky
the stream takes us home. Isn’t it great to feel the sun again!
And the wind playing in the trees!’
</p><p>
‘It’s like music — faraway music,’ said the Mole dreamily.
</p><p>
‘So I was thinking,’ said the Rat. ‘Dance-music — but with
words in it, too — it goes into words and out of them again —
then it is dance-music once more, and then nothing.’
</p><p>
‘You hear better than I,’ said the Mole sadly. ‘I cannot catch
the words.’
</p><p>
‘Let me try and give you them,’ said the Rat softly, his
eyes still closed. ‘Helper and friend, I cheer — Small animals wet — Asking them all forget! No, it is no good; the
song stopped.’
</p><p>
‘But what do the words mean?’ asked the wondering Mole.
</p><p>
‘That I do not know,’ said the Rat simply. ‘I passed them on
to you as they reached me. Ah! now they return again! This
time, at last, it is the real thing, simple — perfect—’
</p><p>
‘Well, let’s have it, then,’ said the Mole, after he waited
patiently for a few minutes, half-sleeping in the hot sun.
</p><p>
But no answer came. He looked, and understood the si-
lence. With a happy smile on his face, the tired Rat was asleep.
</p>

<h3><span>CHAPTER I</span> The River Bank</h3>
<div>

<p>The Mole was working very hard all the morning,
 spring-cleaning his little home. First he dusted it,
then he washed it, and finally he whitewashed it.
Spring was in the air above and in the earth below and
around him, coming even into his dark little house. He suddenly
dropped his brush on the ﬂoor, and jumped out ofthe
house. Something up above was calling him, and he made
for the little tunnel which led to the sun and air. So he
scraped and scratched and scratched and scraped, working
busily with his little paws and saying to himself, ‘Up we go!
Up we go!’ till at last his nose came out into the sunlight, and
he was rolling in the warm grass of a great meadow.</p>
<p>
‘This is fine!’ he said to himself. ‘This is better than
whitewashing!’ The sunshine was hot on his fur, soft breezes
caressed his head, and he heard the song of happy birds.
Iumping on all his four legs at once, in the joy of living
and the delight of spring, he ran across the meadow till he
reached the hedge far off.</p>
<p>
‘Hold up!’ said an elderly rabbit. ‘SiXpence for passing by
the private road!’ The impatient Mole ran along the hedge 
and joked to the other rabbits as they looked from their
holes.</p>
<p>
It all seemed too good to be true. He saw everywhere
birds building, ﬂowers growing, leaves coming out —
everything happy, and busy. And instead of thinking about
whitewashing he could only feel how merry he was. After
all, the best part ofa holiday is perhaps not so much to have
rest, as to see all the others busy working.</p>
<p>
Suddenly he stopped at a river. He never saw a river before
— this shiny, wavy stream, running and laughing, fresh
and free. The Mole loved it at once. He walked by the side of
the river; when tired at last, he sat down on the bank, while
the river still chattered on to him.</p>
<p>
As he sat on the grass and looked across the river, he
saw a dark hole in the bank opposite. He thought, ‘What a
nice living-place it is, far from noise and dust!’ As he looked,
something bright and small appeared there like a little star.
Then he decided it was an eye; and a small face began to
grow up round it.</p>
<p>
A brown little face.</p>
<p>
A serious round face.</p>
<p>
Small ears and thick silky hair.</p>
<p>
It was the Water Rat!</p>
<p>
Then the two animals stood and looked at each other.</p>
<p>
‘Hullo, Mole!’ said the Water Rat.</p>
<p>
‘Hullo, Rat!’ said the Mole.</p>
<p>
‘Would you like to come over?’ asked the Rat.</p>
<p>
‘Oh, it’s all very well to TALK,’ said the Mole, because he
was new to a river and riverside life.</p>
<p>
The Rat said nothing, but pulled at a rope and then
stepped into a little boat. It was blue outside and white in
side, and was just the size for two animals. The Mole loved
it at once.</p>
<p>
The Rat rowed smartly and fast. Then he held up his
paw as the Mole stepped down. And to his surprise the
Mole found himself in the stern of a real boat.</p>
<p>
‘This is a wonderful day!’ said he, as the Rat started to
row again. ‘Do you know, it’s my first time in a boat in all
my life.’</p>
<p>
‘What?’ cried the Rat, with his mouth open: ‘You never —
well Ican’t believe it!’</p>
<p>
‘Is it so nice as all that?’ asked the Mole shyly, as he looked
at the cushions, the oars, and all the fascinating things.</p>
<p>
‘Nice?’ said the Water Rat. ‘Believe me, my young friend,
there is NOTHING — absolutely nothing — better then
travelling about in boats.’ He went on dreamily: ‘Travelling — about — in — boat—’</p>
<p>
‘Look ahead, Rat!’ cried the Mole suddenly.</p>
<p>
It was too late. The boat struck the bank. The dreamer
lay on his back at the bottom of the boat, his feet in the air.</p>
<p>
‘—about in boats — or WITH boats,’ the Rat went on
peacefully, and stood up with a pleasant laugh. ‘In or out of
them, it doesn’t matter. Nothing really matters, that’s the
charm of it. You’re always busy, and you never do anything
in particular. Let’s have a boat-trip together?’</p>
<p>
The Mole was happy. ‘WHAT a day I’m having!’ he said.
‘Let’s start at once!’</p>
<p>
‘]ust a minute, then!’ said the Rat. He climbed up into
his hole above, and after a short interval reappeared with
a big basket.</p>
<p>
‘Put that under your feet,’ he said to the Mole. Then he
took the oars again.</p>
<p>
‘What’s inside it?’ asked the Mole.</p>
<p>
‘There’s cold chicken inside it,’ replied the Rat; ‘cold
tongue, cold ham, cold beef, cucumbers, salad, French rolls,
sandwiches, lemonade, soda water—’</p>
<p>
‘O stop, stop,’ cried the Mole: ‘This is too much!’</p>
<p>
‘Do you really think so?’ asked the Rat seriously. ‘It’s
only what I always take on these little excursions!’</p>
<p>
The Mole didn’t hear a word. He was entering the new
life, fascinated with the sparkle, the scents and the sounds
and the sunlight. He put a paw in the water and dreamed.
The Water Rat didn’t disturb him.</p>
<p>
‘I like your clothes, friend,’ he said after half an hour or
so. ‘I’m going to get a black velvet suit myself someday.’</p>
<p>
‘I beg your pardon,’ said the Mole. ‘But all this is so new
to me. So — this — is — a — River!’</p>
<p>
‘THE River,’ corrected the Rat.</p>
<p>
‘And you really live by the river? What a happy life!’</p>
<p>
‘By it and with it and on it and in it,’ said the Rat. ‘It’s
brother and sister to me, and aunts, and company, and food
and drink, and (naturally) washing. It’s my world, and I
don’t want any other. In winter or summer, spring or autumn,
it’s always got its fun and its excitements. When in February the brown water runs by my best bedroom window; or
again when it all goes away and there is mud that smells
like plum-cake, and I can find fresh food to eat, and things
that people throw out of boats!’</p><p>

‘What is over THERE?’ asked the Mole, showing with a
paw towards woodland that was on one side of the river.
</p><p>
‘O, that’s just the Wild Wood,’ said the Rat. ‘We don’t go
there very much, we river-bankers.’
</p><p>
‘Aren’t they — aren’t they very NICE people in there?’
said the Mole nervously.
</p><p>
‘Well,’ answered the Rat, ‘let me see. The squirrels are
all right. AND the rabbits — some of them. Weasels — and
stoats — and foxes — and so on. They’re all right in a way —
but well, you can’t really trust them, and that’s the fact.’
</p><p>
‘And what’s behind the Wild Wood?’ the Mole asked.
</p><p>
‘Behind the Wild Wood comes the Wide World,’ said the
Rat. ‘And that’s something that doesn’t matter. I never was
there, and I’m never going. Here’s our backwater at last,
where we’re going to lunch.’
</p><p>
They now passed into what looked like a little lake. It
was so very beautiful that the Mole could only hold up both
paws and say, ‘O my! O my! O my!’
</p><p>
The Rat brought the boat to the bank, helped the Mole to
the shore, and took out the lunch-basket. The Mole wanted
to unpack it all by himself; and the Rat happily agreed, and
rested on the grass, while his excited friend shook out the
table-cloth and took out all the mysterious packets one by
one. When all was ready, the Mole was very glad, because
he was very hungry. ‘What are you looking at?’ said the Rat,
when the Mole’s eyes could leave the table-cloth a little.
</p><p>
‘I am looking,’ said the Mole, ‘at bubbles travelling along
the water. That is funny.’
</p><p>
‘Bubbles? Oho!’ said the Rat.
</p><p>
A broad shiny face showed itself above the bank, and the
Otter rose out and shook the water from his coat.
</p><p>
‘Why didn’t you invite me, Ratty?’ he asked.
</p><p>
‘We didn’t plan the trip, it just happened,’ explained the
Rat. ‘By the way — my friend Mr. Mole.’
</p><p>
‘Glad,’ said the Otter, and the two animals became friends.
</p><p>
‘Such a crowd everywhere!’ continued the Otter. ‘All the
world is out on the river today. I came up this backwater to
get a moment’s peace.’
</p><p>
‘Well, tell us, WHO’S out on the river?’ said the Rat.
</p><p>
‘Toad’s out,’ replied the Otter. ‘In his new boat!’
</p><p>
The two animals looked at each other and laughed.
</p><p>
‘Once, it was nothing but sailing,’ said the Rat, ‘Then
he grew tired of that. Last year it was house-boating, and
we all stayed with him in his house-boat. He was going to
spend all his life in a house-boat. It’s all the same, whatever
he takes up; he gets tired of it, and starts on something
new.’
</p><p>
‘Such a good fellow, ’ remarked the Otter. ‘But no stability!’
</p><p>
From where they sat they saw a boat, the rower —
a short, fat figure — rowing badly and rolling much, but
working hard. The Rat stood up and called him, but Toad —
for it was he — shook his head and continued his work.
</p><p>
‘He’ll be out of the boat in a minute, it’s rolling too much,’
said the Rat, sitting down again.
</p><p>
‘Of course he will,’ smiled the Otter.
</p><p>
‘Well, well,’ said the Rat, ‘I think it’s time to go. Who will
pack the lunch-basket?’
</p><p>
‘O, please let me,’ said the Mole. So, of course, the Rat
let him.
</p><p>
Packing the basket was not quite such pleasant work as
unpacking the basket. It never is. The afternoon sun was
getting low as the Rat rowed home, saying poetry-things
to himself, and not paying much attention to Mole. But the
Mole was very full of lunch, and pride, and already quite at 
home in a boat (so he thought), and then he said, ‘Ratty!
Please, I want to row, now!’
</p><p>
The Rat shook his head with a smile. ‘Not yet, my young
friend,’ he said, ‘you need some lessons. It’s not as easy as
it looks.’
</p><p>
The Mole was quiet for a minute or two. But then he
jumped up and took the oars, so suddenly, that the Rat fell
backwards off his seat with his legs in the air for the second
time, while the happy Mole took his place.
</p><p>
‘Stop it, you SILLY!’ cried the Rat, from the bottom ofthe
boat. ‘You can’t do it! You’ll turn us over!’
</p><p>
The Mole made a great dig at the water. He missed, his
legs flew up above his head, and he found himself lying
on the top of the Rat. He caught the side of the boat, and
the next moment the boat went over, and he was in the
river.
</p><p>
O my, how cold the water was, and O, how VERY wet!
How it sang in his ears as he went down, down, down! Then
a strong paw caught him by the back of his neck. It was the
Rat, and he was laughing.
</p><p>
The Rat took the helpless animal to shore, and set him
down on the bank.
</p><p>
Then the Rat said, ‘Now, my fellow! Run up and down
the path till you’re warm and dry again, and I dive for the
basket’
</p><p>
So the Mole, wet and ashamed, ran about till he was
dry, while the Rat caught the boat and got to land with the
basket.
</p><p>
When all was ready, the Mole took his seat in the stern of
the boat; he said in a low voice, ‘Ratty, my dear friend! I am
very sorry for my foolish conduct. Will you forgive me?’
</p><p>
‘That’s all right!’ answered the Rat. ‘What’s a little wet to
a Water Rat? I’m more in the water than out of it most days.
Don’t think any more about it; and, look here! You can come
and stay with me for a little time. And I’ll teach you to row,
and to swim.’
</p><p>
The Mole was happy again.
</p><p>
When they got home, the Rat made a bright fire in the
room and got the Mole a dressing-gown and slippers, and
told him river stories till supper-time. Supper was a cheerful 
meal; but very shortly afterwards a terribly sleepy Mole
went to the best bedroom.
</p><p>
This day was only the first of many similar ones for the
Mole, each of them longer and full of interest. He learnt to
swim and to row, and came into the joy of running water.

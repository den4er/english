<h3><span>CHAPTER VI</span> Mr. Toad</h3>
<div>



<p>
It was a bright morning in the early part of summer. The 
Mole and the Water Rat were getting ready for the 
opening of the boating season; painting and cleaning,
repairing, hunting for missing details, and so on. They
were finishing breakfast in their little room and discussing their plans for the day, when a heavy knock sounded
at the door.
</p><p>
The Mole went to open, and the Rat heard him cry
out in surprise. Then he said with much importance, ‘Mr.
Badger!’
</p><p>
This was a wonderful thing, indeed, that the Badger paid
a formal call on them, or indeed on anybody. The Badger
walked heavily into the room, and stood looking at the two
animals with an expression full of seriousness. The Rat
dropped his egg-spoon on the table-cloth.
</p><p>
‘I’m ready!’ said the Badger at last.
</p><p>
‘Ready! For what?’ asked the Rat uneasily.
</p><p>
‘Why, for Toad!’ answered the Badger. ‘I said, “I’m going
to take him in hand in spring,” and I’m going to take him in
hand today!’
</p><p>
‘Of course!’ cried the Mole. ‘I remember now! WE’LL
teach him to be a clever Toad!’
</p><p>
‘This morning,’ continued the Badger, taking an armchair,
‘another new and very powerful car will arrive at Toad Hall.
At this very moment, perhaps, Toad is changing from a
(comparatively) good-looking Toad into a horrible Object. 
We must act, before it is too late. You will accompany
me to Toad Hall, and the work of rescue must begin.’
</p><p>
‘Right you are!’ cried the Rat, starting up. ‘We’ll rescue
the poor unhappy animal!’
</p><p>
They set offup the road on their mission, Badger leading
the way.
</p><p>
They reached the carriage-drive of Toad Hall to find a
new bright-red car, of great size, standing in front of the
house. As they neared the door it opened, and Mr. Toad, in
driver’s glasses, cap, and enormous overcoat, came down
the steps, pulling on his gloves.
</p><p>
‘Hullo!’ he cried cheerfully. ‘You’re just in time to come
with me for a happy—’
</p><p>
He didn’t finish the sentence as he saw the serious look
on the faces of his friends.
</p><p>
The Badger came up the steps. ‘Take him inside,’ he said
to his companions. Then, as Toad was fighting and protesting, he turned to the driver of the new car.
</p><p>
‘I’m afraid Mr. Toad will not need the car today. You
needn’t wait,’ he said and followed the others inside and
shut the door.
</p><p>
‘Now then!’ he said to Toad, when the four ofthem stood
together in the Hall, ‘first of all, take those funny things off!’
</p><p>
‘I will not!’ replied Toad, with great emotion. ‘What is
the meaning of this?’
</p><p>
‘Take them off him, then, you two,’ said the Badger.
</p><p>
They made Toad lie down on the ﬂoor, and he was fighting and calling them all sorts of names. Then the Rat sat
on him, and the Mole got his car-clothes off him bit by bit,
and they stood him up on his legs again. A lot of his emotions seemed to leave him with his fine clothes. Now hewas
 merely Toad, and no longer the Terror of the Road. He
smiled and looked from one to the other, seeming quite to
understand the situation.
</p><p>
‘You knew it must come to this, sooner or later, Toad,’
the Badger said. ‘You didn’t listen to us, you were losing
your money, and you’re getting us animals a bad name in
the district by your terrible driving and your smashes and
your arguments with the police. We animals never allow
our friends to make fools of themselves. I’ll make one more
effort to bring you to reason. You will come with me into the
room, and there you will hear some facts about yourself;
and I hope they will help you to change your mind.’
</p><p>
He took Toad by the arm, led him into the room, and
closed the door behind them.
</p><p>
‘THAT’S no good!’ said the Rat. ‘TALKING to Toad’ll never
change him. He’ll SAY anything.’
</p><p>
They made themselves comfortable in armchairs and
waited. Through the closed door they could just hear the
sound of the Badger’s voice, and then they also heard the
crying from Toad.
</p><p>
After some three-quarters of an hour the door opened,
and the Badger reappeared, leading by the paw a very weak
Toad. His legs were shaking, and there were tears on his
cheeks.
</p><p>
‘Sit down there, Toad,’ said the Badger kindly, pointing
to a chair. ‘My friends,’ he went on, ‘I am happy to inform
you that Toad is truly sorry for his conduct in the past, and
he is going to give up cars forever.’
</p><p>
‘That is very good news,’ said the Mole.
</p><p>
‘Very good news,’ said the Rat doubtfully, ‘if only — IF
only—’
</p><p>
He was looking at Toad as he said this.
</p><p>
‘There’s only one thing more,’ continued the Badger.
‘Toad, I want you to repeat it, before your friends here. First,
are you sorry for your conduct?’
</p><p>
There was a long, long pause. Toad looked this way and
that, while the other animals waited. At last he spoke.
</p><p>
‘No!’ he said; ‘I’m NOT sorry. And it wasn’t wrong at all!
It was simply wonderful!’
</p><p>
‘What?’ cried the Badger in surprise. ‘Didn’t you tell me
just now, in there—’
</p><p>
‘Oh, yes, yes, in THERE,’ said Toad quickly. ‘I said anything in THERE. You’re so moving, dear Badger — you can
do what you like with me in THERE, and you know it. But
now I find that I’m not a bit sorry really, so it’s no good
saying I am, is it?’
</p><p>
‘Then you don’t promise,’ said the Badger, ‘never to
touch a car again?’
</p><p>
‘Certainly not!’ answered Toad emotionally. ‘On the contrary, I promise to go off in the very first car I see!’
</p><p>
‘Told you so, didn’t I?’ said the Rat to the Mole.
</p><p>
‘Very well, then,’ said the Badger, rising to his feet. ‘As you
won’t listen, we’ll try what force can do. You often asked us three
to come and stay with you, Toad, in this beautiful house; well,
now we’re going to. Take him upstairs, you two, and lock him up
in his bedroom, while we arrange matters between ourselves.’
</p><p>
‘It’s for your own good, Toady, you know,’ said the Rat
kindly, as they were leading the struggling Toad upstairs.
‘Think what fun we will have together, just as before, when
you are over it!’
</p><p>
‘We’ll take great care of everything for you till you’re
well, Toad,’ said the Mole; ‘and we’ll see your money is safe.’
</p><p>
‘No more of those incidents with the police, Toad,’ said
the Rat, as they put him into his bedroom.
</p><p>
‘And no more weeks in hospital, Toad,’ finished the Mole,
turning the key on him.
</p><p>
Then they met in conference on the situation.
</p><p>
‘It’s going to be a difficult business,’ said the Badger. ‘Toad
is really hard this time. Well, we will see it out. We will take
turns to be with him, till the poison goes out of his system.’
</p><p>
They arranged watches accordingly. Each animal took it
in turns to sleep in Toad’s room at night, and they divided
the day up between them. At first Toad was very hard to his
careful guardians. He set bedroom chairs to look like a car
and sat on the first of them, looking fixedly ahead, making
noises, till he reached the peak, when, turning a complete
somersault, he lay among the ruins of the chairs, apparently
quite happy for the moment. As time passed, these games
became fewer, and his friends tried to interest him in something else. But his interest in other matters did not seem to
last, and he grew depressed.
</p><p>
One fine morning the Rat, whose turn it was to keep
watch, went upstairs to change Badger. ‘Toad’s still in bed,’
he told the Rat, outside the door. ‘Can’t get much out of him,
except, ”O leave me alone, I want nothing, perhaps I’ll be better in time,” and so on. Now, you look out, Rat! When Toad’s
quiet, then he’s foxy. I know him. Well, now, I must go.’
</p><p>
‘How are you today, old chap?’ asked the Rat cheerfully,
as he came to Toad’s bedside.
</p><p>
He waited some minutes for an answer. At last a weak
voice answered, ‘Thank you so much, dear Ratty! So good
of you to ask! But first tell me how you are yourself, and the
excellent Mole?’
</p><p>
‘O, WE’RE all right,’ answered the Rat. ‘Mole is going out
for a run with Badger. They’ll be out till lunch time, so you
and I will spend a pleasant morning together. Now jump up,
there’s a good fellow, and don’t lie there on a fine morning
like this!’
</p><p>
‘Dear, kind Rat,’ said Toad, ‘how little you understand my
feelings, and how very far I am from “jumping up” now! But
do not trouble about me. I hate to be a trouble to my friends,
and I will not be one much longer.’
</p><p>
‘Well, I hope not,’ said the Rat. ‘I’m glad to hear it’s going
to stop. And in weather like this, and the boating season just
beginning! It’s too bad of you, Toad! It isn’t the trouble we
mind, but you’re making us miss such a lot.’
</p><p>
‘I’m afraid it IS the trouble you mind,’ said the Toad. ‘I
can quite understand it. It’s natural enough. You’re tired of
me. I mustn’t ask you to do anything further.’
</p><p>
‘But I tell you, I will take any trouble for you, just be a
serious animal.’
</p><p>
‘If only I could ask you, Ratty,’ said Toad, weaker than
ever, ‘—for the last time, probably — to go to the village as
quickly as possible — even now it may be too late — and
bring the doctor.’
</p><p>
‘Why, what do you want a doctor for?’ asked the Rat,
coming nearer and examining him. He certainly lay very
still and ﬂat, and his voice was weak.
</p><p>
‘Of course you noticed of late—’ said Toad. ‘But, no —
never mind — forget what I asked.’
</p><p>
‘Look here, old chap,’ said the Rat, beginning to get nervous, ‘of course I’ll bring a doctor to you. But you can’t be bad
enough for that yet. Let’s talk about something else.’
</p><p>
‘I think, dear friend,’ said Toad, with a sad smile, ‘that
“talk” can do little in a case like this. And, by the way — I
HATE to give you more trouble, but could you at the same
time ask the lawyer to come up?’
</p><p>
‘A lawyer! O, he must be really bad!’ the Rat said to himself, as he ran from the room, not forgetting to shut the door
carefully behind him.
</p><p>
Outside, he stopped to think. The other two were far
away, and he had no one to consult.
</p><p>
‘It’s best to be on the safe side,’ he said. ‘I know Toad
showed off bad before; but I never heard him ask for a lawyer! Iwill better go; it won’t take very long.’ So he ran off to
the village.
</p><p>
The Toad jumped lightly out of bed and watched him
from the window till he disappeared. Then, laughing loudly,
he dressed as quickly as possible, filled his pockets with
money, and next, binding the sheets from his bed together,
he got out, and, taking the opposite direction to the Rat, set
off, singing a merry song.
</p><p>
The Rat was very unhappy telling the Badger and the
Mole his pitiful story. ‘He did it terribly well,’ said the depressed Rat.
</p><p>
‘He fooled you terribly well!’ said the Badger hotly. ‘But
talking won’t change anything. He’s away for the time,
that’s certain; and the worst of it is, he’ll be so happy with
his cleverness that he may make any mistake. One comfort
is, we’re free now, and needn’t lose any more of our time.
Let’s stay at Toad Hall for a while longer. Toad may appear
at any moment — between two policemen, or two doctors.’
</p><p>
So spoke the Badger, not knowing what the future prepared for them.
</p><p>
At that time Toad was walking happily along the road,
some miles from home. ‘Clever piece of work!’ he said to
himself smiling. ‘Brain against force — and brain is the winner. Poor old Ratty! The Badger will be so angry with him!
A nice fellow, Ratty, with many good qualities, but no brains
and absolutely no education. I must take him in hand some
day, and see ifI can make something of him.’
</p><p>
Full of proud thoughts, he walked along till he reached
a little town. There he came into a café, ordered the best
lunch, and sat down to eat it in the coffee-room.
</p><p>
Suddenly a well-known sound, approaching down the
street, made him jump. The poop-poop! nearer and nearer,
the car turned into the yard and stopped, and Toad held on
to the leg ofthe table to hide his emotion. The party entered
the coffee-room, hungry, talkative, and happy. Toad listened
for a time; at last he went out of the room quietly, paid his
bill at the bar, and as soon as he got outside, came quietly
to the yard. ‘There cannot be any harm,’ he said to himself,
‘in my only just LOOKING at it!’
</p><p>
The car stood in the middle of the yard, and there was
no one near it. Toad walked slowly round it, inspecting, criticizing, thinking deeply.
</p><p>
‘Does it START easily, I wonder?’ he said to himself.
</p><p>
Next moment, hardly knowing how it happened, he
found he was in the car. As if in a dream he found himself in
the driver’s seat; as if in a dream, he drove the car out of the
yard. All sense of right and wrong, all fear disappeared. As
the car jumped along on the road through the open country,
he only felt that he was Toad once more, Toad the terror of
the road. He sang, not thinking of what might come to him.
</p><p>

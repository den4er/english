<h3><span>CHAPTER III</span> The Wild Wood</h3>
<div>


<p>
The Mole wanted to make friends with the Badger. He 
seemed to be an important person. The Water Rat
said: ‘It’s all right. Badger will come some day, and
then I’ll introduce you.’
</p><p>
‘Couldn’t you invite him for dinner?’ said the Mole.
‘He won’t come,’ replied the Rat. ‘Badger hates Society,
and invitations, and dinner, and all that sort of thing.’
‘Couldn’t we go and call on HIM, then?’ asked the Mole.
</p><p>
‘O, he won’t like that at ALL,’ said the Rat. ‘He’s so
very shy, I never call on him at his own home. Besides,
we can’t, because he lives in the very middle of the Wild
Wood.’
</p><p>
‘You said, “The Wild Wood is all right.”’
</p><p>
‘O, I know, so it is,’ replied the Rat. ‘But we won’t go there
just now.’
</p><p>
But the Badger never came along, and every day brought
its joys. Summer was over, and cold and frost kept them indoors, and the Mole often thought about the grey Badger,
who lived his own life by himself, in his hole in the middle
of the Wild Wood.
</p><p>
In the winter time the Rat slept a lot. During his short
day he sometimes wrote poetry or did other small jobs
about the house; and, of course, there were always animals
coming in for a chat, and there was much story-telling.
</p><p>
On those short winter days when the animals sat round
the fire, the Mole had a lot of free time. So one afternoon he
decided to go out by himself and explore the Wild Wood,
and perhaps meet Mr. Badger.
</p><p>
It was a cold afternoon with a grey sky, when he left the
warm house. The country lay leaﬂess around him, and he
could see so far on that winter day when Nature was deep
in her sleeping. With great enthusiasm he went towards
the Wild Wood, which lay before him, like a black island in
some southern sea.
</p><p>
There was nothing to be afraid of, all was fun. It led him
on, and he went to where the light was less, and trees came
nearer and nearer, and holes looked ugly.
</p><p>
Everything was very quiet now. The darkness grew.
</p><p>
Then the faces began.
</p><p>
He first thought he saw a face; a little evil face, looking 
out at him from a hole. When he turned, it disappeared.
</p><p>
Then suddenly every hole, far and near — and there
were hundreds of them — seemed to have its face, coming
and going, looking at him, all hard-eyed and evil and sharp.
</p><p>
He got away from the holes in the banks and went into
the wood.
</p><p>
Then the whistling began.
</p><p>
It was behind him, when first he heard it. Then it sounded far ahead ofhim. He stopped and wanted to go back. They
were up and ready, somebody! And he — he was alone, and
far from any help; and the night was coming.
</p><p>
Then he heard the pattering of little feet. Was it in front
or behind? It seemed coming nearer to him. As he stood
listening, a rabbit came running hard towards him through
the trees. ‘Get out of this, you fool, get out!’ the Mole heard
him say as he disappeared in a hole.
</p><p>
The whole wood seemed running now. In panic, the Mole
began to run too, he didn’t know where. He fell over things
and into things, he ran under things and round things. At last
he hid in the deep dark hole of an old tree. He was too tired
to run further, and could only hide into the dry leaves. And as
he lay there and listened to the whistlings and the patterings
outside, he knew it at last — the Terror of the Wild Wood!
</p><p>
At that time the Rat, warm and comfortable, was sleeping by his fireside. Then he woke, and looked round for the
Mole.
</p><p>
But the Mole was not there.
</p><p>
He listened for a time. The house seemed very quiet.
</p><p>
Then he called, ‘Moly!’ several times, and, having no answer, got up and went out into the hall.
</p><p>
There were neither Mole’s cap, nor his galoshes.
</p><p>
The Rat left the house, and examined the muddy ground
outside for the Mole’s tracks. There they were, leading direct to the Wild Wood.
</p><p>
The Rat stood in deep thought for a minute or two. Then
he entered the house, took pistols and a stick and went to
the Wild Wood.
</p><p>
It was already getting dark when he came into the wood,
looking for his friend. Here and there evil little faces looked
out of holes, but disappeared at sight of the brave animal,
his pistols, and the great ugly stick; and the whistling and
pattering stopped. He made his way through the wood, all
the time calling out, ‘Moly, Moly, Moly! Where are you? It’s
me — it’s old Rat!’
</p><p>
At last to his joy he heard a little answering cry. He came
to the foot of an old tree, with a hole in it, and from out of
the hole came a voice, saying ‘Ratty! Is that really you?’
</p><p>
The Rat found the Mole, tired and trembling. ‘O Rat!’ he
said, ‘I was so afraid, you can’t think!’
</p><p>
‘O, I understand,’ said the Rat. ‘Coming here was the
wrong thing to do, Mole. I did my best to keep you from
it. We river-bankers very seldom come here. If we must
come, we come in twos, at least. Besides, there are a hundred things one must know, which we understand all about
and you don’t. I mean secret words, which have power and
effect, and plants you have with you, and poems you repeat.’
</p><p>
‘The brave Mr. Toad can come here by himself, can’t he?’
asked the Mole.
</p><p>
‘Old Toad?’ laughed the Rat. ‘He can’t show his face here
alone, not for a lot of gold.’
</p><p>
The Mole began to feel better and more himself again.
</p><p>
‘Now then,’ said the Rat, ‘we must make a start for home.’
</p><p>
‘Dear Ratty,’ said the poor Mole, ‘I’m so sorry, but I’m so
tired. You MUST let me rest here a little longer.’
</p><p>
‘O, all right,’ said the kind Rat, ‘have some rest. It’s nearly
dark now, and there may be moon later.’
</p><p>
So the Mole got into the dry leaves and fell asleep, while the
Rat covered himself up, too, for warmth, and lay with a pistol
in his paw.
</p><p>
When at last the Mole woke up, much rested, the Rat said,
‘Now then! I’ll just take a look outside and see if everything’s
quiet, and then we must go.’
</p><p>
He went to the entrance and put his head out. Then the
Mole heard him saying quietly to himself, ‘Hullo! hullo!
here — is — a — go!’
</p><p>
‘What is it, Ratty?’ asked the Mole.
</p><p>
‘SNOW,’ replied the Rat brieﬂy; ‘It’s snowing hard.’
</p><p>
The Mole came and, looking out, saw that the wood
changed. Holes, pools, and other black objects were disappearing fast, and a sparkling carpet covered everything.
</p><p>
‘Well, well,’ said the Rat, after some thinking. ‘We must
make a start. The worst of it is, I don’t know where we are.
And now this snow makes everything look so different.’
</p><p>
They went out bravely, and kept walking for an hour or
two. After several falls they were wet through; the snow was
so deep that they could hardly draw their little legs through it.
Then they stopped, tired, and sat down on a fallen tree to rest
and to think what to do. There seemed to be no end to this
wood, and no beginning, and no difference in it, and, worst of
all, no way out.
</p><p>
‘We can’t sit here very long,’ said the Rat. ‘The cold is too
awful for anything, and the snow will soon be too deep for 
us to walk through.’ He looked about him. ‘There in front of
us,’ he went on, ‘the ground seems all hilly. We’ll try and find
some sort of a hole with a dry ﬂoor to it, out of the snow and
the wind, and there we’ll have a good rest before we try again.’
</p><p>
So once more they got on their feet, and went on, and
looked for some corner that was dry. Suddenly the Mole fell
forward on his face.
</p><p>
‘O my leg!’ he cried. ‘O my poor leg!’ and he sat up on the
snow and held his leg in both his front paws.
</p><p>
‘Poor old Mole!’ said the Rat kindly. ‘Let’s have a look at
the leg. Yes,’ he went on, ‘you cut your leg. Wait till I get at
my handkerchief, and I’ll bandage it for you.’
</p><p>
‘It was some stick, I believe’ said the Mole. ‘O, my! O, my!’
</p><p>
‘It doesn’t look like a stick,’ said the Rat. ‘Looks as if it
was something in metal. Funny!’ He examined the snow
around them.
</p><p>
‘Well, it doesn’t matter,’ said the Mole. ‘It’s painful just
the same.’
</p><p>
But the Rat, after carefully tying up the leg with his
handkerchief, left him and got busy scraping in the snow.
He scratched, all four legs working busily, while the Mole
waited, remarking at intervals, ‘O, COME on, Rat!’
</p><p>
Suddenly the Rat shouted ‘Hooray!’
</p><p>
‘What did you find, Ratty?’ asked the Mole. The Rat
showed it. The Mole had a good look.
</p><p>
‘Well,’ he said at last, slowly, ‘I SEE a door-scraper! Well,
what of it?’
</p><p>
‘But don’t you see what it MEANS, you — you silly animal?’ asked the Rat. ‘Here, stop talking and come and
scrape!’ And he worked again and made the snow ﬂy in all
directions around him.
</p><p>
The Mole scraped busily too. Some ten minutes’ hard
work, and the Rat found something and called the Mole to
come and help him. At last the result of their work stood in
front of them.
</p><p>
In the side ofa snow-bank was a little door, painted dark
green. An iron bell hung by the side, and below it they could
read MR. BADGER.
</p><p>
The Mole fell backwards on the snow from surprise
and joy. ‘Rat!’ he shouted, ‘you’re a wonder! A real wonder,
that’s what you are. I see it all now! From the moment that
I fell and cut my leg, and you looked at the cut, and at once
your wonderful mind said, ”Door-scraper!" And did you
stop there? No. Your intellect went on working!’
</p><p>
‘Are you going to sit on the snow all night and TALK?’ the
Rat stopped him, rather unkindly, ‘Get up at once and ring!’
</p><p>
While the Rat attacked the door with his stick, the Mole
rang the bell.
</p>

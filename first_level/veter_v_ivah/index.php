<h3><span>CHAPTER II</span> The Open Road</h3>
<div>


<p>
‘Ratty,’ said the Mole suddenly, one bright summer 
morning, ‘will you do something for me?’
The Rat was sitting on the river bank, singing a little song.
</p><p>
‘What I wanted to ask you,’ said the Mole, ‘won’t you take
me to call on Mr. Toad? I heard so much about him, and I
really want to meet him.’
</p><p>
‘Why, certainly,’ said the Rat, jumping to his feet. ‘Get the
boat out, and we’ll go there at once. It’s never the wrong 
time to call on Toad. He’s always glad to see you, always
sorry when you go!’
</p><p>
‘He must be a very nice animal,’ said the Mole, as he got
into the boat and took the oars, while the Rat sat comfortably in the stern.
</p><p>
‘He is indeed the best ofanimals,’ replied Rat. ‘So simple,
so kind. Perhaps he’s not very clever — but he has got some
great qualities.’
</p><p>
They came to a beautiful old house, with nice green
lawns.
</p><p>
‘There’s Toad Hall,’ said the Rat; ‘and that small river on
the left leads to his boat-house, where we’ll leave the boat.
That’s the banqueting-hall you’re looking at now — very
old, that is. Toad is rich, and this is really one of the nicest
houses in these parts.’
</p><p>
In the large boat-house they saw many beautiful boats,
but none in the water.
</p><p>
The Rat looked around him. ‘I understand,’ said he. ‘He’s
tired of boating. Iwonder what new hobby he has now?
Come along and we will hear all about it.’
</p><p>
They found the Toad, who was resting in a garden-chair,
with a large map on his knees.
</p><p>
‘Hooray!’ he cried, jumping up when he saw them, ‘this
is wonderful!’ He shook the paws of both of them warmly. ‘How KIND of you!’ he went on, dancing round them. ‘I
was just going to send a boat down the river for you, Ratty.
Iwant you badly — both of you. Now what will you take?
Come inside and have something!’
</p><p>
‘Let’s sit a little, Toady!’ said the Rat, while the Mole
spoke about Toad’s ‘wonderful residence.’
</p><p>
‘Finest house on the whole river,’ replied Toad. ‘Now,
look here. You are the very animals I wanted. You must help
me. It’s most important!’
</p><p>
‘It’s about your rowing, I think,’ said the Rat.
</p><p>
‘O, boating!’ said the Toad. ‘Silly hobby. Igave that up
LONG ago. No, I found the real thing, the only interest for
a life time. Come with me, dear Ratty, and your nice friend
also, and you will see!’
</p><p>
He led them to the yard, and there they saw a cart, shining, yellow with green, and red wheels.
</p><p>
‘There you are!’ said the Toad. ‘There’s real life for you, in
that little cart. The open road, camps, villages, towns, cities!
Here today, up and off to somewhere else tomorrow! Travel,
change, interest, joy! The whole world before you! And this
is the finest cart of its sort! Come inside and look!’
</p><p>
The Mole came into the cart with great interest. The Rat
stayed where he was.
</p><p>
It was indeed very comfortable. Little sleeping beds —
a little table — a cooking-stove, bookshelves, and pots, and
kettles of every size.
</p><p>
‘All ready!’ said the Toad triumphantly. ‘You see — biscuits, sardines — everything you can want. Soda-water
here — letter-paper, bacon, jam, cards and dominoes —
you’ll find,’ he continued, as they came out again, ‘you’ll find
everything, when we start this afternoon.’
</p><p>
‘Excuse me,’ said the Rat slowly, ‘but did I hear you say
something about “WE,” and “START,” and “THIS AFTER-
NOON?”
</p><p>
‘Now, you dear good old Ratty,’ said Toad, ‘don’t talk in
that stern way, because you know you MUST come. I can’t
go without you, so please don’t argue — it’s the one thing 
I can’t stand. You don’t want to stay on the old river all your
life, and live in a hole, and BOAT? Iwant to show you the
world! I’m going to make an ANIMAL ofyou, my boy!’
</p><p>
‘I’m not coming,’ said the Rat. ‘AndI AM going to live
on my old river, AND stay in a hole, AND boat. And what’s
more, Mole’s going to do as I do, aren’t you, Mole?’
</p><p>
‘Of course I am,’ said the Mole. ‘I’ll always be with you,
Rat, and what you say is true. All the same, it may be — well,
fun, you know!’ Poor Mole! The Adventure was such a new
thing to him, and he fell in love with the canary-colored cart
and all its little things.
</p><p>
The Rat saw what was going in his head. He was fond of
the Mole, and wanted to do almost anything for him. Toad
was watching them.
</p><p>
‘Come in, and have some lunch,’ he said, diplomatically,
‘let’s not decide anything in a hurry.’
</p><p>
During lunch the Toad painted the prospects of the trip
and the joys of the open life and the roadside in such bright
colours that the Mole couldn’t sit in his chair. The Rat could
not disappoint his two friends, who were already planning
for weeks ahead.
</p><p>
When they were ready, the Toad let his companions
catch the old grey horse. They set off, all talking at once.
It was a golden afternoon. The birds called to them; and
rabbits, sitting at their front doors, held up their paws, and
said, ‘O my! O my! O my!’
</p><p>
Late in the evening, tired and happy and miles from home,
they stopped and ate their simple supper sitting on the grass
by the side of the cart. Toad talked about all he was going
to do in the future, while stars grew larger all around them,
and a yellow moon came to keep them company and listen 
to their talk. At last they went to their little beds in the cart;
and Toad sleepily said, ‘Well, good night, my friends! This is
the real life for a gentleman! Talk about your old river!’
</p><p>
‘I DON’T talk about my river,’ replied the Rat. ‘But I
THINK about it, all the time!’
</p><p>
The Mole took the Rat’s paw in the darkness. ‘I’ll do what
you like, Ratty,’ he said quietly. ‘Will we run away tomorrow
morning, quite early — VERY early — and go back to our
dear old hole on the river?’
</p><p>
‘No, no, ‘answered the Rat. ‘Thanks, but I must be with
Toad till this trip ends. It won’t be safe for him to stay alone.
His hobbies never take long. Good night!’
</p><p>
The end was indeed nearer than even the Rat thought.
</p><p>
After so much open air and excitement the Toad slept
late, and the two friends couldn’t get him out of bed next
morning. So the Mole and Rat took care of the horse, and
made a fire, and cleaned last night’s cups and plates, and
made breakfast. The hard work ended by the time Toad got
up, fresh and happy, saying what a pleasant easy life it was
now, after the difficulties of housekeeping at home.
</p><p>
They had a pleasant trip that day over grassy meadows,
and camped as before, only this time the two friends took
care that Toad did his part of work. Their way lay, as before,
across country, and in the afternoon they came out on the
big road, and there catastrophe happened.
</p><p>
They were walking along the road, the Mole by the horse’s
head, talking to him, the Toad and the Water Rat walking behind the cart talking together, when far behind them they
heard a sound, like a faraway bee. Looking back, they saw
a small cloud of dust, coming after them at great speed.
They continued talking, when in a second the peaceful scene 
changed, and with wind and sound that made them jump to
the nearest ditch, It was on them! The ‘Poop-poop’ rang in
their ears. For a moment they saw a rich interior, and the
wonderful motor-car, great, beautiful, with its driver holding
the wheel, threw a cloud of dust, and then disappeared in the
far distance.
</p><p>
The old grey horse in a new situation let out his natural
emotions. He was jumping and tearing and he drove the
cart backwards to the deep ditch at the side of the road.
There was a loud crash — and the canary-colored cart, their
pride and their joy, fell on its side in the ditch.
</p><p>
The Rat danced up and down in the road. ‘You bandits!’
he shouted, ‘I’ll report you! I’ll take you to the police!’ He
was saying all the things he said to masters of boats when
they drove too near his house.
</p><p>
Toad sat down in the middle of the dusty road, and was
looking in the direction of the disappearing motor-car. His
face was peaceful, and at intervals he repeated, ‘Poop-poop!’
</p><p>
The Mole was busy with the horse. Then he went to look
at the cart, on its side in the ditch. The damage was great.
</p><p>
The Rat came to help him, but they couldn’t right the
cart. ‘Hi! Toad!’ they called. ‘Come and help!’
</p><p>
The Toad didn’t answer, so they went to see what the matter with him was. They found him in a sort of a trance, a happy
smile on his face. At intervals he was still saying, ‘Poop-poop!’
</p><p>
‘Are you coming to help us, Toad?’ the Rat asked sternly.
</p><p>
‘The poetry of moving!’ said Toad. ‘The REAL way to
travel! The ONLY way to travel! Here today — there tomorrow! Villages, towns and cities jumped — always somebody
else’s horizon! O poop-poop! O my! O my!’
</p><p>
‘O STOP it, Toad!’ shouted the Mole.
</p><p>
‘And to think I never KNEW!’ went on the Toad in a dreamy
monotone. ‘All those years, I never knew, never even DREAMT!
But NOW — but now that I know, I fully understand! O what
a ﬂowery road is before me! What dust-clouds will jump up
behind me as I hurry on my way! What carts I will throw into
the ditch! Horrible little carts — canary-coloured carts!’
</p><p>
‘What will we do with him?’ asked the Mole of the Water Rat.
</p><p>
‘Nothing at all,’ replied the Rat firmly ‘You see, I know him
long enough. He has got a new interest, and it always takes
him that way, in the beginning. He’ll continue like that for days
now, like an animal walking in a happy dream. Let’s go and see
what we can do about the cart.’
</p><p>
A careful inspection showed them that the cart could not
travel longer. The Rat led the horse. ‘Come on!’ he said to the
Mole. ‘It’s five or six miles to the nearest town, and we must
walk it. The sooner we make a start the better.’
</p><p>
‘But what about Toad?’ asked the Mole. ‘We can’t leave him
here, sitting in the middle of the road by himself! It’s not safe.’
</p><p>
‘O, BOTHER Toad,’ said the Rat; ‘I’m tired of him!’
</p><p>
They didn’t go very far on their way, when there was a pattering of feet behind them, and Toad caught them up and took
them by paws.
</p><p>
‘Now, look here, Toad!’ said the Rat sternly: ‘as soon as we
get to the town, you must go to the police-station, and see if
they know anything about that car, and report against it. And
then you must take care of the cart. It needs repairing. The
Mole and I will find comfortable rooms where we can stay till
the cart’s ready, and till you’re all right again after the shock.’
</p><p>
‘Police-station! Report!’ said Toad dreamily ‘Me REPORT
on that beautiful sight! Repair the CART! I had enough of carts.
I never want to see the cart, or to hear of it, again!’
‘Do you see what it is?’ the Rat said to the Mole, addressing
him across Toad’s head: ‘I give it up — when we get to the
town we’ll go to the railway station, and with luck we may get
on a train there that’ll get us back to river bank tonight. And I
will never go with this animal again!’
</p><p>
In the town they went to the station, and a slow train landed them at a station not very far from Toad Hall. They led the
sleep-walking Toad to his door and put him inside it. Then
they got out their boat from the boat-house, rowed down the
river home, and at a very late hour sat down to supper in their
own riverside home, to the Rat’s great joy.
</p><p>
The following evening the Mole was sitting on the bank
fishing, when the Rat came running along to find him. ‘Heard
the news?’ he said. ‘Everybody is talking about it, all along the
river bank. Toad went up to Town by an early train this morning. And he ordered a large and very expensive car.’
</p>

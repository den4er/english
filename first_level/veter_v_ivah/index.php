<h3><span>CHAPTER X</span> The Further Adventures of Toad</h3>
<div>

<p>
The Toad woke up early. Sitting up, he rubbed his eyes 
first and his cold toes next; then he remembered
everything — his escape, his ﬂight, his guards; remembered, the first and best thing of all, that he was free!
</p><p>
Free! He was warm from end to end as he thought ofthe
happy world outside, waiting for him to make his triumphal
entrance. He shook himself and marched into the comfortable morning sun, cold but sure, hungry but hopeful. The
practical Toad was looking for something that could talk,
and tell him clearly which way to go.
</p><p>
The road ran side by side with a canal. ‘Well!’ said Toad to
himself. ‘One thing’s clear. They must both be coming FROM
somewhere, and going TO somewhere.’ So he marched by
the water’s edge.
</p><p>
Round a bend in the canal came a horse. There was a
long line attached to its collar, the further part of it 
throwing drops ofwater. Toad let the horse pass, and stood waiting.
</p><p>
With a pleasant sound of quiet water there appeared a
barge. On the barge there was a big stout woman wearing
a big sun-hat.
</p><p>
‘A nice morning, ma’am!’ she said to Toad.
</p><p>
‘It is, ma’am!’ answered Toad politely. ‘It IS a nice morning for those who are not in trouble. ButI am. My married 
daughter sent me a letter, she wanted me to come, so I set
off, not knowing what was wrong, but fearing the worst, as
you will understand, ma’am, ifyou’re a mother, too. I left my
business to look after itself — I’m in the washing line, you
see — and I left my young children to look after themselves,
and Ilost all my money, and lost my way, and I don’t like
to think what may be happening to my married daughter,
ma’am!’
</p><p>
‘Where does your married daughter live, ma’am?’ asked
the barge-woman.
</p><p>
‘She lives near the river, ma’am,’ said Toad. ‘Close to Toad
Hall. That’s a fine house somewhere in these parts.’
</p><p>
‘Toad Hall? Why, I’m going that way myself,’ answered
the barge-woman. ‘This canal joins the river a little above
Toad Hall; and then it’s an easy walk. I’ll give you a lift.’
</p><p>
She led the barge close to the bank, and Toad, with many
thanks, stepped lightly on board and sat down. ‘Toad’s luck
again!’ thought he.
</p><p>
‘So you’re in the washing line, ma’am?’ said the
barge-woman politely, as they went along. ‘It’s a very good
business. But surely you don’t DO all that work yourself,
ma’am?’
</p><p>
‘O, I have girls,’ said Toad lightly: ‘twenty girls, always at
work. But you know what GIRLS are, ma’am!’
</p><p>
‘So do I,’ said the barge-woman. ‘And are you very fond
of washing?’
</p><p>
‘I love it,’ said Toad. ‘A real pleasure, ma’am!’
</p><p>
‘What a bit of luck, meeting you!’ said the barge-woman,
thoughtfully.
</p><p>
‘What do you mean?’ asked Toad, nervously.
</p><p>
‘Well, look at me, now,’ replied the barge-woman. ‘I like
washing, too, just the same as you do; and do all my own 
washing. But my husband often leaves his work and the
barge to me. Now, instead of steering or looking after the
horse, he is hunting. He went off with the dog to get a rabbit for dinner. And how am I to do my washing?’
</p><p>
‘O, never mind about the washing,’ said Toad, not liking
the subject. ‘Try and fix your mind on that rabbit. A nice fat
young rabbit, I’m sure. Do you have any onions?’
</p><p>
‘I can’t fix my mind on anything but my washing,’ said
the barge-woman. ‘Why do you talk about rabbits, with
such a happy prospect before you? There are a lot of my
things in a corner of the cabin. Could you take one or
two ofthe most necessary sort and put them through the
wash-tub as we go along? It’ll be a pleasure to you, as you
rightly say, and a real help to me. You’ll find a tub, and
soap, and a kettle on the stove, and a bucket to take water from the canal with. Then I will know you’re enjoying
yourself, instead of sitting here, looking at the scenery.’
</p><p>
‘Let me steer!’ said Toad in panic, ‘and then you can
do your washing your own way. I might spoil your things,
or not do them as you like. I’m more used to gentlemen’s
things myself. It’s my special line.’
</p><p>
‘Let you steer?’ said the barge-woman, laughing. ‘It
takes some practice to steer a barge properly. Besides, it’s
dull work, and I want you to be happy. No, you will do the
washing you are so fond of, and I’ll do the steering that I un-
derstand.’
</p><p>
Toad looked for escape this way and that, saw that he
was too far from the bank for a ﬂying jump, and gave up. ‘I
think any fool can WASH!’ he thought.
</p><p>
He took the tub, soap and water, chose a few things, and
started.
</p><p>
A long half-hour passed. Toad was getting angrier and
angrier. Nothing that he could do to the things seemed to
make them cleaner. Once or twice he looked nervously over
his shoulder at the barge-woman, but she was looking out
in front of her, busy in her steering. He was tired, and he
saw that his paws were beginning to look bad. He lost the
soap, for the fiftieth time.
</p><p>
A burst of laughter made him look round. The bargewoman was laughing, till the tears ran down her cheeks.
</p><p>
‘What fun!’ she said. ‘I thought you must be somebody
else all along, from the way you talked. Pretty washerwoman you are! Never washed in your life, I think!’
</p><p>
Toad lost all control of himself.
</p><p>
‘You ugly, low, FAT barge-woman!’ he cried; ‘how dare
you talk like that! Washerwoman indeed! Iam a Toad, a
very well-known, rich Toad! I will NOT let you laugh at me!’
</p><p>
The woman moved nearer to him and looked under his
hat closely. ‘Why, you’re a horrid, bad, ugly Toad!’ she cried.
‘And in my nice clean barge, too! That is a thing that I will
NOT have.’
</p><p>
One big arm shot out and caught Toad. Then the world
turned suddenly upside down, the barge seemed to ﬂy
lightly across the sky, and with the wind in his ears, Toad
found himself ﬂying through the air.
</p><p>
The water, when he eventually reached it with a loud
splash, was really cold. He rose to the surface, and the first
thing he saw was the fat barge-woman looking back at him
from the leaving barge and laughing.
</p><p>
He swam out for the shore, but the cotton dress made it
hard, and when he finally touched land he found it hard to
climb up the high bank. He took a short rest; then, gathering
his wet skirts well over his arms, he started to run after the 
barge as fast as his legs could carry him, angry, ready for
revenge.
</p><p>
The barge-woman was still laughing when he caught
up with her. Great revenge was what he wanted. Running
quickly on he took the horse, untied the rope, jumped lightly on the horse’s back, and galloped it for the open country.
Once he looked back, and saw the barge aground on the
other side of the canal, and the barge-woman gesticulating
wildly and crying, ‘Stop, stop, stop!’
</p><p>
The barge-horse was not strong, and its gallop soon
turned into an easy walk; but Toad was quite happy with
this, knowing that he was moving, and the barge was not.
</p><p>
He travelled some miles, his horse and he, and he
was feeling sleepy in the hot sunshine, when the horse
stopped, lowered his head, and began to eat the grass;
and Toad, waking up, just saved himself from falling off.
He looked about him and found near him a dirty gip-
sy caravan, and beside it a man was sitting on a bucket
turned upside down. A fire of sticks was burning nearby,
and over the fire hung an iron pot, and out of that pot
came smells — warm, rich smells. And Toad was real-
ly hungry. He looked the gipsy over carefully. So there
he sat, and looked at the gipsy; and the gipsy sat and
smoked, and looked at him.
</p><p>
The gipsy took his pipe out of his mouth and said, ‘Do
you want to sell that horse?’
</p><p>
The gipsy’s words seemed to show the way towards the
two things Toad wanted so badly — money and breakfast.
</p><p>
‘What?’ he said, ‘to sell this beautiful young horse? O,
no; it’s out ofthe question. Who’s going to take the washing
home to my customers every week? Besides, I’m too fond of 
him, and he simply loves me. All the same, how much might
you give me for this beautiful young horse?’
</p><p>
The gipsy looked the horse over, and then he looked Toad
over with equal care, and looked at the horse again. ‘Shilling
a leg,’ he said shortly, and turned away, continuing to smoke.
</p><p>
‘A shilling a leg?’ cried Toad. ‘I must take a little time to
work that out, and see just what it comes to.’
</p><p>
He climbed down off his horse, and sat down by the gipsy, and did sums on his fingers, and at last he said, ‘A shilling
a leg? That comes to exactly four shillings, and no more. O,
no; I could not think of getting four shillings for this beautiful young horse.’
</p><p>
‘Well,’ said the gipsy, ‘I’ll make it five shillings, and that’s
my last word.’
</p><p>
Then Toad sat and thought long and deeply. He was hungry
and quite penniless, and still some way — he did not know
how far — from home, and enemies might still be looking for
him. To one in such a situation, five shillings may very well
seem a large sum of money. But it did not seem very much to
get for a horse. At last he said firmly, ‘Look here, gipsy! I tell you
what we will do; and this is MY last word. You will give me six
shillings and sixpence; and you will give me as much breakfast
as I can eat, out of that iron pot that is sending such tasty and
exciting smells. In return, I will give you my beautiful young
horse. Or else I know a man who really wants to buy him.’
</p><p>
It was a long discussion. But in the end the gipsy gave him
six shillings and sixpence. Then he disappeared into the caravan for a moment, and returned with a large iron plate and
a knife, fork, and spoon. He put hot rich stew into the plate.
Toad took the plate on his lap, almost crying, and ate, and ate,
and ate. He thought it was the best breakfast in his life.
</p><p>
Then Toad got up and said good-bye to the gipsy and to
the horse; and the gipsy, who knew the riverside well, gave
him directions which way to go, and he happily set off again.
The sun was shining brightly, his wet clothes were quite
dry again, he had money in his pocket once more, he was
nearing home and friends and safety, and, most and best of
all, he was quite full, and strong, and careless.
</p><p>
‘Ho, ho!’ he said to himself as he marched along with
his chin in the air, ‘what a clever Toad I am! There is surely
no animal equal to me for cleverness in the whole world!
My enemies shut me up in prison, watched night and day;
Iwalk out through them all, by courage. They follow me
with engines, and policemen, and revolvers; I disappear,
laughing. I meet a woman who throws me into a canal. What
of it? I swim ashore, I take her horse, I ride off in triumph,
and I sell the horse for a whole pocketful of money and an
excellent breakfast! Ho, ho! I am The Toad, the handsome,
the popular, the successful Toad!’ He made up a song about
himself, and sang it at the top of his voice.
</p><p>
After some miles of country fields he reached the high
road, and as he turned into it and looked along its white
length, he saw coming to him a dot that turned into something he knew so well.
</p><p>
‘Great!’ said the happy Toad. ‘I will stop them, my brothers of the wheel, and they will give me a lift, of course, and
it may even end in my driving up to Toad Hall in a car!’
</p><p>
He stepped out into the road to stop the car. Suddenly he
became very pale, his heart turned to water, his knees shook
under him, and he fell down. ‘It is the car I took out of the
yard of the Red Lion Hotel on that fatal day!’ he thought. ‘And
the people in it are the very same people! It’s all over now!
</p><p>
Chains and policemen again! Prison again! Dry bread and
water again! O, what a fool I was! Why did I want to go about
the country, singing songs, and stopping people in broad day
on the high road, instead of hiding till nightfall and slipping
home quietly by back ways! O unhappy Toad! O poor animal!’
</p><p>
The terrible car drew slowly nearer and nearer, till at last
he heard it stop near him. Two gentlemen got out and walked
round him, and one of them said, ‘O dear! Here is a poor old
washerwoman who fell in the road! Let us lift her into the car
and take her to the nearest village, where surely she has friends.’
</p><p>
They carefully lifted Toad into the car and continued
their way.
</p><p>
When Toad heard them talk in so kind a way, and knew
that they did not remember him, he opened first one eye
and then the other.
</p><p>
‘Look!’ said one of the gentlemen, ‘the fresh air is doing
her good. How do you feel now, ma’am?’
</p><p>
‘Thank you kindly, Sir,’ said Toad in a weak voice, ‘I’m
feeling much better!’
</p><p>
‘That’s right,’ said the gentleman. ‘Now keep quite still,
and, above all, don’t try to talk.’
</p><p>
‘I won’t,’ said Toad. ‘I was only thinking, fresh air full in
my face could do me good. Can I sit on the front seat there,
beside the driver?’
</p><p>
‘What a very sensible woman!’ said the gentleman. ‘Of
course you can.’ So they carefully helped Toad into the front
seat beside the driver.
</p><p>
Toad was quite happy again.
</p><p>
‘Please, Sir,’ he said to the driver, ‘could you let me try
and drive the car for a while? I was watching you carefully,
and it looks so easy and so interesting!’
</p><p>
The driver laughed so loudly that the gentleman asked,
‘What’s the matter?’ When he heard, he said, ‘Bravo, ma’am!
Let her have a try, and look after her. She won’t do any harm.’
</p><p>
Toad quickly got into the driver’s seat, took the steering-wheel, listened to the instructions, and set the car in
motion, but very slowly and carefully at first, for he decided
to be sensible.
</p><p>
The gentlemen behind clapped their hands and ap-
plauded, and Toad heard them saying, ‘How well she does
it, for the first time!’
</p><p>
Toad went a little faster; then faster still, and faster.
</p><p>
He heard the gentlemen call out, ‘Be careful, washerwoman!’ And this made him angry, and he began to lose
his head.
</p><p>
The driver tried to stop him, but he blocked him with
one elbow, and put on full speed. The rush of air in his
face, the sound of the engines, and the light jump of the
car beneath him poisoned his weak brain. ‘Washerwoman,
indeed!’ he shouted. ‘Ho! ho! I am the Toad, the car stealer,
the prison-breaker, the Toad who always escapes! Sit still,
and you will know what driving really is, for you are in the
hands of the bravest Toad!’
</p><p>
With a cry of horror the whole party rose and threw
themselves on him. ‘Hold him!’ they cried, ‘hold the Toad,
the wicked animal who stole our car! Bind him, chain him,
drag him to the nearest police-station!’
</p><p>
With a half-turn of the wheel the Toad sent the car crashing through the low hedge that ran along the roadside. One
mighty jump, and the wheels of the car were mixing up the
thick mud of a horse-pond.
</p><p>
Toad found himself ﬂying through the air. Then he land-
ed on his back in the soft rich grass of a meadow. Sitting up, 
he could just see the car in the pond; the gentlemen and the
driver were swimming helplessly in the water.
</p><p>
He got up fast, and set off running across country as
hard as he could, till he was breathless and tired, and settled
down into an easy walk. When he could think calmly, he began to laugh, and he laughed till he sat down under a hedge.
‘Ho, ho!’ he cried happily, ‘Toad again! Who stopped them?
Who managed to get on the front seat? Who made them see
him driving? Who landed them all in a horse-pond? Who
escaped, ﬂying through the air, leaving them in the mud?
Why, Toad, of course; clever Toad, great Toad, GOOD Toad!’
</p><p>
Then he burst into song again, — ‘O, how clever I am!
How clever, how clever, how very clev—’
</p><p>
A noise at a distance behind him made him turn his head
and look. O horror! About two fields off, a driver and two
large policemen were running towards him as hard as they
could go!
</p><p>
Poor Toad jumped to his feet and hurried away again,
his heart in his mouth. O, my!’ he said, ‘what a FOOL I am!
Shouting and singing songs again! O my! O my! O my!’
</p><p>
He looked back, and saw to his horror that they were
getting nearer. He was doing his best, but he was a fat
animal, and his legs were short. He could hear them close
behind him now. He ran on wildly, looking back over his
shoulder at the now triumphant enemy, when suddenly the
earth turned under his feet, and, splash! he found himself in
deep water, quick water, water that was carrying him along;
and he knew that in his panic he ran straight into the river!
</p><p>
He tried to catch hold of the plants that grew along the
water’s edge, but the river was so strong that it tore them
out of his hands. Soon he saw that he was approaching a
big dark hole in the bank, just above his head, and as the 
river was carrying him past he reached up with one paw
and caught hold of the edge and held on. Then slowly and
with difficulty he drew himself up out of the water, till at
last he could rest his elbows on the edge of the hole. There
he stayed for some minutes, for he was quite tired.
</p><p>
As he looked before him into the dark hole, some bright
small thing shone, moving towards him. As it came nearer,
a face grew up around it, and it was a well-known face!
</p><p>
Brown and small.
</p><p>
Serious and round, with neat ears and silky hair.
</p><p>
It was the Water Rat!
</p>

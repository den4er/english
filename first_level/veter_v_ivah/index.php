<h3><span>CHAPTER VIII</span> Toad’s Adventures</h3>
<div>



<p>
When Toad found himself in a dark prison, he fell on
Wthe ﬂoor, and cried. ‘This is the end of everything’ 
(he said), ‘or it is the end of the career of Toad,
which is the same thing; the popular and handsome Toad, the
rich Toad, the Toad so free! Now I must stay in this prison, till
people who were proud to say they knew me forget my name!
</p><p>
O clever old Badger!’ (he said), ‘O clever, intelligent Rat and
Mole! O unhappy Toad!’ With such cries he spent his days and
nights for several weeks, refusing his meals.
</p><p>
The prison guard had a daughter, a pleasant and kind
girl, who assisted her father. She was very fond of animals,
and, besides her canary, she had several mice and a squirrel. This kind girl, pitying Toad, said to her father one day,
‘Father! I can’t see that poor animal so unhappy, and getting
so thin! Let me take care of him. I’ll make him eat from my
hand, and sit up, and do all sorts of things.’
</p><p>
Her father agreed. He was tired of Toad and his cries. So
that day she knocked at the Toad’s door.
</p><p>
‘Now, cheer up, Toad,’ she said, on entering, ‘and sit up and
dry your eyes. Here’s some food for you, right from the stove!’
</p><p>
The smell of cabbage reached the nose ofToad, and gave
him the idea for a moment that perhaps life was not such
a bad thing after all. But still he cried, and kicked with his
legs, and refused any comfort. So the clever girl left for the
time, but, of course, the smell of hot cabbage stayed, and
Toad, between his cries, began to think new thoughts: of
poetry; of broad meadows, and sun and wind; and of the
comforting sound of dishes on the table at Toad Hall, and
the scrape ofchair-legs on the floor as everyone pulled himself close up to the table. He began to think of his friends,
and how they could do something; and lastly, he thought of
his own great cleverness, and all that he could do with it;
and the cure was almost complete.
</p><p>
When the girl returned, some hours later, she carried
a tray, with a cup of tea; and a plate with very hot toast,
cut thick, very brown on both sides, with butter running
through the holes in it in great golden drops, like honey. The 
smell of that toast simply talked to Toad; talked of warm
kitchens, of breakfasts on bright frosty mornings, of cozy
room firesides on winter evenings. Toad sat up, dried his
eyes, drank his tea and ate his toast, and soon began talking
freely about himself, and his house, and his doings there,
and how important he was.
</p><p>
The guard’s daughter saw that the topic was doing him
as much good as the tea.
</p><p>
‘Tell me about Toad Hall,’ she said. ‘It sounds beautiful.
But first wait till I bring you some more tea and toast.’
</p><p>
She returned with a fresh tray; and Toad, eating the
toast, told her about the boat-house, and the fish-pond,
and the hen-house, and the wash-house; and about the
banqueting-hall, and all the fun, and his friends. Then she
wanted to know about his animal-friends. Of course, she
did not say she was fond of animals as PETS. When she said
good night, Toad was quite cheerful once again. He sang a
little song or two, and had an excellent night’s rest and the
pleasantest of dreams.
</p><p>
They had many interesting talks together, after that, as
the days went on; and the guard’s daughter grew very sorry
for Toad.
</p><p>
‘Toad,’ she said one morning, ‘just listen, please. Ihave
an aunt who is a washerwoman.’
</p><p>
‘Never mind,’ said Toad, ‘don’t think about it. I have several aunts who MUST be washerwomen.’
</p><p>
‘Be quiet a minute, Toad,’ said the girl. ‘You talk too much,
and I’m trying to think. I have an aunt who is a washerwoman; she does the washing for all the prisoners here. She takes
out the washing on Monday morning, and brings it in on Friday evening. This is a Thursday. You’re very rich — at least 
you’re always telling me so — and she’s very poor. A few
pounds won’t make any difference to you, and it will mean a
lot to her. Maybe she will let you have her dress and hat and
so on, and you could escape from the prison as the official
washerwoman.’
</p><p>
‘You don’t mean Mr. Toad of Toad Hall will have to go
about the country dressed as a washerwoman!’
</p><p>
‘Then you can stop here as a Toad,’ said the girl hotly.
</p><p>
Honest Toad was always ready to see his mistakes. ‘You
are a good, kind, clever girl,’ he said, ‘and I am a proud and
stupid toad. Will you introduce me to your aunt, please? No
doubt the excellent lady and I will organize everything.’
</p><p>
The next evening the girl took her aunt to Toad. In return
for his money, Toad got a cotton dress, an apron, a shawl,
and an ugly black hat. Then they bound and gagged the old
lady to make it look like an attack.
</p><p>
‘Now it’s your turn, Toad,’ said the girl. ‘Take off your
coat and waistcoat and change.’
</p><p>
Shaking with laughter, she helped him to change.
</p><p>
‘You’re the portrait of her,’ she laughed, ‘Now, good-bye,
Toad, and good luck. Go straight down the way you came up;
and remember you’re a woman, quite alone in the world.’
</p><p>
With a shaking heart but firm footstep Toad went forward. It seemed hours before he crossed the last yard,
heard the key in the great outer door click behind him, felt
the fresh air of the outer world upon his face, and knew that
he was free!
</p><p>
He walked quickly towards the lights of the town, not
knowing what to do next. As he walked along, he saw some
red and green lights a little way off, and heard the sound of
the puffing of engines. ‘Aha!’ he thought, ‘A railway station 
is the thing I want most at this moment; and what’s more,
I needn’t go through the town to get it.’
</p><p>
He made his way to the station, looked at a timetable,
and found that a train in the direction of his home was going to start in half-an-hour.
</p><p>
He was going to buy a ticket to the station nearest to
Toad Hall. But as he reached for his waistcoat pocket where
he kept his money he felt only the cotton dress. He had no
money!
</p><p>
He said to the clerk, ‘Look here! I left my money behind.
Just give me that ticket, and I’ll send the money tomorrow.’
</p><p>
‘Stand away from the window, please, madam; you’re
disturbing the other passengers!’ the clerk answered.
</p><p>
Sad and full of anger, he went down the platform where
the train was standing, and tears fell down each side of his
nose. Very soon they will discover his escape, the hunt will
start, they will catch him, and put him back again to prison
and bread-and-water; and O, what sarcastic comments the
girl will make! What must he do?
</p><p>
As he thought, he found himself near the engine and its
driver.
</p><p>
‘Hullo, mother!’ said the engine-driver, ‘what’s the trouble? You don’t look cheerful.’
</p><p>
‘O, sir!’ said Toad, crying again, ‘I am a poor unhappy
washerwoman, and Ilost all my money, and can’t pay for
a ticket, and I must get home tonight somehow, and I don’t
know what to do.’
</p><p>
‘Well, I’ll tell you what I’ll do,’ said the good engine-driver. ‘You’re a washerwoman. And I’m an engine-driver, as you
well may see, and it’s terribly dirty work. Will you wash a
few shirts for me when you get home, and send them along?
</p><p>
And I’ll give you a ride on my engine. It’s against the Company’s rules, but we’re not so very strict in these here.’
</p><p>
The Toad quickly got up into the cab of the engine. Of
course, he never washed a shirt in his life, and wasn’t going
to begin; but he thought: ‘When I get safely home to Toad
Hall, and have money again, and pockets to put it in, Iwill
send the engine-driver enough to pay for a lot of washing.’
</p><p>
The train moved out of the station. The Toad could see
on either side of him real fields, and trees, and hedges,
and cows, and horses, and as he thought how every min-
ute was bringing him nearer to Toad Hall, and his friends,
and money in his pocket, and a soft bed to sleep in, and
good things to eat, he began to jump up and down and sing
songs, to the great surprise of the engine-driver.
</p><p>
They covered many and many miles, and Toad was al-
ready thinking what to have for supper at home, when he
saw that the engine-driver, with a surprise on his face, was
looking over the side of the engine and listening hard. Then
he said to Toad: ‘It’s very strange; we’re the last train running
in this direction tonight, yet I hear another following us!’
</p><p>
Toad stopped jumping at once. By this time the moon
was shining brightly, and the engine-driver could see the
line behind them for a long distance.
</p><p>
He said, ‘I can see it clearly now! It is an engine, coming
along very quickly! It looks as if they follow us. And the engine
is full of the strangest men like guards, policemen in their
hats, and detectives, showing revolvers and walking-sticks;
and all crying the same thing — ”Stop, stop, stop!"’
</p><p>
Then Toad fell on his knees, raising his paws, and cried,
‘Save me, only save me, dear kind Mr. Engine-driver, and
I will tell you everything! I am not the simple washerwoman
 I seem to be! I am the well-known and popular Mr. Toad.
They want to catch me and put me back to prison!’
</p><p>
The engine-driver looked down upon him, and said,
‘Now tell the truth; what was your crime?’
</p><p>
‘I only took a car while the owners were at lunch; they
had no need of it at the time. I didn’t mean to steal it, really;
but people — especially judges — give such severe punish-
ments for thoughtless and emotional actions.’
</p><p>
The engine-driver looked very serious and said, ‘I think
that indeed you were a bad toad. But you are in great trouble and distress, so I will not leave you. I don’t like cars, and
I don’t like it when policemen tell me what to do on my own
engine. And an animal in tears always makes me feel pity. So
cheer up, Toad! I’ll do my best, and we may beat them yet!’
</p><p>
They threw on more coals, the engine ﬂew, but still the
policemen were close behind. The engine-driver said, ‘It’s
no good, Toad. They have the better engine. Now, a short
way ahead is a long tunnel, and on the other side is a thick
wood. I will go the quickest while we are running through
the tunnel. When we are on the other side, I will stop for a
second, and you jump and hide in the wood, before they get
through the tunnel and see you. Then I will go quick ahead
again, and they can follow me as far as they like. Be ready
to jump when I tell you!’
</p><p>
And so they did.
</p><p>
Toad saw his train disappear, and then the following
one. Then he had a great laugh.
</p><p>
But he soon stopped laughing when he saw that it was
very late and dark and cold, and he was in an unknown
wood, with no money and no chance of supper, and still far
from his friends and home; and it was something ofa shock.
</p><p>
He got into the wood, with the idea of leaving the railway as
far as possible behind him. At last, cold, hungry, and tired,
he found an empty tree, made himself a comfortable bed of
dead leaves, and slept peacefully till the morning.
</p>

</p><p>
The Clerk scratched his nose with his pen. ‘You may say
twelve months for the stealing, which is mild; and three
years for the dangerous driving, which is kind; and fifteen
years for the quarrel with the police, which was pretty bad
sort — those figures, if we add them together, make up to

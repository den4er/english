<h3><span>CHAPTER IV</span> Mr. Badger</h3>
<div>


<p>
They waited for a very long time. At last they heard the 
sound of slow footsteps. The door opened a few inchesl, 
enough to show a long nose and sleepy eyes.
‘Now, the next time this happens,’ said a voice, ‘I will be
very angry. Who is it THIS time? Speak up!’
</p><p>
‘Oh, Badger,’ cried the Rat, ‘let us come in, please. It’s me,
</p><p>
Rat, and my friend Mole, and we lost our way in the snow.’
</p><p>
‘What, Ratty, my dear little man!’ said the Badger, in a
different voice. ‘Come in, both of you, at once. Lost in the
snow! And in the Wild Wood, too, and at this time of night!
But come in.’
</p><p>
The two animals heard the door shut behind them with
great joy.
</p><p>
The Badger wore a long dressing-gown and slippers. He
looked kindly on them and patted both their heads. ‘This
is not the sort of night for small animals to be out,’ he said.
‘But come along; come into the kitchen. There’s a fire there,
and supper and everything.’
</p><p>
He went on in front of them, down a long, dark corridor, into a central hall; out of which they could see other
long tunnel-like corridors. But there were doors in the hall.
One of these the Badger opened, and at once they were in
warmth of a large kitchen.
</p><p>
In the middle of the room stood a long table. At one end
of it, where an armchair stood, was the Badger’s supper.
Overhead hung hams, dried herbs, onions, and baskets of
eggs. It seemed a place where two or three friends could sit
and eat and talk in comfort.
</p><p>
The kindly Badger sat them down, and told them to take
off their wet coats and boots. Then he gave them dressing-gowns and slippers, and bathed the Mole’s leg with
warm water and put the sticking-plaster on the cut. In the
light and warmth, warm and dry at last, it seemed to the
animals, that the cold Wild Wood was miles and miles away.
</p><p>
The Badger laid the table, and when they saw at last the
supper, really it seemed only a question of what they attack 
first where all was so nice. Talk was with their mouths full.
The Badger did not mind that sort ofthing at all, he did not
see elbows on the table, or everybody speaking at once. As
he did not go into Society himself, he had got an idea that
these things didn’t really matter. (We know of course that
he was wrong, because they matter very much.) He sat in
his armchair at the head of the table, and listened as the
animals told their story; and he did not show any surprise
or shock, and he never said, ‘I told you so,’ or, ‘Iust what I
always said.’ The Mole began to feel very friendly towards
him.
</p><p>
After supper they gathered round the fire, and the
Badger asked, ‘Now then! tell me the news from your part
of the world. How’s old Toad going on?’
</p><p>
‘Oh, from bad to worse,’ said the Rat. ‘You see, he wants
to drive himself, and he can’t. Nobody can teach him anything.
</p><p>
‘How many did he have?’ asked the Badger.
</p><p>
‘Cars?’ asked the Rat. ‘Oh, this is the seventh.’
</p><p>
‘Three times in hospital,’ added the Mole; ‘and it’s simply awful to think of the money he paid for fines.’
</p><p>
‘Yes, and that’s part of the trouble,’ said the Rat. ‘Toad’s
rich, we all know; but he’s not a millionaire. And he’s a bad
driver, and doesn’t know order. He will lose his life or his
money — sooner or later. Badger! we’re his friends — We
must do something!’
</p><p>
The Badger thought hard. ‘Now look here!’ he said at
last, ‘of course you know I can’t do anything NOW?’
</p><p>
His two friends agreed. No animal can do anything heroic, or even active during winter. All are sleepy — some
asleep. All are resting.
</p><p>
‘Very well then!’ continued the Badger. ‘BUT, when the
nights are shorter — YOU know!’
</p><p>
Both animals agreed. THEY knew!
</p><p>
‘Well, THEN,’ went on the Badger, ‘we — I mean, you and
me and our friend the Mole here — we’ll take Toad seriously in hand. We’ll bring him back to reason. We’ll MAKE him
be a clever Toad. We’ll — you’re sleeping, Rat!’
</p><p>
‘Not me!’ said the Rat, waking up.
</p><p>
‘You are, and not for the first time since supper,’ said the
Mole, laughing. He himself was feeling wakeful and even lively, he didn’t know why. The reason was, of course, that he
was naturally an underground animal, and the situation of
Badger’s house made him feel at home; while the Rat, who
slept every night in a bedroom the windows of which opened
on the river, naturally felt the atmosphere oppressive.
</p><p>
‘Well, it’s time to go to bed,’ said the Badger. ‘Come, and
I’ll show you to your room. And take your time tomorrow
morning — breakfast at any hour you like!’
</p><p>
He led the two animals to a long room that seemed only
half bedroom. The Badger’s winter foods took up half the
room — piles of apples, turnips, and potatoes, baskets full
of nuts, and jars of honey; but the two little white beds on
the floor looked soft and nice, and the Mole and the Water
Rat lay down in great joy.
</p><p>
The two tired animals came down to breakfast very late
next morning, and found a bright fire in the kitchen, and
two young hedgehogs sitting at the table, eating porridge.
The hedgehogs left their spoons and stood up respectfully.
</p><p>
‘Sit down, sit down,’ said the Rat, ‘and go on with your
porridge. Where did you youngsters come from? Lost your
way in the snow?’
</p><p>
‘Yes, sir,’ said the elder of the two hedgehogs respectfully. ‘Me and little Billy here, we were trying to find our way to
school, and of course we lost ourselves, sir, and Billy, he got
frightened and cried. And at last we saw Mr. Badger’s back
door, and came in, sir, for Mr. Badger, he’s a kind gentleman,
as everyone knows—’
</p><p>
‘I understand,’ said the Rat, cutting himself some bacon,
while the Mole put some eggs into a plate. ‘And what’s the
weather like outside?’
</p><p>
‘O, terribly bad, sir, the snow is terribly deep,’ said the
hedgehog. ‘No getting out today.’
</p><p>
‘Where’s Mr. Badger?’ asked the Mole, as he warmed the
coffee-pot before the fire.
</p><p>
‘The master went to his room, sir,’ answered the hedgehog, ‘and he said, “I’m was very busy this morning.”’
</p><p>
The animals well knew that Badger, after a big breakfast,
went to his room and sat himself in an armchair with his
legs up on another and a red cotton handkerchief over his
face, and was ‘busy’ in the usual way at this time of the year.
</p><p>
The front-door bell rang loudly, and the Rat, who was
very busy with his toast, sent Billy, the smaller hedgehog, to
see who it was. There was a sound of stamping in the hall,
and Billy returned in front of the Otter, who threw himself
on the Rat with joy.
</p><p>
‘Get off!’ cried the Rat, with his mouth full.
</p><p>
‘Thought I could find you here all right,’ said the Otter.
‘They were all in a great alarm along River Bank when I arrived this morning. Rat wasn’t home all night — nor Mole
either — something dreadful happened, they thought; and
the snow covered up all your tracks, of course. But I know
that when people are in trouble they mostly go to Badger, 
or else Badger gets to know of it somehow. So I came here,
through the Wild Wood and the snow! My! it was fine, com-
ing through the snow as the red sun was rising and showing
against the black tree-trunks! About halfway across I came
on a rabbit, cleaning his silly face with his paws. He was a
pretty scared animal. He told me about the Mole’s coming
to the Wild Wood.’
</p><p>
‘Weren’t you at all — er — nervous?’ asked the Mole,
some of yesterday’s terror coming back to him at the words
of the Wild Wood.
</p><p>
‘Nervous?’ cried The Otter showing strong white teeth
as he laughed. ‘I can make them nervous myself. Here, Mole,
fry me some slices of ham, like the good little boy you are.
I’m very hungry, and I’ve got something to say to Ratty here.’
</p><p>
So the Mole cut some slices ofham, asked the hedgehogs
to fry it, and returned to his own breakfast, while the Otter
and the Rat, their heads together, talked about the river.
</p><p>
Then the Badger came, rubbing his eyes, and greeted them
all. ‘It must be lunch time,’ he said to the Otter. ‘Better stop and
have it with us. You must be hungry, this cold morning.’
</p><p>
‘Here, you two youngsters, be off home to your mother,’
said the Badger kindly. ‘I’ll send someone with you to show
you the way.’
</p><p>
He gave them siXpence apiece and a pat on the head, and
they went off with much respect.
</p><p>
They all sat down to lunch together. The Mole’s place
was near Mr. Badger, and, as the other two were still deep
in talk, he told Badger how comfortable and home-like it
all felt to him. ‘When you are underground,’ he said, ‘you
know exactly where you are. Nothing can happen to you.
You’re your own master, and you don’t need to ask anybody.
</p><p>
Things go on all the same overhead, and you let them, and
don’t bother about them. When you want to, you go up, and
there the things are, waiting for you.’
</p><p>
The Badger simply smiled at him. ‘That’s what I always
say,’ he answered. ‘There’s no security, or peace, except
underground. If you feel your house is a bit too big, you stop
up a hole or two, and there you are again! No builders, no
salesmen, and, above all, no WEATHER. Look at Rat, now.
A couple of feet of ﬂood water, and he needs to leave his house;
uncomfortable, badly situated, and horribly expensive. Take
Toad. I say nothing against Toad Hall; quite the best house
in these parts, AS a house. But if a fire breaks out — where’s
Toad? If walls sink or crack, or windows break — where’s
Toad? No, up and out of doors is good enough to walk; but
underground to come back to at last — that’s my idea of
HOME.’
</p><p>
The Mole agreed; and the Badger got very friendly with
him. ‘When lunch is over,’ he said, ‘I’ll take you all round
this little place. You understand what domestic architecture
must be, you do.’
</p><p>
After lunch the Mole followed the Badger. Crossing the
hall, they went down one of the tunnels, and saw rooms
on either side, both large and small, some like cupboards,
others nearly as big as Toad’s dining-hall. ‘How, Badger,’ the
Mole said at last, ‘did you ever find time and strength to do
all this? It’s wonderful!’
</p><p>
‘I did none of it — only cleaned out the corridors and
rooms,’ said the Badger simply, ‘I see you don’t understand, 
and I must explain it to you. Well, very long ago, on the place
where the Wild Wood is now, before it, there was a city —
a city of people, you know. Here, where we are standing,
they lived, and walked, and talked, and slept, and carried on
their own business. They were a powerful people, and rich,
and great builders.’
</p><p>
‘But what happened to them all?’ asked the Mole.
</p><p>
‘Who can tell?’ said the Badger. ‘People come — they stay
for a while, they build — and they go. It is their way But we
stay There were badgers here, long before that city ever came
to be. And now there are badgers here again. We may move
out for a time, but we wait, and are patient, and we come back.
And so it will ever be.’
</p><p>
‘Well, and when they went at last, those people?’ said the
Mole.
</p><p>
‘When they went,’ continued the Badger, ‘the strong
winds and rains went on year after year. Perhaps we badgers too, in our small way, helped a little — who knows?
It was all down, down, down, — ruin and disappearance.
Then it was all up, up, up, as seeds grew to trees, and trees
to forest, and insects came in to help. Leaf-mounts rose,
streams brought sand, and in time our home was ready for
us again, and we moved in. Up above us, the same thing
happened. Animals came, liked the look of the place, took
up their homes. There are a lot ofanimals in the Wild Wood.
We must all live and let live.’
</p><p>
When they got back to the kitchen again, they found the
Rat walking up and down, very nervous. The underground
atmosphere was oppressing him and getting on his nerves and
he worried about the river. So he had his overcoat on, and his
pistols into his belt again. ‘Come along, Mole,’ he said, as soon 
as he saw them. ‘We must get off while it’s daylight. I don’t
want to spend another night in the Wild Wood again.’
</p><p>
‘It’ll be all right, my fine fellow,’ said the Otter. ‘I’m coming
along with you, and I know every path.’
</p><p>
The Badger said them good-bye and soon they were out
of the Wild Wood. Rocks and tree-roots were behind them;
in front, a great space of quiet fields, hedges black on the
snow, and, far ahead, familiar old river, while the wintry sun
hung red and low on the horizon. Pausing there a moment
and looking back, they saw the Wild Wood, large, evil in
white surroundings; they turned and went quickly home,
for firelight, for the river that they knew, that never made
them afraid.
</p>
</div> 

 
<h3><span>CHAPTER V</span> Sweet Home</h3>
<div>




<p>
The two animals were walking with much chatter and 
laughter. They were returning across the country 
after a long day with Otter, hunting and exploring
where streams to their own River had their first small
beginnings; and the short winter day was closing on them,
and they had still some distance to go. ‘It seems we are
coming to a Village,’ said the Mole, as the path became a
road. The animals did not like villages. ‘Oh, never mind!’
said the Rat. ‘At this season of the year they’re all indoors
by this time, sitting round the fire; men, women, and
children, dogs and cats and all. We will slip through all right, 
without any trouble, and we can have a look at them through
their windows if you like, and see what they’re doing.’
</p><p>
The quick nightfall of mid-December surrounded the little village as they came on soft feet over a first thin fall of
snow. They saw only squares of orange-red on either side of
the street, where the firelight or lamplight of each cottage
burnt. Most of the low windows were without curtains, and
the lookers-in from outside saw people sitting round the
tea-table, doing handiwork, or talking with laughter and
gesture. Moving from one theatre to another, the two spectators, so far from home themselves, felt sad.
</p><p>
Then a cold wind took them in the back of the neck,
woke them as from a dream, and they knew their toes were
cold and their legs tired, and their own home far away.
</p><p>
Behind the village, where the cottages disappeared, on
either side of the road they could smell through the darkness
the friendly fields again; and they got ready for the last long
walk, the walk home, that we know must end, some time, in
the sudden firelight, and the sight of well-known things greeting us as long-absent travellers from far over-sea. They went
along, each of them thinking his own thoughts. The Mole was
following the Rat, as it was dark, and it was a strange country
for him. As for the Rat, he was walking a little way ahead, his
eyes on the straight grey road in front of him; so he did not
notice poor Mole when suddenly the smell reached him, and
took him like an electric shock.
</p><p>
Home! That was what the smell meant! Why, it seemed
to be quite near at that moment, his old home! Now, for
the first time since his escape on that bright morning, he
remembered it!
</p><p>
‘Ratty!’ he called, full ofjoy, ‘wait! Come back!’
</p><p>
‘Oh, COME along, Mole!’ replied the Rat, still walking.
</p><p>
‘PLEASE stop, Ratty!’ asked the poor Mole. ‘You don’t
understand! It’s my home, my old home! It’s really quite
near. I MUST go to it, I must, I must! Oh, come back, Ratty!
Please, please come back!’
</p><p>
The Rat was too far ahead to hear clearly what the Mole
was calling. And he too could smell something — something like coming snow.
</p><p>
‘Mole, we mustn’t stop now, really!’ he called back.
‘We’ll come for it tomorrow, whatever it is. But I can’t stop
now — it’s late, and the snow’s coming on again, and I’m
afraid of losing the way! And I want your nose, Mole, so
come on, quick!’ Saying this, the Rat kept walking without
waiting for an answer.
</p><p>
Poor Mole stood alone in the road, and a big cry was
gathering somewhere low down inside him. But even under
such a test as this his loyalty to his friend stood firm. He
followed the Rat.
</p><p>
The Rat began chattering about what they could do back
home, and about the fire, and what a supper he wanted to
eat. He didn’t notice his friend’s silence and distressful face.
At last, he stopped and said kindly, ‘Look here, Mole old
chap, you seem very tired. We’ll sit down here for a minute
and rest. The snow isn’t coming so far, and the biggest part
of our journey is over.’
</p><p>
The Mole sat on a fallen tree and tried to control himself.
But the cry was winning, and poor Mole gave up, and cried
freely and helplessly and openly, now that he knew it was
all over.
</p><p>
It was a shock for the Rat. He did not speak for a while.
At last he said, very quietly, ‘What is it, old fellow? Tell me
your trouble, and let me see what I can do.’
</p><p>
Poor Mole answered with difficulty, ‘I know it’s a — dull,
dirty little place,’ he said at last, brokenly: ‘not like — your
cozy house — or Toad’s beautiful hall — or Badger’s great
house — but it was my own little home — and I was fond of
it. I smelt it suddenly — on the road, when I called and you
didn’t listen, Rat — and everything came back to me — and
I WANTED it! — O dear, O dear! — and when you DIDN’T
turn back, Ratty — I left it, though I was smelling it all the
time. — Why didn’t we just go and have one look at it, Ratty? — Only one look — it was near — but you didn’t turn
back, Ratty, you didn’t turn back! O dear, O dear!’
</p><p>
Remembering brought new crying, and he couldn’t
speaklongen
</p><p>
The Rat looked straight in front of him, saying nothing,
only patting Mole gently on the shoulder. After a time he
said, ‘I see it all now! What a PIG I was! Just a pig!’
</p><p>
He waited till Mole’s crying became gradually less
stormy; he waited till it stopped. Then he stood up, said,
‘Let’s go, old chap! We’re going to find it!’ and went up the
road again.
</p><p>
‘Oh, come back, Ratty!’ cried the Mole, getting up and run-
ning after him. ‘It’s too late, and too dark, and the place is too
far off, and the snow’s coming! And — and I never meant to
let you know I was feeling that way about it — it was all a
mistake! And think of River Bank, and your supper!’
</p><p>
‘Oh no!’ said the Rat. ‘I tell you, I’m going to find this
place now. So cheer up, old chap, and take my arm, and we’ll
very soon be back there again.’
</p><p>
Mole let the Rat pull him along the road, and his friend
kept talking cheerfully. Then the Rat said, ‘Now, no more
talking. Business! Use your nose.’
</p><p>
They moved on in silence for a while, when suddenly the
Mole stopped. His nose felt the air. Then he set off.
</p><p>
The Rat followed the Mole, who with the look of a
sleep-walker crossed a dry ditch, got through a hedge, and
nosed his way across a field.
</p><p>
Suddenly, he went down; the Rat followed him down the
tunnel to which Mole’s nose was leading him.
</p><p>
It was close and the earthy smell was strong, and it
seemed a long time to Rat before the tunnel ended and he
could stand up and shake himself. The Rat saw that they
were standing in an open space, clean and sanded underfoot, and in front of them was Mole’s little door and a sign
‘Mole End’.
</p><p>
Mole took a lamp from the wall and lit it... and the Rat,
looking round him, saw that they were in a sort of yard.
</p><p>
Mole smiled and led Rat through the door, lit a lamp in
the hall, and looked round his old home. He saw the dust
lying thick on everything, and fell again on a hall-chair, his
nose to his paws. ‘O Ratty!’ he cried, ‘why did I bring you to
this poor, cold little place instead of River Bank?’
</p><p>
The Rat paid no attention to it. He was running here and
there, opening doors, inspecting rooms and cupboards,
and lighting lamps. ‘What a great little house this is!’ he
called out cheerfully. ‘Everything here and everything in
its place! We’ll make a joyful night of it. The first thing we
want is a good fire; I’ll see to that — I always know where
to find things. So this is the living-room. Beautiful! Your
own idea, those little sleeping beds in the wall? Great!
</p><p>
Now, I’ll take the wood and the coals, and you get a duster,
Mole — and try and clean things up a bit. Move about, old
chapV
</p><p>
The Mole rose and dusted and polished with energy,
and the Rat soon had a cheerful fire. He called the Mole to
come and warm himself; but Mole had another cry, hiding
his face in his duster. ‘Rat,’ he said, ‘how about your supper,
you poor, cold, hungry, tired animal? I have nothing to give
you — nothing!’
</p><p>
‘Why, I see a sardine-opener on the kitchen table,’ said
the Rat, ‘and everybody knows that means there are sardines about somewhere. Come with me and look.’
</p><p>
They went, hunting through every cupboard and turning out every box. They found a tin of sardines — a box of
biscuits, nearly full — and a German sausage in silver paper.
</p><p>
‘There’s a banquet for you!’ said the Rat, as he laid the
table. ‘I know some animals who would give their ears to be
sitting down to supper with us tonight!’
</p><p>
‘No bread!’ cried the Mole; ‘no butter, no—’
</p><p>
‘No cream-cakes, no champagne!’ continued the Rat,
smiling. ‘This is really the happiest little place I ever was
in. Now, where did you pick up those prints? They make
the place look so home-like. No wonder you’re so fond of
it, Mole. Tell me all about it, and how you came to make it
what it is.’
</p><p>
The Mole, a little shyly at first, told how he planned, and
how he thought, and how he got this from an aunt, and that
was a wonderful find, and how he bought this other thing.
He took a lamp to show them off to his visitor, quite forgetful of the supper they both so much needed; Rat, who
was very hungry but tried not to show it, kept examining
everything seriously, and saying, ‘Wonderful,’ and ‘most
beautiful,’ at intervals.
</p><p>
At last they returned to the table, and the Rat was busy
with the sardine-opener when they heard the sounds from
the yard — sounds like pattering of small feet and little
voices — ‘Now, all in a line — hold the lamp up a bit, Tommy — after I say one, two, three. — Where’s young Bill? —
Here, come on, we’re all waiting—’
</p><p>
‘What’s up?’ asked the Rat.
</p><p>
‘I think it must be the field-mice,’ replied the Mole, with
pride. ‘They go round carol-singing regularly at this time of
the year. And they never forget me — they come to Mole End
last of all; and I usually give them hot drinks, and supper too
sometimes. It will be like old times to hear them again.’
</p><p>
‘Let’s have a look at them!’ cried the Rat, jumping up and
running to the door.
</p><p>
In the yard, some eight or ten little field mice stood, red
warm scarves round their necks, their paws deep into their
pockets, their feet dancing for warmth. With bright eyes
they looked shyly at each other. As the door opened, one of
the elder ones that carried the lamp was just saying, ‘Now
then, one, two, three!’ and their little voices rose on the air,
singing one of the old-time carols.
</p><p>
The voices stopped, the singers, smiling, exchanged
looks. Then, from up above and far away, there came the
sound of distant bells ringing.
</p><p>
‘Very well, boys!’ cried the Rat. ‘And now come in, all of
you, and warm yourselves by the fire, and have something
hot!’
</p><p>
‘Yes, come along, field-mice,’ cried the Mole. ‘This is
quite like old times! Now, you just wait a minute, while 
we — O, Ratty!’ he cried, falling on a seat, with tears gathering. ‘What are we doing? We have nothing to give them!’
</p><p>
‘You leave all that to me,’ said the Rat. ‘Here, you with the
lamp! Now, tell me, are there any shops open at this hour
of the night?’
</p><p>
‘Why, certainly, sir,’ replied the field-mouse respectfully. ‘At
this time of the year our shops keep open to all sorts of hours.’
</p><p>
‘Then look here!’ said the Rat. ‘You go off at once, you
and your lamp, and you get me—’
</p><p>
Here the Mole only heard parts of it, such as — ‘Fresh,
mind — no, a pound of that enough — no, only the best —
well then, do the best you can!’ Finally, there was a sound
of coin passing from paw to paw, the field-mouse took a big
basket and left.
</p><p>
The rest of the field-mice sat down close to the fire; the
Mole went into family history and made each of them tell
the names of his many brothers, who were too young to go
out carol-singing this year. The Rat, meanwhile, was busy;
and soon every field-mouse was drinking hot tea and laughing and forgetting he was ever cold in all his life.
</p><p>
‘They act plays too, these fellows,’ the Mole said to the
Rat. ‘Make them up all by themselves, and act them. They
gave us a great one last year, about a field-mouse whom a pirate caught at sea, and made him row in a galleyz; and when
he escaped and got home again, he found his lady-love in a
monastery. Here, YOU! You were in it, I remember. Get up
and read a bit.’
</p><p>
The field-mouse got up, laughed shyly, looked round the
room, and couldn’t say a word. They were all busy cheering
him up, when the door opened, and the field-mouse with
the lamp appeared with his basket.
</p><p>
There was no more talk of play-acting when the con-
tents of the basket were out on the table. In a very few
minutes supper was ready, and they ate, and talked of old
times, and the field-mice gave the Mole the local news, and
answered as well as they could the hundred questions he
asked them. The Rat said little or nothing, only taking care
that each guest had what he wanted, and a lot of it, and that
Mole had no trouble about anything.
</p><p>
They left at last, very grateful and with their jacket pockets full of sweets for the small brothers and sisters at home.
When the door closed on the last of them, Mole and Rat sat
down near the fire and discussed the long day. At last the
Rat said, ‘Mole, old chap, I’m ready to fall. Is that your own
bed over there? Very well, then, I’ll take this one. What a
nice little house this is! Everything so near!’
</p><p>
He got into his bed and instantly fell asleep.
</p><p>
The tired Mole also was glad to have his head on his
pillow, in great joy and peace. But before he closed his eyes
he let them go round his old room, on friendly things which
were long a part of him, and now smilingly welcomed him
back. He saw clearly how ordinary and simple it all was;
but clearly, too, how much it all meant to him. He did not
at all want to forget the new life and its wonderful places,
to turn his back on sun and air. But it was good to think he
had this place to come back to; these things which were so
glad to see him again and could always give him the same
simple welcome.

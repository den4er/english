<h3><span>CHAPTER IX</span> Wayfarers All</h3>
<div>



<p>
The Water Rat was restless, and he did not exactly 
know why. The summer was still at fullest height,
but there was a feeling in the air of change and 
departure. It was difficult to settle down to anything seriously. Leaving the waterside, he went to the country,
crossed a field or two, and dived into the great sea of
wheat, yellow, and full of quiet motion and small sounds.
He loved to walk here. Here, too, he had many small
friends, leading full and busy lives, but always with a moment to talk, and exchange news with a visitor. Today,
however, the field-mice were busy. Many were digging
and tunneling busily; others gathered together in small
groups and examined plans and drawings of small flats.
Some were pulling out dusty trunks, others were packing
their things. Piles of wheat, oats, barley, and nuts, lay
about ready for transport.
</p><p>
‘Here’s old Ratty!’ they cried as soon as they saw him.
‘Come and help us, Rat!’
</p><p>
‘What sort of games are you playing?’ said the Water Rat.
‘You know it isn’t time to be thinking of winter ﬂats yet!’
</p><p>
‘O yes, we know that,’ answered a field-mouse; ‘but we re-
ally MUST get all the furniture and baggage moved out of this
before those horrid machines begin clicking round the fields;
and then, you know, the best ﬂats get picked up so quickly nowadays, and all the rest need such a lot of doing up. Of course,
we’re early, we know that; but we’re only just making a start.’
</p><p>
‘O, forget it,’ said the Rat. ‘It’s a splendid day. Come for a
boat trip, or a walk, or a picnic in the woods, or something.’
</p><p>
‘Well, I THINK not TODAY, thank you,’ said the field-
mouse hurriedly. ‘Perhaps some OTHER day—’
</p><p>
The Rat went out of the field.
</p><p>
He returned to his river again — his old river, which
never packed up, or went into winter ﬂats.
</p><p>
In the willows he saw a swallow sitting. Then another
joined it, and then a third; and the birds talked together.
</p><p>
‘What, ALREADY,’ said the Rat, coming up to them.
‘What’s the hurry?’
</p><p>
‘O, we’re not leaving yet,’ answered the first swallow.
‘We’re only making plans and arranging things. Talking it
over, you know.’
</p><p>
‘Couldn’t you stop on for just this year?’ asked the Water Rat. ‘We’ll all do our best to make you feel at home. You
have no idea what good times we have here, while you are
far away.’
</p><p>
‘I tried “stopping on” one year,’ said another swallow.
‘For a few weeks it was well enough, but afterwards, O! The
cold, sunless days! The air so frosty, and not an insect in it!
No, it was no good; and one cold, one stormy night I had to
ﬂy away. I will never forget the happy feeling of the hot sun
again on my back, and the taste of my first fat insect! No,
never again I thought of staying.’
</p><p>
‘Ah, yes, the call ofthe South, ofthe South!’ twittered the
other swallows dreamily.
</p><p>
‘Why do you ever come back, then, at all?’ asked the Rat.
</p><p>
‘And do you think,’ said the first swallow, ‘that there is
no call of meadow-grass, gardens, warm ponds, and all the
farm-buildings?’
</p><p>
They went on talking of blue seas, brown sands, and
warm walls.
</p><p>
The Rat walked off once more. Footsteps fell on his ear,
and the figure of one that walked somewhat tiredly came into
view; and he saw that it was a Rat, and a very dusty one. The
wayfarer, as he reached him, saluted with a gesture of something foreign about it — then with a pleasant smile turned
from the path and sat down by his side in the cool grass. He
seemed tired, and the Rat let him rest unquestioned, understanding something of what was in his thoughts.
</p><p>
The wayfarer was thin, and somewhat bowed at the
shoulders; his paws were thin and long, and he wore small
gold earrings in his ears. His sweater and breeches were
blue and his small possessions he carried in a blue cotton
handkerchief.
</p><p>
‘Those are cows we hear,’ the stranger said; ‘and over
there rises a blue line of cottage smoke against the woodland. The river runs somewhere close by. Everything seems
asleep, and yet going on all the time. It is a good life that
you lead, friend.’
</p><p>
‘Yes, it’s THE life, the only life, to live,’ answered the Water Rat dreamily.
</p><p>
‘It is the best,’ said the stranger; ‘I tried it, and I know.
And now here I am, tired and hungry, following the old call,
back to the old life, THE life which will not let me go.’
</p><p>
‘And where did you come from?’ the Rat asked.
</p><p>
‘Nice little farm,’ answered the wayfarer. ‘From that
direction’ — he showed to the north. ‘I had everything I
could want — everything I had any right to expect of life,
and more; and here I am! Glad to be here all the same! So
many miles further on the road, so many hours nearer to
my heart’s dream!’
</p><p>
His shining eyes looked to the horizon, and he seemed
to be listening for some sound.
</p><p>
‘You are not one of US,’ said the Water Rat. ‘You’re not a
farmer. I think you are a foreigner.’
</p><p>
‘Right,’ replied the stranger. ‘I’m a seafaring rat, and the
port Ioriginally am from is Constantinople, though I’m a
sort of a foreigner there too. The city of my birth is no more
my home than any pleasant port between there and the
London River. I know them all, and they know me. Set me
down on any of their shores, and I am home again.’
</p><p>
‘I think you go great voyages,’ said the Water Rat with
growing interest. ‘Months and months out of land, and 
provisions running short, and water too, and your mind speaking with the mighty ocean, and all that sort of thing?’
</p><p>
‘By no means,’ said the Sea Rat honestly. ‘Such a life as you
describe could not suit me at all. I’m in the coasting trade,
and rarely out of land. It’s the happy times on shore that
I like, as much as any seafaring. O, those southern seaports!’
</p><p>
‘Well, perhaps you chose the better way,’ said the Water
Rat doubtfully. ‘Tell me something of your coasting, then;
for my life, I must tell, feels to me today somewhat boring.’
</p><p>
‘Let me tell you about my last voyage that landed me in
this country,’ began the Sea Rat, ‘Family troubles, as usual,
began it. Icame on board a small trading boat from Constantinople
 to the Grecian Islands and the Levant. Those
were golden days and warm nights! In and out of port all
the time — old friends everywhere — sleeping in some cool
church during the heat of the day — parties after sundown,
under a velvet sky with great stars! From there we started
up the Adriatic and to Venice. O, Venice is a fine city, where
a rat can walk at his ease and take his pleasure!’
</p><p>
He was silent for a time; and the Water Rat was silent too.
</p><p>
‘Then we left for Palermo,’ continued the Sea Rat, ‘and
there I landed for a long, happy stay on shore. I never stay
too long on one ship. Besides, I love Sicily. I know everybody
there. I spent many happy weeks in the island. When I grew
restless again Itook a ship to Sardinia and Corsica. From
Corsica, by easy trips, and partly on foot, partly by sea, I got
to Marseilles. Sometimesl dream of the shell-fish of Marseilles, and wake up crying!’
</p><p>
‘By the way,’ said the polite Water Rat; ‘I believe you’re
hungry. Of course, you will stop and take your midday meal
with me? My hole is close by.’
</p><p>
‘That’s very kind of you,’ said the Sea Rat. ‘I am indeed
hungry. But could you bring it along out here? I am not too
fond of going underground; and then, while we eat, I could
tell you more about my voyages. Indoors I will at once fall
asleep.’
</p><p>
The Water Rat hurried off home. There he got out the
lunch-basket and packed a simple meal, in which he took
care to put a yard long French bread, a sausage, some cheese,
and a long-necked bottle. Together they unpacked the basket and laid out the food on the grass by the roadside.
</p><p>
The Sea Rat, as soon as he finished eating, continued the
history of his latest voyage. With excitement, the Water Rat 
followed him over stormy seas, across port bars, up rivers;
and left him at his dull farm.
</p><p>
The quiet world outside disappeared. And the talk, the
wonderful talk went on — and with beating heart he was
following the adventures of seaports, the fights, the escapes;
or he looked for treasure on islands, fished in lagoons and
slept day-long on warm white sand.
</p><p>
‘And now,’ the Sea Rat continued, ‘I take to the road
again, for many long and dusty days; till at last I reach the
little grey sea town I know so well. There, sooner or later,
the ships of all seafaring nations arrive. I will choose a ship
and run on board, and the voyage will begin!
</p><p>
‘And you will come too, young brother; for the days pass,
and never come back, and the South still waits for you. Take
the Adventure, hear the call! I will walk, and look back; and
at last I will see you coming!’
</p><p>
The words grew quieter and quieter; and the Water Rat
saw at last a distant spot on the road.
</p><p>
Mechanically he rose and repacked the lunch-basket,
carefully and slowly. Mechanically he came back home, and
packed his special treasures, carefully selected a strong
walking-stick, and with no hurry, but with no doubt at all, he
stepped out of his house as the Mole appeared at the door.
</p><p>
‘Where are you going, Ratty?’ asked the Mole in great
surprise, catching him by the arm.
</p><p>
‘Going South, with the rest of them,’ said the Rat dreamily, not looking at him.
</p><p>
It alarmed the Mole. He looked into his eyes and saw
that they were glassy — not his friend’s eyes, but the eyes of
some other animal! He pulled him inside, threw him down,
and held him.
</p><p>
The Rat fought for a while, and then his strength seemed
suddenly to leave him, and he lay still, with closed eyes, trembling. The Mole helped him to rise and placed him in a chair.
Mole closed the door, threw the Rat’s pack into a drawer, and
sat down quietly on the table by his friend, waiting for the
strange illness to pass. After some time the Rat fell asleep.
</p><p>
The Mole left him for a time and busied himself with
household matters; and it was getting dark when he returned to the room and found the Rat awake but apathetic,
silent, and depressed. But his eyes were his own eyes once
again. The Mole sat down and tried to cheer him up and
help him to tell his story.
</p><p>
Poor Ratty did his best, by degrees, to explain things; but
how could he put into cold words what was an idea? It is not
surprising, then, that he couldn’t explain it to the Mole.
</p><p>
The Mole understood that his friend was out of danger.
But he had no interest in the matters of his daily life.
</p><p>
The Mole began to talk about the harvest, and the large
moon rising over fields. He talked of the reddening apples
around, of the browning nuts; he reached midwinter, its joys
and its cozy home life, and then he became simply lyrical.
</p><p>
By degrees the Rat began to sit up and to join in. His dull
eyes brightened.
</p><p>
Then the tactful Mole went away and returned with a
pencil and a few sheets of paper, which he placed on the table at his friend’s elbow.
</p><p>
‘It’s quite a long time since you did any poetry,’ he said.
‘Why don’t you try it this evening, instead of — well, thinking
over things so much?’
</p><p>
When the Mole secretly looked in again some time later, he
was happy to see that the Rat was busily writing something.
</p>

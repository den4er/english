<h3><span>CHAPTER XI</span> ‘Like Summer Tempests Came His Tears’</h3>
<div>




<p>
The Rat put out a neat little brown paw, took Toad 
firmly by the neck, and pulled; and the Toad came up 
slowly but surely over the edge of the hole, till at last 
he stood in the hall, with the water streaming off him, but 
quite happy, for now he found himself once more in the
house of a friend.
</p><p>
‘O, Ratty!’ he cried. ‘Let me tell you about my adventures! Such sufferings! Then such escapes! Oh, AM a
smart Toad!’
</p><p>
‘Toad,’ said the Water Rat, seriously and firmly, ‘go off
upstairs at once, and take off that old cotton dress, and
clean yourself, and put on some of my clothes, and try and 
come down looking like a gentleman if you CAN! Now, stop
arguing, and be off! I’ll have something to say to you later!’
</p><p>
Toad wanted to stop and argue, but he saw himself in
the looking-glass, and he changed his mind and went very
quickly upstairs. There he washed his face and changed his
clothes, and stood for a long time before the glass, looking at himself with pride and pleasure, and thinking, ‘How
could anyone really mistake me for a washerwoman?’
</p><p>
By the time he came down again lunch was on the ta-
ble, and Toad was very glad to see it. While they ate Toad
told the Rat all his adventures, speaking mostly of his own
cleverness. But the more he talked and boasted, the more
serious and silent the Rat became.
</p><p>
Then there was silence for a while; and then the Rat
said, ‘Now, Toady, I don’t want to hurt your feelings, after
all your experiences, but, seriously, where’s the fun in that?
What caused all this? Why, it was a car. Why did you steal
it? Why do you want to be a prisoner? When are you going
to be clever, and think of your friends? Do you think it’s any
pleasure to me to hear animals saying, as I go about, “There
are criminals among his friends"?’
</p><p>
Toad was a nice kind animal and never minded such
words from his real friends. And he was always able to see
the other side ofthe question. So, while the Rat was talking
so seriously, he kept saying to himself, ‘But it WAS fun!’ But
after the Rat’s speech he said, very sadly, ‘Quite right, Ratty!
How SERIOUS you always are! Yes, Iwas a fool, I can quite
see that; but now I’m going to be a good Toad. I’m not so
fond of cars after my drive into that river. We’ll have our
coffee, and a quiet chat, and then I’m going to walk quietly
down to Toad Hall, and put on my own clothes, and lead a 
quiet, respectable life, taking care of my property, and doing a little landscape gardening at times. There will always
be a bit of dinner for my friends when they come to see me;
and Iwill keep a pony to ride about the country, just as in
the good old days.’
</p><p>
‘Walk quietly down to Toad Hall?’ cried the Rat. ‘What
are you talking about? Do you mean to say you don’t KNOW?’
</p><p>
‘Know what?’ said the Toad, turning rather pale. ‘Go on,
Ratty! Quick! What is it?’
</p><p>
‘Do you mean to tell me,’ said the Rat, beating with his
little fist upon the table, ‘that you don’t know about the
Stoats and Weasels and how they live in Toad Hall?’
</p><p>
Toad put his elbows on the table, and his chin on his
paws; and a large tear came up in each of his eyes, and
dropped on the table.
</p><p>
‘Go on, Ratty,’ he said; ‘tell me all. The worst is over. I
can bear it.’
</p><p>
‘After your disappearance,’ said the Rat, slowly and impressively; ‘they talked about it a lot down here, naturally;
not only along the river-side, but even in the Wild Wood.
Animals took sides, as always happens. The river-bankers
were for you, but the Wild Wood animals said hard things,
and served you right, and went about saying, “That’s the
end of Toad! He will NEVER come back, never, never!”’
</p><p>
Toad nodded.
</p><p>
‘But Mole and Badger,’ the Rat went on, ‘they kept saying,
“No, Toad will come back again soon, somehow.”’ So they moved
their things to Toad Hall, and slept there, and had it all ready
for your return. They didn’t know what was going to happen,
of course; still, they didn’t trust the Wild Wood animals. Now
I come to the most tragic part of my story. One dark night — 
it was a VERY dark night, and blowing hard, too, and raining
heavily — a band of weasels carrying guns came silently up to
the front entrance. At the same time, a band of ferrets, coming
through the kitchen-garden, occupied the backyard; while a
company of stoats occupied the billiard-room, and held the
French windows opening onto the lawn.
</p><p>
‘The Mole and the Badger were sitting by the fire, telling
stories, when those bandits broke down the doors and rushed
in upon them from every side. They made the best fight they
could, but what was the good? What can two animals do
against hundreds? They took and beat them with sticks, those
two poor creatures, and threw them out into the cold and the
wet, with many bad words!’
</p><p>
Here the unfeeling Toad broke into a laugh, and then pulled
himself together and tried to look serious.
</p><p>
‘So the Wild Wooders are living in Toad Hall,’ continued
the Rat; ‘They are used to lying in bed half the day, and have
breakfast at all hours, and the place is in such a mess! They
are eating your food, and drinking your drinks, and making
bad jokes about you, and singing vulgar songs, about — well,
about prisons and policemen; horrid personal songs, with no
humor in them. And they’re telling everybody, “We’re going to
stay here for ever.”’
</p><p>
‘O, are they!’ said Toad getting up and taking a stick. ‘I’ll
soon see about that!’
</p><p>
‘It’s no good, Toad!’ called the Rat after him. ‘You’ll only
get into trouble.’
</p><p>
But the Toad was off. He marched quickly down the road,
his stick over his shoulder, speaking to himself in his anger,
till he got near his front gate, when suddenly there jumped
up from behind a bush a long yellow ferret with a gun.
</p><p>
‘Who comes there?’ said the ferret sharply.
</p><p>
‘Rubbish and nonsense!’ said Toad, very angrily. ‘What
do you mean by talking like that to me? Come out of that at
once, or I’ll—’
</p><p>
The ferret didn’t answer, but he brought his gun up to
his shoulder. Toad dropped flat in the road, and BANG! a
bullet went over his head.
</p><p>
The Toad got to his feet and ran off down the road as
hard as he could; and as he ran he heard the ferret laughing,
and other little laughs joining him.
</p><p>
He went back, very depressed, and told the Water Rat
about it.
</p><p>
‘That’s it,’ said the Rat. ‘It’s no good. They all have guns.
You must just wait.’
</p><p>
Still, Toad didn’t want to give up all at once. So he got
out the boat, and set off rowing up the river to where the
garden front of Toad Hall came down to the waterside.
There he rested on his oars and looked the land over. All
seemed very peaceful and quiet. He could see the whole
front of Toad Hall in the evening; the garden, the flowers;
the path that led up to the boat-house, the little wooden
bridge that crossed it; all waiting for his return. ‘I must try
the boat-house first,’ he thought. Very carefully he rowed,
and was just passing under the bridge, when a great stone
from above smashed through the bottom of the boat. It
filled and sank, and Toad found himself swimming in deep
water. Looking up, he saw two stoats looking over the parapet of the bridge and watching him merrily. ‘It will be
your head next time, Toady!’ they called out to him. The
angry Toad swam to shore, while the stoats laughed and
laughed.
</p><p>
The Toad came back on foot, and retold his sad experiences to the Water Rat once more.
</p><p>
‘Well, WHAT did I tell you?’ said the Rat hotly. ‘And
you lost my boat that I was so fond of, and simply ruined
the nice clothes that I gave you! Really, Toad, of all the
animals — I wonder how you manage to keep any friends
at all!’
</p><p>
Toad made a full apology to Rat for losing his boat and
spoiling his clothes. He finished by saying, with that face
which always disarmed his friend’s criticism and won them
back to his side, ‘Ratty! I see it was foolish. Believe me, I will
take no action without your kind advice!’
</p><p>
‘Then my advice to you is, because ofthe lateness ofthe
hour, to sit down and have your supper, which will be on
the table in a minute, and be very patient. I am sure that we
can do nothing until we see the Mole and the Badger, and
hear their latest news, and take their advice in this difficult
matter.’
</p><p>
‘Oh, ah, yes, of course, the Mole and the Badger,’ said
Toad, lightly. ‘How are they?’
</p><p>
‘Well may you ask!’ said the Rat. ‘They keep camping out
in the open, in every sort of weather, watching over your
house, patrolling your gardens, watching the stoats and the
weasels, planning how to get your property back for you.
You don’t deserve to have such friends, Toad, you don’t, really. Some day you’ll be very sorry!’
</p><p>
‘I’m an ungrateful beast, I know,’ cried Toad with bitter
tears. ‘Let me go out and find them, out into the cold, dark
night, and share their difficulties, and try — Oh, supper’s
here at last, hooray! Come on, Ratty!’
</p><p>
Just after their meal there came a heavy knock at the door.
</p><p>
Toad was nervous, but the Rat, nodding mysteriously at
him, went straight up to the door and opened it, and let in
Mr. Badger.
</p><p>
He was looking very wild and tired. He came up to Toad,
shook him by the paw, and said, ‘Welcome home, Toad!
What am I saying? Home, indeed! This is a poor home-coming. Unhappy Toad!’ Then he sat down to the table, and took
a large piece of cold pie.
</p><p>
The Rat said to Toad, ‘Don’t say anything to him just yet.
He’s always rather depressed when he is hungry. In half an
hour’s time he’ll be quite a different animal.’
</p><p>
So they waited in silence, and then there came another
knock. The Rat went to the door and let in the Mole, with
bits of dry grass in his fur.
</p><p>
‘Hooray! Here’s old Toad!’ cried the Mole, smiling.
‘Great!’ And he began to dance round him. ‘You managed to
escape, you clever Toad!’
</p><p>
The Rat pulled him by the elbow; but it was too late.
Toad was growing proud already.
</p><p>
‘Clever? O, no!’ he said. ‘I’m not really clever, according
to my friends. I only broke out of the strongest prison in
England, that’s all! And caught a railway train and escaped
on it, that’s all! And went about the country in disguise,
that’s all! Oh, no! I’m a fool, I am! I’ll tell you one or two
of my little adventures, Mole, and you will judge for yourself!’
</p><p>
‘Well, well,’ said the Mole, moving towards the supper-
table; ‘let you talk while I eat. Not a bite since breakfast! O
my! O my!’ And he sat down to supper.
</p><p>
Toad sat down, put his paw into his pocket and pulled
out a handful of silver. ‘Look at that!’ he cried, displaying it.
</p><p>
‘That’s not so bad, is it, for a few minutes’ work? And how do
you think I did it, Mole? Horse-selling! That’s how I did it!’
</p><p>
‘Go on, Toad,’ said the Mole with great interest.
</p><p>
‘Toad, be quiet, please!’ said the Rat. ‘Never mind it,
Mole, but please tell us as soon as possible what the position is, and what’s best to do, now that Toad is back at last.’
</p><p>
‘The position’s about as bad as it can be,’ answered the
Mole sadly; ‘and as for what to do, I don’t know! There are
guards and weapons everywhere, and they threw stones at
us; always an animal keep watch, and when they see us,
they start laughing! That is the worst!’
</p><p>
‘It’s a very difficult situation,’ said the Rat, deep in
thought. ‘But I think I see now what Toad really must do. I
will tell you. He must—’
</p><p>
‘Oh no!’ cried the Mole, with his mouth full. ‘Nothing of the
sort! You don’t understand. What he must do is, he must—’
</p><p>
‘Well, I won’t do it, anyway!’ cried Toad in excitement.
‘I’m not going to listen to your orders! It’s my house we’re
talking about, and I know exactly what to do, and I’ll tell
you. I’m going to—’
</p><p>
By this time they were all three talking at once, at the
top of their voices, when a thin, dry voice said, ‘Be quiet, all
of you!’ and then everyone was silent.
</p><p>
It was the Badger, who was looking at them severely.
Then he turned back to the table and reached out for the
cheese. And the others kept silence till the end of his meal.
Then the Badger got up from his seat and stood before the
fireplace, deep in thought. At last he spoke.
</p><p>
‘Toad!’ he said severely. ‘You bad, troublesome little animal!
Aren’t you ashamed of yourself, you, a son of my good friend?’
</p><p>
Toad burst out crying.
</p><p>
‘There, there!’ went on the Badger, more kindly. ‘Never
mind. Stop crying. We’re going to forget everything, and try
and turn over a new leaf. But what the Mole says is quite true.
The stoats are on guard, at every point, and they are the best
guards in the world. It’s quite useless to think of attacking the
place. They’re too strong for us.’
</p><p>
‘Then it’s all over,’ said the Toad, crying into the sofa. ‘I will
go and become a soldier, and never see my dear Toad Hall
again!’
</p><p>
‘Cheer up, Toady!’ said the Badger. ‘There are more ways
of getting back a place than attacking it. Now I’m going to tell
you a great secret.’
</p><p>
Toad sat up slowly and dried his eyes. He loved secrets
very much, because he never could keep one, and he enjoyed
sharing them.
</p><p>
‘There — is — an — underground — passage,’ said the
Badger, impressively, ‘that leads from the river-bank right up
into the middle of Toad Hall.’
</p><p>
‘Oh, nonsense, Badger,’ said Toad. ‘I know every inch of
Toad Hall, inside and out. There is nothing of the sort!’
</p><p>
‘My young friend,’ said the Badger, with great severity,
‘your father, who was a great animal — a lot better than some
others I know — was my special friend. He discovered that
passage — he didn’t make it, of course — and he repaired it
and cleaned it out; and he showed it to me. “Don’t let my son
know about it,” he said. “He’s a good boy, but very light in character, and simply cannot hold his tongue. Don’t tell him about
it till he really needs it.”’
</p><p>
‘Well, well,’ Toad said; ‘of course I talk a lot. A popular fellow such as I am — my friends get round me — we chatter,
we tell funny stories — and somehow my tongue gets too 
free. Never mind. Go on, Badger. How’s this passage going to
help us?’
</p><p>
‘I found out that there’s going to be a big banquet tomorrow night,’ continued the Badger. ‘It’s somebody’s
birthday — the Chief Weasel’s, I think — and all the weasels will be together in the dining-hall, eating and having
fun. No guns, no sticks, no arms of any sort!’
</p><p>
‘But the guards will keep watch as usual,’ said the Rat.
</p><p>
‘Exactly,’ said the Badger; ‘And that is where the passage
comes in. It leads right up under the cupboard, next to the dining-hall!’
</p><p>
‘Aha! that old board!’ said Toad. ‘Now I understand it!’
</p><p>
‘We will come out quietly—’ cried the Mole.
</p><p>
‘—with our pistols and sticks—’ said the Rat.
</p><p>
‘—and rush in upon them,’ said the Badger.
</p><p>
‘—and beat them, and beat them, and beat them!’ cried the
Toad in ecstasy, running round and round the room, and jumping over the chairs.
</p><p>
‘Very well, then,’ said the Badger, with his usual dry manner
, ‘now go to bed. We will make all the necessary arrangements in the course of the morning tomorrow.’
</p><p>
Toad slept till a late hour next morning. He found the
Badger in the armchair, reading the paper, and not thinking
the least about what was going to happen that very evening.
The Rat was running round the room busily, with his arms
full of weapons of every kind, putting them in four little piles
on the ﬂoor, and saying excitedly, ‘Here’s-a-stick-for-the-Rat,
here’s-a-stick—for-the Mole, here’s-a-stick-for-the-Toad, here’s-
a-stick—for-the-Badger! Here’s-a-pistol-for-the-Rat, here’s-a-
pistol-for-the-Mole, here’s-a-pistol-for-the-Toad, here’s-a-pis-
tol-for-the-Badger!’ and so on.
</p><p>
‘That’s all very well, Rat,’ said the Badger, looking at the
busy little animal over the edge of his newspaper; ‘But just
let us once get past the stoats, with their ugly guns, and we
won’t need any sticks or pistols. We four, with our sticks,
once we’re inside the dining-hall, will clear the ﬂoor of all
of them in five minutes.’
</p><p>
‘Better be on the safe side,’ said the Rat, polishing a pistol.
</p><p>
Then the Mole came into the room. ‘I had such fun!’ he
began at once; ‘I frightened the stoats!’
</p><p>
‘I hope you were very careful, Mole,’ said the Rat ner-
vously.
</p><p>
‘I hope so, too,’ said the Mole. ‘I found that Toad’s washerwoman dress hanging before the fire. So I put it on, and
the hat, and the shawl, and went off to Toad Hall. The guards
were there, of course, with their guns and their “Who comes
there?” and all the rest of their nonsense. “Good morning,
gentlemen!” said I, very respectfully. “Do you want any
washing today?”
</p><p>
‘They said, “Go away, washerwoman! We don’t do any
washing on duty.”
</p><p>
“‘Or any other time?” said. Ho, ho, ho! Wasn’t I FUNNY,
Toad?’
</p><p>
‘Careless animal!’ said Toad, very hotly. He felt very jealous of Mole’s act.
</p><p>
‘Some of the stoats turned quite pink,’ continued the
Mole, ‘and the Sergeant, he said to me, “Now run away, my
good woman, run away! Don’t keep my men talking on their
posts.” “Run away?” said I; “it won’t be me that’ll be running
away, in a very short time!”’
</p><p>
‘O MOLY, how could you?’ said the Rat in alarm.
</p><p>
The Badger laid down his paper.
</p><p>
‘I could see them pricking up their ears and looking
at each other,’ went on the Mole; ‘and the Sergeant said
to them, "Never mind HER; she doesn’t know what she’s
talking about."’
</p><p>
“‘O! don’t I?"’ said. “Well, let me tell you this. My daughter,
she washes for Mr. Badger, so I know what I’m talking about;
and YOU’LL know pretty soon, too! A hundred angry badgers
with guns are going to attack Toad Hall this very night. Rats
with pistols will come up the river and land in the garden;
and a group of dangerous Toads will attack the house!" Then I
ran away, and I hid; and then I came secretly back and looked
at them through the hedge. They were all as nervous as they
could be, running all ways at once, and falling over each other,
and every one giving orders to everybody else and not listen-
ing; and the Sergeant kept sending off parties of stoats to distant parts of the grounds, and then sending other fellows to
take them back again; and I heard them saying to each other,
”That’s just like the weasels; they’re to have fun, and food, and
toasts, and songs, while we must keep watch in the cold and
the dark, and meet angry Badgersl’"
</p><p>
‘Oh, you fool, Mole!’ cried Toad, ‘You spoilt everything!’
</p><p>
‘Mole,’ said the Badger, in his dry, quiet way, ‘I must say
you have more brains in your little finger than some other
animals have in the whole of their fat bodies. Good Mole!
Clever Mole!’
</p><p>
The Toad was simply wild with jealousy. He couldn’t un-
derstand what the Mole did that was so very clever. Fortunately for him, the bell rang for lunch.
</p><p>
It was a simple meal, after which the Badger fell asleep,
and the nervous Rat continued his preparations, and started running between his four little piles. And the Mole took 
Toad by his arm, led him out into the open air, sat him into
</p><p>
a chair, and made him tell him all his adventures from beginning to end, which Toad was only too glad to do.
</p>
</div>


<h3><span>CHAPTER XII</span> The Return of Ulysses</h3>
<div>



<p>
When it began to grow dark, the Rat, with excitement
 and mystery, gathered them into the room, 
 stood each of them in front of his little pile, and
tried to dress them up for the coming expedition. He was
very serious about it, and it took quite a long time.
</p><p>
When all was quite ready, the Badger took a dark lamp
in one paw, his great stick in the other, and said, ‘Now then,
follow me! Mole first; Rat next; Toad last. And look here,
Toady! Don’t you chatter so much as usual, or you’ll go back!’
</p><p>
The Badger led them along by the river for a little way,
and then suddenly jumped into a hole in the river-bank, a
little above the water. The Mole and the Rat followed silently, but when it came to Toad’s turn, of course he managed to
slip and fall into the water with a loud splash and a cry of
alarm. His friends caught him, rubbed him down and comforted him; but the Badger was really angry.
</p><p>
So at last they were in the secret passage, and the expedition really began!
</p><p>
It was cold, and dark, and wet, and low, and narrow, and
poor Toad began to shake, partly from horror, partly because he was wet through. The lamp was far ahead, and
he kept behind a little in the darkness. Then he heard the
Rat call out, ‘COME on, Toad!’ and he ‘came on’ with such a
rush that he upset the Rat into the Mole and the Mole into
the Badger, and for a moment all was confusion. The Badger thought somebody attacked them from behind, and, as
there was no space to use a stick, drew a pistol, and was
ready to put a bullet into Toad. When he found out the cause
of the confusion he was very angry indeed, and said, ‘Now
this time that tiresome Toad WILL stay behind!’
</p><p>
But Toad cried, and the other two promised to look after him, and at last the Badger agreed, and the procession
moved on; only this time the Rat was the last, with a strong
hand on the Toad’s shoulder.
</p><p>
So they went along, with their ears up and their paws
on their pistols. Then suddenly they heard, far away and
yet nearly over their heads, a mixed murmur of sound, as if
people were cheering and stamping on the ﬂoor and beating on tables. The Badger said, ‘They ARE having a banquet!’
</p><p>
The passage now began to go up; and then the noise broke
out again, quite distinct this time, and very close above them.
They hurried along the passage till they found themselves
standing under the door that led up into the servants’ room.
</p><p>
Such a great noise was going on in the banqueting-hall
that nobody could hear them. The Badger said, ‘Now, boys,
all together!’ and the four of them put their shoulders to
the door and opened it. They found themselves standing
in a small room, with only a door between them and the
banqueting-hall.
</p><p>
The noise, as they entered from the passage, was great. At
last, as the cheering and beating slowly became lower, a voice
said, ‘Well, I do not want to speak much longer’ — (great
applause) — ‘but before I take my seat’ — (new cheering) —
‘I’d like to say one word about our kind host, Mr. Toad. We all
know Toad!’ — (great laughter) — ‘GOOD Toad, KIND Toad,
HONEST Toad!’ (laughter).
</p><p>
‘Only just let me get at him!’ said Toad.
</p><p>
‘Get ready, all of you!’ said the Badger.
</p><p>
‘—Let me sing you a little song,’ went on the voice, ‘which
I composed on the subject of Toad’—(long applause).
</p><p>
When the ChiefWeasel — for it was he — began singing,
the Badger took his stick with both paws, looked round at
his friends, and cried—
</p><p>
‘It’s time! Follow me!’
</p><p>
And opened the door wide.
</p><p>
What a noise and cries filled the air!
</p><p>
How the weasels dived under the tables and jumped
madly up at the windows! How the ferrets ran wildly for
the fireplace to hide in the chimney! How tables and chairs
were upset, in the panic of that terrible moment when the
four Heroes ran into the room! The mighty Badger, his great
stick flying through the air; Mole, black and severe, with his
terrible war-cry, ‘A Mole! A Mole!’ Rat, brave and confident,
with weapons of every sort at his belt; Toad, jumping into
the air! There were only four of them, but to the terrified
weasels the hall seemed full of monstrous animals, grey,
black, brown and yellow, jumping with enormous sticks;
and they ﬂed in terror, this way and that, through the windows, up the chimney, anywhere to get from those terrible
sticks.
</p><p>
It was all over in five minutes. Through the broken windows came the cries of terrified weasels escaping across
the lawn; on the ﬂoor lay some dozen or so of the enemy,
on whom the Mole was busily fitting handcuffs. The Badger
was resting from his labors.
</p><p>
‘Mole,’ he said, ‘you’re the best of fellows! Just come outside and look after those stoat-guards, and see what they’re
doing. I think that, thanks to you, we won’t have much trouble from them tonight!’
</p><p>
The Mole went through a window; and the Badger asked
the others, ‘Could you two set a table on its legs again, pick
up knives and forks and plates and glasses from the floor,
and look for some food?’ They were ready to sit down to
supper when the Mole came in through the window, laughing, with an armful of riﬂes.
</p><p>
‘It’s all over,’ he said. ‘As soon as the stoats heard the
cries and the noise inside the hall, some of them threw
down their riﬂes and ran away. The others stood for a bit,
but when the weasels came rushing out upon them and the
stoats mixed with the weasels, they all disappeared. Here
are their rifles.’
</p><p>
‘Excellent animal!’ said the Badger, his mouth full of
chicken. ‘Now, there’s just one more thing Iwant you to
do, Mole, before you sit down to your supper with us; and
I don’t want to trouble you only Iknow you will do it all
right. Iwant you to take those fellows on the ﬂoor there
upstairs with you, and make them clean the bedrooms and
tidy up. See that they wash UNDER the beds, and put clean
sheets and pillow-cases on, and have a can of hot water,
and clean towels, and soap in each room. And then you can
give them a beating, if you like, and put them out by the 
back-door, and we won’t see any more of THEM, Ithink.
And then come along and have some of this cold tongue.
It’s first class.’
</p><p>
The Mole picked up a stick, formed his prisoners up in
a line on the ﬂoor, gave them the order ‘Quick march!’ and
led them off to the upper ﬂoor. After a time, he appeared
again, smiling, and said, ‘The rooms are clean and ready!
And I didn’t beat them. They had enough for one night, and
the weasels quite agreed with me. They said, “We’re very
sorry. It was all because of the Chief Weasel and the stoats.
We’re happy to be of any service to you.” So I gave them a
roll apiece, and let them out at the back, and off they ran, as
hard as they could!’
</p><p>
Then the Mole pulled his chair up to the table, and ate the
cold tongue; and Toad put all his jealousy from him, and said
heartily, ‘Thank you kindly, dear Mole, for all your trouble
tonight, and especially for your cleverness this morning!’ The
Badger liked that, and said, ‘There spoke my brave Toad!’ So
they finished their supper in great joy and peace, and then
went to rest between clean sheets, safe in Toad’s home.
</p><p>
The following morning, the Badger said to Toad: ‘I’m
sorry, Toad, but I’m afraid there’s a heavy morning’s work
in front of you. You see, we really must have a Banquet at
once, to celebrate the victory. It’s the rule.’
</p><p>
‘O, all right!’ said the Toad, readily. ‘Anything you want.
But why do you want to have a Banquet in the morning?’
</p><p>
‘Don’t try to look stupider than you really are,’ replied
the Badger; ‘The Banquet will be at night, ofcourse, but you
must write and send the invitations at once. Now, sit down
at that table — there’s letter-paper on it, with “Toad Hall” at 
the top in blue and gold — and write invitations to all our
friends. And I’LL order the Banquet.’
</p><p>
The Badger left the room in the direction of the kitchen,
and as soon as the door closed behind him, Toad hurried to the
writing-table. A fine idea came to him. He WILL write the invitations; and he will take care to tell about the leading part he
took in the fight, and how he knocked down the Chief Weasel;
and he will speak of his adventures. It will be like this —
</p><p>
SPEECH. . . . BY TOAD.
</p><p>
(There will be other speeches by TOAD during the evening.)
</p><p>
ADDRESS. . . BY TOAD
</p><p>
SYNOPSIS — Our Prison System — the Waterways of
Old England — Horse-selling, and how to sell — Property,
its rights and its duties — Back to the Land — A Typical
English Squire.
</p><p>
SONG. . . . BY TOAD. (Author: Toad.) OTHER COMPOSITIONS. BY TOAD
</p><p>
The idea pleased him very much, and he worked very
hard and wrote all the letters by noon. One of the weasels
was to deliver them. When the other animals came back
to lunch, very fresh after a morning on the river, the Mole
looked at Toad, expecting to find him depressed. Instead,
he was so happy that the Mole grew nervous; while the Rat
and the Badger exchanged looks.
</p><p>
As soon as the meal was over, Toad put his paws deep
into his trouser-pockets, and said, ‘Well, look after yourselves, you fellows! Ask for anything you want!’ and was
going off in the direction of the garden, where he wanted to
think out an idea or two for his coming speeches, when the
Rat caught him by the arm.
</p><p>
Toad did his best to get away; but when the Badger took
him firmly by the other arm he began to see that the game
was up. The two animals put him into a chair. Then they
both stood in front of him, while Toad sat silent and looked
at them darkly.
</p><p>
‘Now, look here, Toad,’ said the Rat. ‘We want you to understand clearly, once and for all, that there are going to be
no speeches and no songs. Try and understand the fact that
this time we’re not arguing with you; we’re just telling you.’
</p><p>
Toad saw that they saw through him.
</p><p>
‘May I sing them just one LITTLE song?’ he asked.
</p><p>
‘No, not ONE little song,’ answered the Rat firmly, though
his heart hurt as he noticed the trembling lip of the poor
Toad. ‘It’s no good, Toady; you know well that your songs
are all too proud and self-important; and your speeches are
all self-praise and — and — well, and not true and — and—’
</p><p>
‘And rubbish,’ put in the Badger, in his common way.
</p><p>
‘It’s all for your own good, Toady,’ went on the Rat. ‘You
know you MUST turn over a new leaf sooner or later, and
now seems a splendid time to begin; a sort ofturning-point
in your career. Please don’t think that saying all this doesn’t
hurt me more than it hurts you.’
</p><p>
Toad stood a long while in thought. At last he raised his
head, and the strong emotions were on his face. ‘You win, my
friends,’ he said. ‘You are right, Iknow, and am wrong. So
now I will be a very different Toad. My friends, you will never
blush for me again. But, O dear, O dear, this is a hard world!’
</p><p>
And, pressing his handkerchief to his face, he left the
room, with shaking footsteps.
</p><p>
‘Badger,’ said the Rat, ‘I feel like a monster; I wonder
what YOU feel like?’
</p><p>
‘O, I know, I know,’ said the Badger sadly. ‘But we must do
it. This good fellow will live here, and have respect. Would
you like him to be a common laughing-stock, for stoats and
weasels?’
</p><p>
‘Of course not,’ said the Rat. ‘And, talking of weasels, it’s
lucky we came upon that little weasel, just as he was setting
out with Toad’s invitations. They were simply ugly. I con-
fiscated them, and the good Mole is now writing the new
ones’
</p><p>
At last the hour for the banquet began to draw near,
and Toad was still sitting in his bedroom. Gradually his face
cleared, and he began to smile. Then he took to laughing in
a quiet manner. At last he got up, locked the door, drew the
curtains across the windows, collected all the chairs in the
room and arranged them around, and took up his position
in front ofthem. Then he bowed, and, letting himself go, he
sang his last little song to the audience that his imagination
so clearly saw.
</p><p>
Then he went quietly down the stairs to greet his guests.
</p><p>
